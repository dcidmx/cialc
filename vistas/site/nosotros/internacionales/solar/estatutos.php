               <h5>De los objetivos</h5>
               <ol>
                  <li>Fomentar, coordinar y difundir el estudio sobre la realidad social o cultural de América Latina y el Caribe.</li>
                  <li>Vincular el estudio de América Latina y el Caribe como instrumento necesario para enfrentar adecuadamente las relaciones internacionales en los aspectos económicos, políticos, sociales y culturales. En tal virtud, ese estudio no se limitará al conocimiento de estados y/o sociedades aisladas, sino que debe procurar un análisis a escala supranacional y/o continental.</li>
                  <li>Procurar que los conocimientos alcanzados a través de estos estudios trasciendan diversos sistemas y niveles de educación, acción cultural y medios de comunicación de los países de América Latina y el Caribe.</li>
                  <li>Trabajar para que en todos los países del área se otorgue especial énfasis y se establezca como obligatorio el estudio y conocimiento de la realidad latinoamericana y el Caribe.</li>
                  <li>Apoyar la existencia y fomentar la creación de instituciones que contribuyan a la realización de los objetivos propuestos.</li>
                  <li>Promover los encuentros necesarios para la consecución de los mismos propósitos.</li>
               </ol>
               <h5>De los Congresos</h5>
               <p align="justify">Se celebrarán cada dos años en un país distinto de América Latina y el Caribe.</p>
               <p align="justify">Se propiciará que las sedes sean ciudades pertenecientes a las diferentes regiones de América Latina y el Caribe.</p>
               <h5>De la Asamblea general</h5>
               <p align="justify">Se reunirá cada dos años en un país distinto de América Latina o el Caribe, en el marco del Congreso.</p>
               <h5>De la Coordinación general</h5>
               <p align="justify">La sede permanente de la SOLAR es la Universidad Nacional Autónoma de México, a través del Centro Coordinador y Difusor de Estudios Latinoamericanos (acta constitutiva de la SOLAR del 11 de agosto de 1982 y oficio del 12 de agosto de 1982 de Darcy Ribeiro al rector de la UNAM).</p>
               <p align="justify">El CCyDEL, de manera institucional, tiene la responsabilidad de la coordinación general de todas las actividades de la SOLAR, en apoyo del presidente en turno.</p>
