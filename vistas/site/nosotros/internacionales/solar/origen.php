                        <p align="justify">En 1976 la Organización de las Naciones Unidas para la Educación, la Ciencia y la Cultura (siglas en inglés UNESCO) convocó a una reunión, en su sede de París, para estudiar la posibilidad de integrar los estudios que se realizaban sobre América Latina. A fines del mes de noviembre y principios de diciembre de 1978, con el patrocinio de la Universidad Nacional Autónoma de México (UNAM) e inspiración del Dr. Leopoldo Zea, se realizó el I Simposio para la Coordinación y Difusión de los Estudios Latinoamericanos, en la Ciudad de México. De la amplia cantidad de reflexiones y propuestas contenidas en sus conclusiones, destaca el acuerdo de crear la Sociedad Latinoamericana de Estudios sobre América Latina y el Caribe, entonces con las siglas SLEALC. Como hubo participantes de Estados Unidos y Europa, también se decidió crear la Federación de Estudios sobre América Latina y el Caribe. Estas dos organizaciones le solicitaron a la UNAM que fuera sede permanente de un órgano coordinador y ejecutor de las resoluciones de las mismas. Así, en el año de 1982, por acuerdo del Rector Guillermo Soberón, la Universidad aceptó la sede del Centro Coordinador y Difusor de Estudios Latinoamericanos (CCyDEL), proporcionó las instalaciones y lo dotó de elementos académicos y administrativos para su funcionamiento.</p>
                        <p align="justify">Para concretar la decisión de crear la SLEALC se nombró una comisión encargada de elaborar el anteproyecto de estatutos, el cual sería presentado y discutido en el II Simposio, que tuvo como sede la Universidad Simón Bolívar de Caracas, Venezuela, durante el mes de junio de 1980. Uno de los acuerdos de esta reunión fue determinar que en el III Simposio se aprobara el proyecto definitivo, lo cual sucedió en Río de Janeiro en agosto de 1982, donde se decidió que la sigla oficial de la Sociedad Latinoamericana de Estudios sobre América Latina y el Caribe fuera SOLAR, por pedido de su presidente fundador, Darcy Ribeiro.</p>
