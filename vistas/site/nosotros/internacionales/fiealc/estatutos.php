               III- DE SUS ÓRGANOS Y FUNCIONAMIENTO
               </a>
            </h4>
         </div>
         <div class="panel-collapse collapse" id="faq_2_3">
            <div class="panel-body">
               <ol>
                  <li>
                     La Federación Internacional de Estudios sobre América Latina y el Caribe está constituida por los siguientes órganos:
                     <ol>
                        <li>Comité Ejecutivo</li>
                        <li>Coordinación General</li>
                        <li>Asamblea Consultiva</li>
                     </ol>
                  </li>
                  <h6>Del Comité Ejecutivo</h6>
                  <li>Los miembros federados, de acuerdo con su organización y estatuto, designarán representante al Comité Ejecutivo de la Federación.</li>
                  <li>El Comité Ejecutivo estará formado por un presidente, un coordinador y tantos vocales como miembros forman parte de la Federación.</li>
                  <li>La presidencia será rotatoria entre las instituciones que realicen los simposios y la Asamblea Consultiva. El presidente durará en su función hasta el término de cada Asamblea Consultiva.</li>
                  <li>El presidente tendrá como funciones presidir el Comité Ejecutivo y la Asamblea, convocar a las reuniones y asambleas y cuidar del cumplimiento de las propuestas y recomendaciones, a través de la Coordinación General.</li>
                  <li>El Comité Ejecutivo se reunirá cuando menos cada dos años en el lugar que se acuerde, planeará en esas reuniones programas a efectuar y tomará cuenta de lo realizado y probará igualmente la participación de la Federación en las asambleas que realicen sus miembros.</li>
                  <h6>De la Coordinación General y su sede</h6>
                  <li>El Coordinador general será designado por el Comité Ejecutivo y durará en su puesto cuatro años, pudiendo ser reelegido una vez. Sus funciones serán las de hacer cumplir los acuerdos y recomendaciones hechas por el Comité, así como las que surjan de la Asambleas Consultivas, informará al Comité Ejecutivo de la obra realizada en cada una de sus reuniones periódicas, mantendrá el intercambio y la información que la Federación se haya propuesto y representará, igualmente, a la Federación en las reuniones en las que participe.</li>
                  <li>La Coordinación General tendrá una sede permanente para la mejor conveniencia de la marcha de las funciones que le sean encomendadas.</li>
                  <h6>De la Asamblea Consultiva</h6>
                  <li>De especial importancia para los fines propuestos por la Federación será la Asamblea Consultiva, que deberá reunirse al menos cada tres años, convocada especialmente por el presidente de la Federación.</li>
                  <li>A esta Asamblea podrán asistir representantes de todas las instituciones asociadas.</li>
               </ol>
