              <h4>TRANSITORIOS</h4>
              <p align="justify"><em>PRIMERO.-</em> Los profesores de educación física, serán reclasificados como profesores de asignatura, de conformidad con las normas establecidas por el presente Estatuto, a más tardar el 31 de diciembre de 1974.</p>

              <p align="justify"><em>SEGUNDO.-</em> Los técnicos académicos y los ayudantes de profesor o de investigador, serán reclasificados en las nuevas categorías y niveles previstos en este Estatuto, a más tardar el 31 de diciembre de 1974.</p>

              <p align="justify"><em>TERCERO.-</em> Los consejos técnicos de las dependencias podrán autorizar que los ayudantes de profesor que a la iniciación de la vigencia de este Estatuto impartan cátedra en exceso de los porcentajes a que se refiere el artículo 28, puedan continuar haciéndolo hasta el día último de diciembre de 1975.</p>

              <p align="justify"><em>CUARTO.-</em> Los nuevos derechos que este Estatuto establece a favor del personal académico, que impliquen erogaciones no previstas en el presupuesto correspondiente a 1974, empezarán a regir a partir del 1o. de enero de 1975, con el objeto de que la Universidad procure los recursos necesarios en el próximo presupuesto.</p>

              <p align="justify"><em>QUINTO.-</em> La Universidad Nacional Autónoma de México a través de sus órganos competentes, procederá a elaborar los instructivos que adecuen los principios generales contenidos en el presente Estatuto a la situación de los profesores de enseñanza media superior.</p>

              <p align="justify"><em>SEXTO.-</em> Los asuntos cuyo trámite fue iniciado con anterioridad a la aprobación del presente Estatuto, continuarán tramitándose hasta su conclusión de conformidad con el Estatuto del 16 de diciembre de 1970.</p>

              Las comisiones dictaminadoras previstas en el presente Estatuto deberán constituirse en un plazo no mayor de seis meses, contados a partir de la fecha de su aprobación, mientras tanto continuarán en funciones las constituidas según el Estatuto de 1970.

              <p align="justify"><em>SÉPTIMO.-</em> Este Estatuto aprobado en el Consejo Universitario en su sesión ordinaria celebrada el día 28 de junio de 1974, abroga al Estatuto del Personal Académico del 16 de diciembre de 1970 e iniciará su vigencia el día siguiente al de su publicación en la "Gaceta UNAM".</p>

              Aprobado en sesión del Consejo Universitario el día 28 de junio de 1974.
              Publicado en Gaceta UNAM el día 5 de julio de 1974.

              <p align="justify"><em>OCTAVO.-</em> (Adicionado en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>

              <p align="justify"><em>OCTAVO.-</em> Los técnicos académicos serán reclasificados en los términos del artículo segundo transitorio del presente Estatuto y de acuerdo con los requisitos académicos establecidos.</p>

              La reclasificación tendrá efectos retroactivos al 1o. de enero de 1975 y en ningún caso podrá disminuirse la remuneración actual.










              <h4>TRANSITORIOS</h4>
              <p align="justify"><em>PRIMERO.-</em> Los profesores de carrera o de asignatura que al momento de entrar en vigor estas reformas impartan un número mayor de horas de clase que las señaladas como máximo en la fracción VII del artículo 6o., o en el artículo 35 en su caso, siempre que la asignación de las mismas derive de la aplicación de normas hasta ahora existentes en la Legislación Universitaria, podrán conservarlas pero no aumentarlas.</p>

              <p align="justify"><em>SEGUNDO.-</em> Se conservará la definitividad de los profesores de carrera de enseñanza media superior y de los profesores de asignatura que, con esa calidad, hayan obtenido la categoría de profesores de carrera de tiempo completo.</p>

