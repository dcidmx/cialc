              <h4>
                     NORMATIVIDAD ACADÉMICA DE LA UNAM
                     PERSONAL ACADÉMICO
                     ESTATUTO DEL PERSONAL ACADÉMICO DE LA UNIVERSIDAD NACIONAL AUTÓNOMA DE MÉXICO
              </h4>


              ----------------------------------------------------------------




              <h4>TÍTULO PRIMERO</h4>
              <h5>Disposiciones Generales</h5>




              <p align="justify"><em>Artículo 1o.-</em> Este Estatuto regirá las relaciones entre la Universidad y su personal académico, de acuerdo con lo dispuesto en los artículos 13 y 14 de la Ley Orgánica y en el Título Cuarto del Estatuto General de la UNAM.</p>




              <p align="justify"><em>Artículo 2o.-</em> Las funciones del personal académico de la Universidad son: impartir educación, bajo el principio de libertad de cátedra y de investigación, para formar profesionistas, investigadores, profesores universitarios y técnicos útiles a la sociedad; organizar y realizar investigaciones principalmente acerca de temas y problemas de interés nacional, y desarrollar actividades conducentes a extender con la mayor amplitud posible los beneficios de la cultura, así como participar en la dirección y administración de las actividades mencionadas.</p>




              <p align="justify"><em>Artículo 3o.-</em> La enseñanza de las asignaturas que forman parte de los planes de estudio para el otorgamiento de grados académicos, títulos o diplomas, se impartirá bajo el control académico de las facultades y escuelas que enumera el artículo 8. del Estatuto General de la Universidad.</p>

              <p align="justify">La investigación y labores conexas que realice el personal académico se desarrollarán en los institutos, en las facultades y escuelas que enumera el Estatuto General, en las coordinaciones de Ciencias y Humanidades y en los centros que dependan de ellas. Las enseñanzas complementarias se podrán llevar a cabo en las dependencias citadas y en los centros y unidades de extensión universitaria.</p>




              <p align="justify"><em>Artículo 4o.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, publicado Gaceta UNAM el 2 de julio del mismo año, como sigue):</p>




              <p align="justify"><em>Artículo 4o.-</em> El personal académico de la Universidad estará integrado por:</p>

              Técnicos académicos;

              Ayudantes de profesor o de investigador;

              Profesores e investigadores.




              <p align="justify"><em>Artículo 5o.-</em> El personal académico podrá laborar mediante nombramiento interino o definitivo o por contrato de prestación de servicios.</p>




              <p align="justify"><em>Artículo 6o.-</em> Serán derechos de todo el personal académico:</p>

							<ol>
							<li>Realizar sus actividades de acuerdo con el principio de libertad de cátedra e investigación, de conformidad con los programas aprobados por el respectivo consejo técnico, interno o asesor;</li>

              <li>Percibir la remuneración correspondiente a su nombramiento o contrato; los aumentos generales y los establecidos por razón de antigüedad;</li>

              <li>Obtener, de acuerdo con los recursos presupuestales disponibles y en forma independiente de la promoción a categorías o niveles más elevados, los aumentos que conceda la UNAM al revisar bienalmente los sueldos del mismo personal académico;</li>

              <li>Recibir las prestaciones que les otorguen la Ley del Instituto de Seguridad y Servicios Sociales de los Trabajadores del Estado y las demás disposiciones legales aplicables;</li>

              <li>Conservar su adscripción de dependencia, su categoría y nivel, pudiendo ser cambiados únicamente de acuerdo con los procedimientos que establece este Estatuto;</li>

              <li>Laborar 40 horas a la semana cuando se trate de personal de tiempo completo y 20 horas semanarias cuando sea de medio tiempo;</li>

              <li>(Modificada en las sesiones del Consejo Universitario del 28 de marzo de 1985 y 22 de marzo de 1988, publicada en la Gaceta UNAM el 14 de abril de 1988, como sigue):</li>

              <li>En ningún caso podrá encomendarse a un profesor enseñanza oral por más de 30 horas a la semana en el nivel bachillerato o de 18 horas a la semana en los niveles profesional y de posgrado. En los casos anteriores, podrán autorizarse horas adicionales de enseñanza práctica efectiva frente a grupo, sin que la suma total exceda de 40 horas semanales. Cuando se trate exclusivamente de enseñanza práctica el máximo será también de 40 horas a la semana;</li>

              <li>El tiempo total de servicios que preste el personal académico a la Universidad en cualquier cargo, no podrá exceder de 48 horas semanales;</li>

              <li>Disfrutar con goce de salario los días de descanso obligatorio que determinen las leyes;</li>

              <li>Disfrutar de 40 días naturales de vacaciones al año de acuerdo con el calendario escolar y, en su caso, con el calendario de actividades de la dependencia a la que estén adscritos, y recibir la prima correspondiente;</li>

              <li>Gozar de licencias en los términos de este Estatuto y de las demás disposiciones aplicables;</li>

              <li>Disfrutar en total de 90 días naturales de descanso, repartidos antes y después del parto, percibiendo salario íntegro;</li>

              <li>En caso de defunción, la UNAM entregará a la persona o personas que el miembro del personal académico hubiere señalado, o en caso de omisión a sus herederos legales, el importe de cinco meses de salario si tenía una antigüedad de diez a menos de quince años de servicios al ocurrir su fallecimiento, o de seis meses de salario en caso de que su antigüedad fuese de quince o más años de servicios;</li>

              <li>Recibir de la UNAM al jubilarse, independientemente de cualquier otra prestación, una gratificación conforme a la siguiente tabla:</li>
	            <ol>
							<ol>
              <li>on una antigüedad en la UNAM de cinco a menos de veinte años, dos meses de salario;</li>

              <li>Con una antigüedad en la UNAM de veinte a menos de veinticinco años, cuatro meses de salario;</li>

              <li>Con una antigüedad en la UNAM de veinticinco años en adelante, seis meses de salario;</li>
						  </ol>
						  </ol>
              <li>Manifestar su situación académica dentro y fuera de la UNAM y hacer uso de la toga universitaria de acuerdo con el reglamento respectivo;</li>

              <li>Recibir las distinciones, estímulos y recompensas que les correspondan de acuerdo con la Legislación Universitaria;</li>

              <li>Votar en los términos que establecen los reglamentos respectivos, para la integración de los consejos técnicos y Universitario u otros cuerpos colegiados y, en su caso, formar parte de dichos órganos;</li>

              <li>Ser notificado de las resoluciones que afecten su situación académica en la UNAM e inconformarse de ellas, con arreglo a la Legislación Universitaria aplicable;</li>

              <li>Conservar los derechos que este Estatuto, les confiere, cuando sean nombrados por la Junta de Gobierno o por el Rector de la Universidad, para el desempeño de un cargo académico-administrativo de tiempo completo;</li>

              <li>Organizarse en forma libre e independiente de conformidad con las disposiciones de la Ley Orgánica y del Estatuto General de la Universidad, y</li>

              <li>Percibir por trabajos realizados al servicio de la Universidad las regalías que les correspondan por concepto de derechos de autor y/o de propiedad industrial.</li>
						  </ol>



              <p align="justify"><em>Artículo 7o.-</em> Los miembros del personal académico deberán recabar autorización previa y escrita de los directores, consejos técnicos u otras autoridades universitarias competentes, para gestionar ayuda económica en beneficio de la Universidad, de cualesquiera personas o instituciones.</p>




              <p align="justify"><em>Artículo 8o.-</em> Todo lo relativo al personal académico de los centros de extensión universitaria, se establecerá en el reglamento respectivo.</p>



              <h4>TÍTULO SEGUNDO</h4>
              <h5>De los Técnicos Académicos</h5>



              <h4>CAPÍTULO I</h4>
							<h5>Definición, Niveles y Requisitos</h5>



              <p align="justify"><em>Artículo 9o.-</em> Son técnicos académicos ordinarios quienes hayan demostrado tener la experiencia y las aptitudes suficientes en una determinada especialidad, materia o área, para realizar tareas específicas y sistemáticas de los programas académicos y/o de servicios técnicos de una dependencia de la UNAM.</p>




              <p align="justify"><em>Artículo 10.-</em> Son técnicos académicos visitantes los invitados por la Universidad para el desempeño de funciones técnico-académicas específicas por un tiempo determinado. En ese lapso podrán recibir remuneración de la Universidad.</p>




              <p align="justify"><em>Artículo 11.-</em> Los técnicos académicos ordinarios podrán tener nombramiento interino, definitivo, o laborar por contrato y ser de tiempo completo o de medio tiempo.</p>



              <p align="justify"><em>Artículo 12.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, publicado en Gaceta UNAM el 2 de julio del mismo año, como sigue):</p>




              <p align="justify"><em>Artículo 12.-</em> Los técnicos académicos ordinarios podrán ocupar cualquiera de las siguientes categorías:</p>
              <ol><ol>
              <li>Auxiliar;</li>

              <li>Asociado;</li>

              <li>Titular.</li>
						  </ol></ol>
              En cada categoría habrá tres niveles: "A", "B" y "C"




              <p align="justify"><em>Artículo 13.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, publicado en Gaceta UNAM el 2 de julio del mismo año, como sigue):</p>




              <p align="justify"><em>Artículo 13.-</em> Los requisitos mínimos para ingresar como técnico académico de la categoría de auxiliar son:</p>
              <ol><ol>
              <li>Para el nivel "A", tener grado de bachiller o una preparación equivalente;</li>

              <li>Para el nivel "B", haber acreditado el 50% de los estudios de una licenciatura o tener una preparación equivalente;</li>

              <li>Para el nivel "C", haber acreditado todos los estudios de una licenciatura o tener una preparación equivalente.</li>
						  </ol></ol>
              Los requisitos mínimos para ingresar o ser promovido a la categoría de técnico académico asociado son:
              <ol><ol>
              <li>Para el nivel "A", tener grado de licenciado o preparación equivalente, haber trabajado un mínimo de un año en la materia o área de su especialidad;</li>

              <li>Para el nivel "B", tener grado de licenciado o preparación equivalente, haber trabajado un mínimo de un año en la materia o área de su especialidad y haber colaborado en trabajos publicados;</li>

              <li>Para el nivel "C", tener grado de licenciado o preparación equivalente, haber trabajado un mínimo de dos años en la materia o área de su especialidad y haber colaborado en trabajos publicados.</li>
						  </ol></ol>
              Los requisitos mínimos para ingresar o ser promovido a la categoría de técnico académico titular son:
              <ol><ol>
              <li>Para el nivel "A", tener grado de maestro o preparación equivalente y haber trabajado un mínimo de tres años en la materia o área de su especialidad;</li>

              <li>Para el nivel "B", tener grado de maestro o preparación equivalente, y haber trabajado un mínimo de dos años en tareas de alta especialización;</li>

              <li>Para el nivel "C", tener grado de doctor o preparación equivalente, haber trabajado un mínimo de cinco años en tareas de alta especialización, y haber colaborado en trabajos publicados.</li>
						  </ol></ol>
              Los consejos técnicos tomando en cuenta la opinión del consejo interno respectivo, establecerán las reglas y criterios para determinar lo que debe entenderse por preparación equivalente.



              <h4>CAPÍTULO II</h4><h5>Selección y Adscripción</h5>




              <p align="justify"><em>Artículo 14.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>




              <p align="justify"><em>Artículo 14.-</em> Para dictaminar sobre los nombramientos y promociones de los técnicos académicos, los consejos técnicos, internos o asesores, nombrarán comisiones dictaminadoras integradas por tres miembros propietarios y tres suplentes; o podrán decidir que sean las mismas que funcionan en relación con profesores e investigadores.</p>




              <p align="justify"><em>Artículo 15.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>




              <p align="justify"><em>Artículo 15.-</em> Para nombrar a los técnicos académicos se observará el siguiente procedimiento, que deberá concluirse en un plazo no mayor de dos meses a partir de la fecha de publicación de la convocatoria respectiva:</p>
              <ol><ol>
              <li>El director de la dependencia someterá a consideración del consejo técnico, interno o asesor, según el caso, la convocatoria respectiva y una vez aprobada ordenará la publicación en el órgano oficial de información de la UNAM, y dispondrá que se fije en lugares visibles de la dependencia;</li>

              <li>La convocatoria señalará los requisitos para ocupar la plaza, la fecha límite para recibir solicitudes que no podrá ser menor de 15 días hábiles y la clase de pruebas a que deberán sujetarse los candidatos para demostrar su aptitud y conocimientos;</li>

              <li>Las solicitudes serán presentadas en la propia dependencia en el lugar y forma que señale la convocatoria, y</li>

              <li>La comisión después de oír al director de la dependencia, emitirá un dictamen razonado en que se especificará el nombre de la persona a quien deba adjudicarse la plaza o la circunstancia de no haberse presentado candidato idóneo.</li>
					  	</ol></ol>


              <p align="justify"><em>Artículo 16.-</em> La resolución de la comisión dictaminadora se basará principalmente en la capacidad demostrada, en los antecedentes académico-técnicos, en la experiencia de los aspirantes y en las necesidades de la dependencia.</p>




              <p align="justify"><em>Artículo 17.-</em> Las resoluciones de la comisión dictaminadora serán sometidas al consejo técnico correspondiente para su ratificación.</p>




              <p align="justify"><em>Artículo 18.-</em> Los técnicos académicos tendrán los derechos y las obligaciones a que se refiere el Título Tercero, Capítulo III de este Estatuto.</p>

              Los técnicos académicos definitivos podrán desempeñar además, con remuneración adicional, puestos de confianza.




              <p align="justify"><em>Artículo 19.-</em> Los técnicos académicos al cumplir tres años de servicio ininterrumpido en una misma categoría y nivel, sin perjuicio de participar en cualquier concurso que se convoque, tendrán derecho a que se abra un concurso de oposición para promoción, con objeto de que se resuelva si es procedente otorgarles la definitividad o promoverlos.</p>



              <h4>TÍTULO TERCERO</h4>
              <h5>(La denominación de este Título, fue modificada en la sesión del Consejo Universitario del 10 de junio de 1975, para quedar como sigue):</h5>




              <h4>TÍTULO TERCERO</h4>
              <h5>De los Ayudantes de Profesor y de Investigador</h5>




              <h4>CAPÍTULO I</h4> <h5>Definición, Niveles y Requisitos</h5>




              <p align="justify"><em>Artículo 20.-</em> (Modificado en su primer párrafo en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>




              <p align="justify"><em>Artículo 20.-</em> Son ayudantes quienes auxilian a los profesores y los investigadores en sus labores. La ayudantía debe capacitar al personal para el desempeño de funciones docentes, o de investigación.</p>

              Los nombramientos de los ayudantes se otorgarán por un plazo no mayor de un año y podrán renovarse hasta por cuatro veces, siempre que hayan cumplido satisfactoriamente con sus labores y que así lo requieran los planes y programas de la dependencia a la que estén adscritos y los de formación de personal académico.

              El consejo técnico de cualquiera de las dependencias, con base en sus propias necesidades podrá aprobar la prórroga de los nombramientos de los ayudantes por un número mayor de años. La adscripción de los ayudantes se hará de acuerdo con las bases que fije al efecto el consejo técnico o interno en su caso, de la dependencia respectiva.




              <p align="justify"><em>Artículo 21.-</em> (Modificado en sus párrafos primero y tercero en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>




              <p align="justify"><em>Artículo 21.-</em> Los ayudantes de profesor serán nombrados por horas, y medio tiempo o tiempo completo. Los ayudantes de investigador serán designados por medio tiempo o tiempo completo.</p>

              Los ayudantes por horas podrán ocupar los niveles A o B y auxiliar a los profesores en una materia determinada, un curso específico o una sección académica, sin exceder de doce horas semanarias, salvo que por acuerdo especial del consejo técnico se autorice un número mayor de horas.

              Los ayudantes de profesor, o de investigador de medio tiempo o de tiempo completo podrán ocupar cualquiera de los niveles siguientes: A, B o C y realizarán las labores determinadas en los planes y programas de trabajo de la respectiva dependencia.




              <p align="justify"><em>Artículo 22.-</em> Para ingresar a los niveles a que se refiere el artículo anterior, se requiere:</p>
              <ol><ol>
              <li>Para el nivel A, haber acreditado cuando menos el 75% del plan de estudios de una licenciatura o tener la preparación equivalente a juicio del consejo técnico respectivo y un promedio no menor de 8 en los estudios realizados;</li>

              <li>Para el nivel B, además de satisfacer los requisitos exigidos para el nivel A, haber acreditado la totalidad del plan de estudios de una licenciatura o tener la preparación equivalente a juicio del consejo técnico respectivo;</li>

              <li>Para el nivel C, además de los requisitos para el nivel B, haber trabajado cuando menos un año como ayudante de profesor, de investigador, o de técnico académico.</li>
						  </ol></ol>


              <h4>CAPÍTULO II</h4> <h5>Selección, Promoción y Adscripción</h5>




              <p align="justify"><em>Artículo 23.-</em> Las comisiones que dictaminen sobre el ingreso o promoción de los profesores e investigadores, dictaminarán también sobre el ingreso y promoción de los ayudantes de medio tiempo y de tiempo completo, observando el procedimiento que en seguida se expresa, el cual deberá concluirse en un plazo no mayor de dos meses contados a partir de la fecha en que sea publicada la convocatoria correspondiente:</p>
              <ol><ol>
              <li>El director de la dependencia, con aprobación del respectivo consejo técnico, expedirá una convocatoria en los términos a que se refiere el inciso a) del artículo 15 de este Estatuto;</li>

              <li>La convocatoria deberá expresar el número de plazas, las materias, áreas y especialidades; los requisitos que deberán satisfacerse de acuerdo con este Estatuto y con la naturaleza de los servicios de que se trate, así como el término en que los interesados deberán entregar la documentación correspondiente, que no será menor de 15 días hábiles a partir de la fecha de publicación de la convocatoria;</li>

              <li>El director turnará la documentación, con su opinión, a la comisión dictaminadora;</li>

              <li>La comisión dictaminará quiénes deben cubrir la plaza o plazas o, en su caso, las declarará vacantes. El dictamen deberá ser ratificado por el consejo técnico.</li>
					  	</ol></ol>



              <p align="justify"><em>Artículo. 24.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>




              <p align="justify"><em>Artículo 24.-</em> Para nombrar y promover a los ayudantes de profesor por horas el consejo técnico a propuesta del director de la dependencia, establecerá el o los procedimientos que estime adecuados a las necesidades de la propia dependencia.</p>




              <p align="justify"><em>Artículo 25.-</em> Los ayudantes de medio tiempo y tiempo completo niveles A y B, sin perjuicio de que puedan concursar por una plaza de nivel superior, tendrán derecho a que después de un año ininterrumpido de labores, se abra un concurso de oposición que les dé la oportunidad de ser promovidos, siempre que reúnan los requisitos establecidos en este Estatuto y hayan cumplido con sus tareas académicas.</p>



              <h4>CAPÍTULO III</h4> <h5>Derechos y Obligaciones</h5>



              <p align="justify"><em>Artículo 26.-</em> Los técnicos académicos y los ayudantes de profesor o de investigador tendrán, además de los consignados en el artículo 6o. de este Estatuto, los siguientes derechos:</p>
              <ol><ol>
              <li>Recibir el crédito correspondiente por su participación en los trabajos colectivos, de acuerdo con el director del proyecto de que se trate;</li>

              <li>Conservar su horario de labores o solicitar el cambio del mismo. El director, en este último caso, resolverá atendiendo a las necesidades de la dependencia;</li>

              <li>Hacer valer su antigüedad;</li>

              <li>Recibir de la Universidad, remuneraciones adicionales provenientes de ingresos extraordinarios de su dependencia, de conformidad con el reglamento que al efecto se expida, y</li>

              <li>Los que señalen su nombramiento y la Legislación Universitaria.</li>
					  	</ol></ol>



              <p align="justify"><em>Artículo 27.-</em> Los técnicos y los ayudantes tendrán las siguientes obligaciones:</p>
              <ol><ol>
              <li>Prestar sus servicios, según el horario que señale su nombramiento y de acuerdo con los planes y programas de la dependencia a la que se encuentren adscritos;</li>

              <li>En su caso, coadyuvar en el plan de actividades del profesor o investigador del que dependan;</li>

              <li>Enriquecer y actualizar sus conocimientos;</li>

              <li>Abstenerse de impartir clases particulares remuneradas o no a los alumnos de las cátedras en que sean ayudantes, y</li>

              <li>Las demás que señalen su nombramiento y la Legislación Universitaria.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 28.-</em> Los ayudantes de profesor no podrán ser encargados responsables de una cátedra, ni impartir más del 15% de un curso los del nivel A; del 25% los del nivel B y del 40% los del nivel C.</p>



              <h4>TÍTULO CUARTO</h4>
              <h5>De los Profesores e Investigadores</h5>




              <h4>CAPÍTULO I</h4> <h5>(La denominación El rubro de este Capítulo, fue modificada en la sesión del Consejo Universitario del 10 de diciembre de 1974, como sigue):</h5>




              <h4>CAPÍTULO I Definiciones</h4>




              <p align="justify"><em>Artículo 29.-</em> Los profesores o investigadores podrán ser:</p>

              Ordinarios;

              Visitantes;

              Extraordinarios;

              Eméritos.




              <p align="justify"><em>Artículo 30.-</em> Son profesores o investigadores ordinarios quienes tienen a su cargo las labores permanentes de docencia e investigación.</p>




              <p align="justify"><em>Artículo 31.-</em> (Modificado en la sesión del Consejo Universitario del 10 de junio de 1975, como sigue):</p>



              <p align="justify"><em>Artículo 31.-</em> Son profesores, investigadores o técnicos académicos visitantes los que con tal carácter desempeñen funciones académicas o técnicas específicas por un tiempo determinado, las cuales podrán ser remuneradas por la Universidad.</p>



              <p align="justify"><em>Artículo 32.-</em> Son profesores o investigadores extraordinarios los provenientes de otras universidades del país o del extranjero, que de conformidad con el Reglamento del Reconocimiento al Mérito Universitario, hayan realizado una eminente labor docente o de investigación en la UNAM o en colaboración con ella.</p>




              <p align="justify"><em>Artículo 33.-</em> Son profesores o investigadores eméritos, aquellos a quienes la Universidad honre con dicha designación por haberle prestado cuando menos 30 años de servicios, con gran dedicación y haber realizado una obra de valía excepcional.</p>



              <h4>CAPÍTULO II</h4> <h5>De los Profesores e Investigadores Ordinarios</h5>




              <p align="justify"><em>Artículo 34.-</em> Los profesores ordinarios podrán ser de asignatura o de carrera. Los investigadores serán siempre de carrera.</p>



              <h4>CAPÍTULO III</h4> <h5>De los Profesores de Asignatura</h5>



              <p align="justify"><em>Artículo 35.-</em> (Modificado en las sesiones del Consejo Universitario del 28 de marzo de 1985 y 22 de marzo de 1988, publicado en Gaceta UNAM el 14 de abril de 1988, como sigue):</p>




              <p align="justify"><em>Artículo 35.-</em> Son profesores de asignatura quienes de acuerdo con la categoría que fije su nombramiento, sean remunerados en función del número de horas de clase que impartan.</p>

              Podrán impartir una o varias materias, ser interinos o definitivos y ocupar cualesquiera de las siguientes categorías: A o B.




              <p align="justify"><em>Artículo 36.-</em> Para ser profesor de asignatura A, se requiere:</p>
              <ol><ol>
              <li>Tener título superior al de bachiller en una licenciatura del área de la materia que se vaya a impartir, y</li>

              <li>Demostrar aptitud para la docencia.</li>
						  </ol></ol>
              El requisito del título podrá dispensarse por acuerdo del consejo técnico, en los casos siguientes:
              <ol><ol><ol>
              <li>En el ciclo de bachillerato, cuando no concurran aspirantes que tengan título, y los que se presenten hayan aprobado los cursos correspondientes a una licenciatura en el área de la materia de que se trate;</li>

              <li>En la enseñanza de lenguas vivas, de materias artísticas, de educación física, de adiestramiento y en las que sólo se impartan en carreras en que no haya más de quince graduados. En los supuestos a que se refiere este inciso los interesados deberán haber aprobado los cursos correspondientes a la especialidad de que se trate o demostrar mediante los procedimientos que señale el consejo técnico respectivo, el conocimiento de la materia que se vaya a impartir.</li>
					  	</ol></ol></ol>



              <p align="justify"><em>Artículo 37.-</em> Para ser profesor de asignatura categoría B, además de los requisitos señalados para la categoría A, se requiere:</p>
              <ol><ol>
              <li>Haber trabajado cuando menos dos años en labores docentes o de investigación en la categoría A y haber cumplido satisfactoriamente sus labores académicas, y</li>

              <li>Haber publicado trabajos que acrediten su competencia en la docencia o en la investigación.</li>
						  </ol></ol>
              Este último requisito podrá dispensarse a los profesores que en la dirección de seminarios y tesis o en la impartición de cursos especiales, hayan desempeñado sus labores de manera sobresaliente.




              <h4>CAPÍTULO IV</h4> <h5>De los Profesores e Investigadores de Carrera</h5>



              <p align="justify"><em>Artículo 38.-</em> Son profesores o investigadores de carrera quienes dedican a la Universidad medio tiempo o tiempo completo en la realización de labores académicas. Podrán ocupar cualquiera de las categorías siguientes: asociado o titular. En cada una de éstas habrá tres niveles: A, B y C.</p>




              <p align="justify"><em>Artículo 39.-</em> Para ingresar como profesor o investigador de carrera de la categoría de asociado nivel A, se requiere:</p>
              <ol><ol>
              <li>Tener una licenciatura o grado equivalente;</li>

              <li>Haber trabajado cuando menos un año en labores docentes o de investigación, demostrando aptitud, dedicación y eficiencia, y</li>

              <li>Haber producido un trabajo que acredite su competencia en la docencia o en la investigación.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 40.-</em> Para ingresar o ser promovido a la categoría de profesor o investigador asociado nivel B, se requiere:</p>
              <ol><ol>
              <li>Tener grado de maestro o estudios similares o bien conocimientos y experiencia equivalentes;</li>

              <li>Haber trabajado eficientemente cuando menos dos años en labores docentes o de investigación, en la materia o área de su especialidad, y</li>

              <li>Haber producido trabajos que acrediten su competencia en la docencia o en la investigación.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 41.-</em> Para ingresar o ser promovido a la categoría de profesor o investigador asociado nivel C, se requiere:</p>
              <ol><ol>
              <li>Tener grado de maestro o estudios similares, o bien los conocimientos y la experiencia equivalentes;</li>

              <li>Haber trabajado cuando menos tres años en labores docentes o de investigación, en la materia o área de su especialidad, y</li>

              <li>Haber publicado trabajos que acrediten su competencia, o tener el grado de doctor, o haber desempeñado sus labores de dirección de seminarios y tesis o impartición de cursos, de manera sobresaliente.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 42.-</em> Para ingresar a la categoría de profesor o investigador titular nivel A, se requiere:</p>
              <ol><ol>
              <li>Tener título de doctor o los conocimientos y la experiencia equivalentes;</li>

              <li>Haber trabajado cuando menos cuatro años en labores docentes o de investigación, incluyendo publicaciones originales en la materia o área de su especialidad, y</li>

              <li>Haber demostrado capacidad para formar personal especializado en su disciplina.</li>
						  </ol></ol>


              <p align="justify"><em>Artículo 43.-</em> Además de los requisitos exigidos para alcanzar la categoría de titular nivel A, para ingresar o ser promovido a titular nivel B, es necesario:</p>
              <ol><ol>
              <li>Haber trabajado cuando menos cinco años en labores docentes o de investigación, en la materia o área de su especialidad, y</li>

              <li>Haber demostrado capacidad para dirigir grupos de docencia o de investigación.</li>
						</ol></ol>



              <p align="justify"><em>Artículo 44.-</em> Para ingresar o ser promovido a la categoría de profesor o investigador titular nivel C, además de los requisitos exigidos para ser titular nivel B, es necesario:</p>
              <ol><ol>
              <li>Haber trabajado cuando menos seis años en labores docentes o de investigación, en la materia o área de su especialidad;</li>

              <li>Haber publicado trabajos que acrediten la trascendencia y alta calidad de sus contribuciones a la docencia, a la investigación, o al trabajo profesional de su especialidad, así como su constancia en las actividades académicas, y</li>

              <li>Haber formado profesores o investigadores que laboren de manera autónoma.</li>
						</ol></ol>


              <h4>CAPÍTULO V</h4> <h5>Selección y Promoción de los Profesores e Investigadores Ordinarios</h5>




              <h5>SECCIÓN A De la Definitividad y Promoción</h5>




              <p align="justify"><em>Artículo 45.-</em> La definitividad en el cargo de profesor o de investigador, así como sus promociones, sólo se podrán obtener mediante los procedimientos establecidos en el presente Estatuto.</p>



              <h5>SECCIÓN B De los Profesores Interinos de Asignatura</h5>




              <p align="justify"><em>Artículo 46.-</em> Cuando no exista profesor definitivo para impartir una materia, el director de la dependencia podrá nombrar un interino que deberá satisfacer los requisitos establecidos en el presente Estatuto, por un plazo no mayor de un periodo lectivo, prorrogable por dos más si se ha demostrado capacidad para la docencia. La persona así designada empezará a laborar de inmediato y su nombramiento será sometido a la ratificación del consejo técnico respectivo.</p>




              <p align="justify"><em>Artículo 47.-</em> Los derechos y las obligaciones de los profesores interinos de asignatura serán los mismos que los de los otros miembros del personal académico ordinario en cuanto sean compatibles con su carácter temporal.</p>




              <p align="justify"><em>Artículo 48.-</em> Los profesores interinos de asignatura con antigüedad mayor de un año, deberán presentarse a los concursos de oposición para ingreso que se convoquen en la materia que impartan. Los que no cumplan esta obligación o no sean seleccionados, no tendrán derecho a que se les asigne grupo, salvo que la comisión dictaminadora los declare aptos para la docencia y recomiende la prórroga de su nombramiento.</p>

              Los profesores interinos con tres años de docencia, tendrán derecho a que se abra un concurso de oposición para ingreso.



              <h5>SECCIÓN C Ingreso por Contrato</h5>




              <p align="justify"><em>Artículo 49.-</em> Cuando los programas de trabajo de una dependencia requieran aumento de personal académico y existan partidas presupuestales disponibles, o sea declarado desierto un concurso, se podrá contratar a nuevo personal para la prestación de servicios profesionales o para la realización de una obra determinada.</p>




              <p align="justify"><em>Artículo 50.-</em> Para que les sea otorgado un contrato de prestación de servicios, los candidatos deberán satisfacer los requisitos de ingreso que establece este Estatuto para las categorías y niveles equivalentes. El requisito de tiempo podrá acreditarse en casos excepcionales por acuerdo expreso del consejo técnico, tomando en cuenta los antecedentes académicos del candidato: labores docentes, de investigación, profesionales, estudios de posgrado, participación en el programa de formación del personal académico de la UNAM, y creación científica o artística de reconocida importancia.</p>




              <p align="justify"><em>Artículo 51.-</em> En la contratación de personal académico, se deberá seguir el procedimiento que se señala en este Estatuto para el concurso de oposición o concurso abierto para ingreso, salvo en casos excepcionales o para la realización de una obra determinada. En estos dos últimos casos los términos de la contratación deberán ser previamente aprobados por el consejo técnico, interno o asesor, oyendo la opinión de la comisión dictaminadora respectiva. El personal así contratado sólo podrá adquirir la definitividad a través de un concurso de oposición para ingreso.</p>



              <h4>CAPÍTULO VI</h4> <h5>Selección de los Profesores e Investigadores Visitantes, Extraordinarios y Eméritos</h5>



              <p align="justify"><em>Artículo 52.-</em> La designación del personal académico visitante, y en su caso la prórroga, se harán por los directores de las dependencias, previa autorización del consejo técnico respectivo.</p>




              <p align="justify"><em>Artículo 53.-</em> El Consejo Universitario designará a los profesores extraordinarios a propuesta del director de la dependencia interesada, quien deberá contar con la previa aprobación del consejo técnico o interno que corresponda.</p>



              <p align="justify"><em>Artículo 54.-</em> (Modificado en la sesión del Consejo Universitario del 17 de noviembre de 1977, como sigue):</p>



              <p align="justify"><em>Artículo 54.-</em> La designación de profesores e investigadores eméritos se normará por el Reglamento del Reconocimiento al Mérito Universitario. La propuesta que haga el consejo técnico al Consejo Universitario, tomará en cuenta la opinión debidamente fundada de la comisión dictaminadora correspondiente y, en su caso, del consejo interno. La Comisión del Mérito Universitario resolverá previa opinión de la Comisión del Trabajo Académico. La designación deberá ser aprobada cuando menos por el voto de las dos terceras partes del Consejo Universitario.</p>



              <h4>CAPÍTULO VII</h4> <h5>Derechos y Obligaciones de los Profesores de Asignatura</h5>




              <h5>SECCIÓN A De los Derechos</h5>




              <p align="justify"><em>Artículo 55.-</em> Los profesores de asignatura tendrán, además de los consignados en el artículo 6o. de este Estatuto, los siguientes derechos:</p>
              <ol><ol>
              <li>Percibir la remuneración que fijen los reglamentos y acuerdos de la Universidad por asistencia a exámenes, participación en comisiones, prestación de asesorías u otras actividades;</li>

              <li>Conservar su horario de labores o solicitar el cambio del mismo. El director, en este último caso, resolverá atendiendo a las necesidades de la dependencia;</li>

              <li>Si son definitivos, ser adscrito a materias equivalentes o afines de un nuevo plan de estudios, cuando por reformas se modifiquen o supriman asignaturas;</li>

              <li>Desempeñar sus labores, en la medida de lo posible, en una sola dependencia, y</li>

              <li>Los demás que se deriven de su nombramiento y de la Legislación Universitaria.</li>
						  </ol></ol>

              <h5>SECCIÓN B De las Obligaciones</h5>




              <p align="justify"><em>Artículo 56.-</em> Los profesores de asignatura tendrán las siguientes obligaciones:</p>
              <ol><ol>
              <li>Prestar sus servicios según el horario que señale su nombramiento, y de acuerdo a lo que dispongan los planes y programas de labores y reglamentos aprobados por el consejo técnico de la dependencia a la que se encuentren adscritos;</li>

              <li>Presentar anualmente a las autoridades de su dependencia un informe de sus actividades académicas;</li>

              <li>Cumplir, salvo excusa fundada, las comisiones que les sean encomendadas por las autoridades de la dependencia de su adscripción o por el Rector con el conocimiento de éstas;</li>

              <li>Formar parte de comisiones y jurados de exámenes; y remitir oportunamente la documentación relativa;</li>

              <li>Enriquecer sus conocimientos en la materia o materias que impartan;</li>

              <li>Impartir enseñanza y calificar los conocimientos de los alumnos, sin considerar su sexo, raza, nacionalidad, religión o ideología;</li>

              <li>Indicar su adscripción a una dependencia de la Universidad, en las publicaciones en las que aparezcan resultados de los trabajos que en ella se les hayan encomendado;</li>

              <li>Abstenerse de impartir clases particulares remuneradas o no a sus propios alumnos;</li>

              <li>Impartir las clases que correspondan a su asignatura en el calendario escolar. No se computará como asistencia la del profesor que llegue a la clase con un retraso mayor de 10 minutos;</li>

              <li>Cumplir los programas de su materia aprobados por el consejo técnico respectivo y dar a conocer a sus alumnos, el primer día de clases, dicho programa y la bibliografía correspondiente;</li>

              <li>Realizar los exámenes en las fechas y lugares que fije el consejo técnico respectivo;</li>

              <li>Defender la autonomía de la Universidad y la libertad de cátedra; velar por su prestigio; contribuir al conocimiento de su historia y fortalecerla en cuanto institución nacional dedicada a la enseñanza, la investigación y la difusión de la cultura, y</li>

              <li>Las demás que establezcan su nombramiento y la Legislación Universitaria.</li>
						  </ol></ol>


              <h4>CAPÍTULO VIII</h4> <h5>Derechos y Obligaciones de los Profesores e Investigadores de Carrera</h5>




              <h5>SECCIÓN A De los Derechos</h5>




              <p align="justify"><em>Artículo 57.-</em> Los profesores e investigadores de carrera tendrán, además de los consignados en los artículos 6o. y 55 de este Estatuto, los siguientes derechos:</p>
              <ol><ol>
              <li>Recibir de la Universidad remuneraciones adicionales provenientes de ingresos extraordinarios de su dependencia, de conformidad con el reglamento que al efecto se expida;</li>

              <li>Desempeñar en otras instituciones, previa autorización del consejo técnico respectivo, cátedras u otras labores remuneradas, siempre que el tiempo que dedique a éstas, sumado al que deba dedicar a la Universidad, no exceda de 48 horas semanarias, y</li>

              <li>Ser funcionario académico, recibir la remuneración correspondiente y al término de su encargo reintegrarse a su dependencia de origen, con su misma categoría y nivel y sin menoscabo de sus demás derechos.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 58.-</em> Por cada seis años de servicios ininterrumpidos, los profesores e investigadores ordinarios de tiempo completo gozarán de un año sabático, que consiste en separarse de sus labores durante un año, con goce de sueldo y sin pérdida de su antigüedad, para dedicarse al estudio y a la realización de actividades que les permitan superarse académicamente. Para el ejercicio de este derecho se observarán las siguientes reglas:</p>
              <ol><ol>
              <li>Los interesados podrán solicitar al director de la dependencia de su principal adscripción, que el año sabático se divida en dos semestres, pudiendo disfrutar del primero al cumplir seis años de labores y del segundo en la fecha que de común acuerdo convengan con el director y el consejo técnico;</li>

              <li>Después del primer año sabático, los interesados podrán optar por disfrutar de un semestre sabático por cada tres años de servicios, o de un año por cada seis;</li>

              <li>La fecha de iniciación de cada periodo sabático estará supeditada a los programas de actividades de la dependencia de su principal adscripción, pudiendo adelantarse hasta en tres meses, si no se interfieren los programas mencionados y lo autoriza el consejo técnico;</li>

              <li>A petición de los interesados, podrá diferirse el disfrute del año sabático por no más de dos años, y el lapso que hubiesen trabajado después de adquirido ese derecho, se tomará en consideración para otorgar el subsecuente.</li>

              Los profesores o investigadores designados funcionarios académicos y los que desempeñen un cargo de supervisión o coordinación en alguna dependencia, deberán diferir el disfrute del año sabático hasta el momento en que dejen el cargo. El año sabático sólo será acumulable en los casos previstos en este párrafo;

              <li>Durante el disfrute del periodo sabático, los profesores e investigadores recibirán su salario íntegro;</li>

              <li>El tiempo que se haya laborado ininterrumpidamente como profesor o investigador de tiempo completo interino o por contrato, se computará para los efectos del año sabático;</li>

              <li>El profesor o investigador que tenga dos nombramientos simultáneos de medio tiempo dentro de la Universidad, será considerado como de tiempo completo para los efectos del año sabático;</li>

              <li>Si al solicitar un año sabático o fracción del mismo el interesado presenta al director de la dependencia de su adscripción principal un plan de actividades que desarrollará durante ese intervalo y éstas son de especial interés para la Universidad, el director con la aprobación del consejo técnico respectivo gestionará que el interesado reciba ayuda o estímulos para su proyecto. Al reintegrarse a la Universidad el interesado entregará al director un informe de sus actividades.</li>
              </ol></ol>



              <p align="justify"><em>Artículo 59.-</em> (Modificado en la sesión del Consejo Universitario del 11 de diciembre de 1985, publicado en Gaceta UNAM el 6 de enero de 1986, como sigue):</p>




              <p align="justify"><em>Artículo 59.-</em> Los profesores e investigadores de carrera designados por la Junta de Gobierno para el desempeño de un cargo directivo de funcionario académico conservarán como remuneración mensual, cuando dejen dicho cargo directivo, la establecida en el tabulador vigente a la fecha de su separación durante los tres años inmediatos posteriores a la fecha en que termine el mencionado cargo, además de la correspondiente a su categoría y nivel académico, siempre que sigan formando parte del personal académico de carrera de tiempo completo en forma ininterrumpida, y estén en alguno de los siguientes supuestos:</p>
              <ol><ol><ol>
              <li>Tener más de 20 años de antigüedad académica al servicio de la UNAM y haber permanecido cuando menos 2 años en el cargo directivo de funcionario académico;</li>

              <li>Haber desempeñado sin interrupción durante cuatro años el cargo directivo de que se trate.</li>
						  </ol></ol></ol>

              <h5>SECCIÓN B De las Obligaciones</h5>




              <p align="justify"><em>Artículo 60.-</em> (Modificado en la sesión del Consejo Universitario del 28 de marzo de 1985, publicado en Gaceta UNAM el 22 de abril de 1985, como sigue):</p>




              <p align="justify"><em>Artículo 60.-</em> Además de las obligaciones del artículo 56 el personal académico de carrera deberá someter oportunamente a la consideración del consejo de la dependencia de su adscripción, el proyecto de las actividades de investigación, preparación, estudio y evaluación del curso o cursos que impartan, dirección de tesis o prácticas, aplicación de exámenes, dictado de cursillos y conferencias y demás que pretenda realizar durante el año siguiente; llevarlas a cabo y rendir en su oportunidad un informe sobre la realización de las mismas. Dicho proyecto constituirá su programa anual de labores una vez que sea aprobado por el consejo técnico, interno o asesor.</p>

              Los profesores de enseñanza media superior ajustarán sus actividades a los planes y programas académicos de la enseñanza media superior, y el consejo técnico respectivo determinará, además de las de docencia, aquellas que considere que constituirán el mínimo a desempeñar por dicho personal, así como la distribución del tiempo que dedicarán a cada una de ellas.




              <p align="justify"><em>Artículo 61.-</em> (Modificado en la sesión del Consejo Universitario del 11 de septiembre de 1986, publicado en Gaceta UNAM el 22 de septiembre de 1986, como sigue):</p>




              <p align="justify"><em>Artículo 61.-</em> El personal académico de carrera, de medio tiempo y de tiempo completo tiene la obligación de desempeñar labores docentes y de investigación, según la distribución de tiempo que haga el consejo técnico correspondiente, conforme a los siguientes límites para impartir clases o desarrollar labores de tutoría.</p>
              <ol><ol>
              <li>A nivel profesional y de posgrado:</li>
              <ol>
              <li>Los investigadores, un mínimo de tres horas o las que correspondan a una asignatura y un máximo de seis horas semanales, o bien las que se asignen a labores de tutoría;</li>

              <li>Los profesores titulares, un mínimo de seis horas o las que correspondan a dos asignaturas y un máximo de doce horas por semana, y las que se asignen a labores de tutoría;</li>

              <li>Los profesores asociados, un mínimo de nueve horas o las que correspondan a tres asignaturas y un máximo de dieciocho horas semanales, y las que se asignen a labores de tutoría.</li>
						  </ol>
              <li>A nivel de bachillerato:</li>
              <ol>
              <li>Los profesores titulares, entre doce y dieciocho horas por semana;</li>

              <li>Los asociados, entre quince y veinte horas por semana.</li>
					   	</ol></ol></ol>
              Los límites señalados, se entenderán aplicables a planes de estudio anuales; y se promediarán en planes de estudio de menor temporalidad.



              <p align="justify"><em>Artículo 62.-</em> (Modificado en la sesión del Consejo Universitario del 11 de septiembre de 1986, publicado en Gaceta UNAM el 22 de septiembre de 1986, como sigue):</p>




              <p align="justify"><em>Artículo 62.-</em> El consejo interno respectivo, de común acuerdo con los directores de las facultades o escuelas correspondientes, podrá eximir a los investigadores de impartir clases o de desempeñar labores de tutoría por un tiempo determinado, siempre que exista causa que lo justifique.</p>



              <h4>CAPÍTULO IX</h4> <h5>Derechos y Obligaciones del Personal Académico Visitante,
              Extraordinario y Emérito</h5>




              <p align="justify"><em>Artículo 63.-</em> Los profesores o investigadores visitantes tendrán los derechos y obligaciones que estipule su nombramiento o contrato y no podrán participar en ninguno de los cuerpos colegiados de la UNAM.</p>




              <p align="justify"><em>Artículo 64.-</em> Los profesores e investigadores extraordinarios tendrán los derechos y obligaciones que señale el acuerdo que los designe.</p>




              <p align="justify"><em>Artículo 65.-</em> Los profesores e investigadores eméritos continuarán prestando sus servicios con los derechos y las obligaciones que correspondan a la categoría y nivel que tengan en la fecha en que reciban tal distinción.</p>

              El personal emérito jubilado podrá continuar laborando previa aprobación del respectivo consejo técnico, mediante la celebración de un contrato anual de prestación de servicios.

              Los honorarios que se pacten en dicho contrato no serán inferiores en ningún caso a la cantidad que importen el o los aumentos que se acuerden para la categoría y nivel de que sean titulares, a partir de la fecha de jubilación.



              <h4>TÍTULO QUINTO</h4>
              <h5>De los Procedimientos para los Nombramientos Definitivos y Promociones de Profesores e Investigadores</h5>




              <h4>CAPÍTULO I</h4> <h5>Reglas Comunes de los Concursos de Oposición</h5>



              <p align="justify"><em>Artículo 66.-</em> (Adicionado en la sesión del Consejo Universitario del 10 de diciembre de 1974, como sigue):</p>



              <p align="justify"><em>Artículo 66.-</em> Los concursos de oposición son los procedimientos para el ingreso o la promoción de los profesores e investigadores. El concurso de oposición para ingreso, o concurso abierto, es el procedimiento público a través del cual se puede llegar a formar parte del personal académico como profesor o investigador de carrera interino, o a contrato, o como profesor definitivo de asignatura.</p>

              El concurso de oposición para promoción, o concurso cerrado, es el procedimiento de evaluación mediante el cual los profesores o investigadores de carrera, interinos o a contrato, pueden ser promovidos de categoría o de nivel o adquirir la definitividad; y los definitivos de carrera y asignatura ser promovidos de categoría o de nivel.

              (El párrafo siguiente fue adicionado en la sesión del Consejo Universitario del 10 de diciembre de 1974, como sigue):

              Los profesores e investigadores al servicio de la UNAM cualquiera que sea su categoría o nivel podrán participar también en los concursos de oposición para ingreso o concursos abiertos, con el solo objeto de ser promovidos de nivel o de categoría.




              <p align="justify"><em>Artículo 67.-</em> Pueden solicitar al consejo técnico respectivo que se abra un concurso de oposición:</p>
              <ol><ol>
              <li>El director de la dependencia;</li>

              <li>El consejo interno;</li>

              <li>Tres o más miembros del mismo consejo técnico;</li>

              <li>Los interesados en los casos expresamente previstos en el presente Estatuto.</li>
						  </ol></ol>



              <h4>CAPÍTULO II</h4> <h5>De los Concursos de Oposición para Ingreso o Concursos Abiertos</h5>




              <p align="justify"><em>Artículo 68.-</em> Los criterios de valoración que deberán tomar en cuenta las comisiones para formular sus dictámenes, serán:</p>
              <ol><ol>
              <li>La formación académica y los grados obtenidos por el concursante;</li>

              <li>Su labor docente y de investigación, incluyendo su actividad como becario, técnico o ayudante;</li>

              <li>Sus antecedentes académicos y profesionales;</li>

              <li>Su labor de difusión cultural;</li>

              <li>Su labor académico-administrativa;</li>

              <li>Su antigüedad en la UNAM;</li>

              <li>Su intervención en la formación de personal académico;</li>

              <li>Las opiniones del consejo interno o asesor, en los casos en que así proceda, e</li>

              <li>Los resultados de los exámenes a que se refiere el artículo 74.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 69.-</em> En igualdad de circunstancias se preferirá:</p>
							<ol><ol>
              <li>A los aspirantes cuyos estudios y preparación se adapten mejor al programa de labores de la dependencia;</li>

              <li>A los profesores definitivos de asignatura;</li>

              <li>A los capacitados en los programas de formación de profesores e investigadores de la UNAM y de su dependencia;</li>

              <li>A quien labore en la dependencia, y</li>

              <li>A quien labore en la UNAM.</li>
						  </ol></ol>




              <p align="justify"><em>Artículo 70.-</em> No procederá el concurso de oposición para ingreso:</p>
              <ol><ol>
              <li>Cuando un profesor de carrera definitivo se haga cargo de un nuevo grupo en la asignatura o área de su especialidad;</li>

              <li>Cuando un profesor de asignatura, nombrado a través de concurso, solicite un grupo más en la materia que imparta. En estos casos el director de la dependencia hará la designación correspondiente.</li>
					  	</ol></ol>



              <p align="justify"><em>Artículo 71.-</em> Cuando el consejo técnico resuelva cubrir las plazas vacantes o de nueva creación mediante concurso de oposición para ingreso, el director de la dependencia emitirá una convocatoria que, luego de ser enviada al Secretario General de la UNAM para su consideración, deberá publicarse en el órgano oficial de información de la Institución y en un diario de circulación nacional y fijarse en lugares visibles de la propia dependencia.</p>




              <p align="justify"><em>Artículo 72.-</em> El procedimiento para designar profesores e investigadores a través de concurso de oposición para ingreso o concurso abierto, deberá quedar concluido en un plazo de 60 días hábiles contados a partir de la fecha de publicación de la convocatoria a que se refiere el artículo anterior.</p>




              <p align="justify"><em>Artículo 73.-</em> La convocatoria deberá indicar:</p>
              <ol><ol>
              <li>La clase de concurso;</li>

              <li>El área de la materia en que se celebrará el concurso;</li>

              <li>El número, la categoría y el nivel de las plazas, así como los requisitos que deberán satisfacer los aspirantes;</li>

              <li>Los procedimientos y pruebas que se realizarán para evaluar la capacidad profesional y académica de los aspirantes, de acuerdo con las disposiciones de este Estatuto;</li>

              <li>Los lugares y fechas en que se practicarán las pruebas, y</li>

              <li>El plazo para la presentación de la documentación requerida, que no será menor de 15 días hábiles, contados a partir de la publicación de la convocatoria.</li>
					   	</ol></ol>




              <p align="justify"><em>Artículo 74.-</em> En los concursos de oposición para ingreso, el respectivo consejo técnico determinará a cuáles de las siguientes pruebas específicas deberán someterse los aspirantes:</p>
              <ol><ol>
              <li>Crítica escrita del programa de estudios o de investigación correspondiente;</li>

              <li>Exposición escrita de un tema del programa en un máximo de 20 cuartillas;</li>

              <li>Exposición oral de los puntos anteriores;</li>

              <li>Interrogatorio sobre la materia;</li>

              <li>Prueba didáctica consistente en la exposición de un tema ante un grupo de estudiantes, que se fijará cuando menos con 48 horas de anticipación;</li>

              <li>Formulación de un proyecto de investigación sobre un problema determinado.</li>
						  </ol></ol>
              Los exámenes y pruebas de los concursos serán siempre públicos. Para las pruebas escritas se concederá a los concursantes un plazo no menor de 15 ni mayor de 30 días hábiles.




              <p align="justify"><em>Artículo 75.-</em> El dictamen de las comisiones se turnará al consejo técnico respectivo para su ratificación. Si es favorable a un candidato y el consejo lo ratifica, el director de la dependencia tramitará el nombramiento. Si el concurso se declara desierto, el director podrá contratar personal académico.</p>




              <p align="justify"><em>Artículo 76.-</em> Si el consejo técnico niega la ratificación, devolverá el dictamen a la comisión con sus observaciones. La comisión revisará el caso y volverá a someterlo a la consideración del citado consejo para su decisión final, en un plazo no mayor de 15 días hábiles.</p>




              <p align="justify"><em>Artículo 77.-</em> La resolución final del consejo técnico será dada a conocer a los concursantes dentro de los 15 días hábiles siguientes a la fecha en que se tome.</p>



              <h4>CAPÍTULO III</h4> <h5>De los Concursos de Oposición para Promoción o Concursos Cerrados</h5>




              <p align="justify"><em>Artículo 78.-</em> Tendrán derecho a que se abra un concurso de oposición para promoción:</p>
              <ol><ol><ol>
              <li>Los profesores o investigadores interinos o a contrato que cumplan tres años de servicios ininterrumpidos, con objeto de que se resuelva si es o no el caso de promoverlos u otorgarles la definitividad en la categoría y nivel que tengan;</li>

              <li>Los profesores o investigadores definitivos que cumplan tres años de servicios ininterrumpidos en una misma categoría y nivel, con objeto de que se resuelva si procede su ascenso a otra categoría o nivel.</li>
						  </ol></ol></ol>



              <p align="justify"><em>Artículo 79.-</em> Para el efecto del artículo precedente, se seguirá el siguiente procedimiento:</p>
              <ol><ol>
              <li>Los interesados solicitarán por escrito al director de la dependencia que se abra el concurso;</li>

              <li>Después de verificar si se satisfacen los requisitos estatutarios, el director enviará a la comisión dictaminadora, dentro de los 15 días hábiles, siguientes a la fecha de presentación de la solicitud, los expedientes de los aspirantes junto con sus observaciones sobre su labor académica, así como la opinión del consejo interno o asesor cuando proceda;</li>

              <li>La mencionada comisión, previo estudio de los expedientes, y en su caso, de la práctica de las pruebas específicas a que se refiere el artículo 74 de este Estatuto, emitirá su dictamen dentro de los 45 días hábiles siguientes a la fecha en que reciba dichos expedientes;</li>

              <li>Si la comisión encuentra que los interesados satisfacen los requisitos estatutarios y que han cumplido con los planes de docencia o investigación de su programa de actividades, propondrá según el caso:</li>
              <ol>
              <li>(Modificado en la sesión del Consejo Universitario del 10 de diciembre de 1974, como sigue):</li></ol>
              <ol>
							<li>Que sean promovidos al nivel o categoría inmediato superior, y</li>

              <li>Que se les otorgue la definitividad.</li>
						  </ol>
              <li>El dictamen de la comisión se turnará al consejo técnico para su ratificación, el que tomará en cuenta los criterios de valoración a que se refiere el artículo 68;</li>

              <li>Si el dictamen de la comisión es desfavorable al solicitante, éste conservará su misma categoría y nivel, sin perjuicio del derecho de participar en los concursos de oposición para ingreso que se abran, y</li>

              <li>Si el dictamen de la comisión es desfavorable para el profesor o investigador interino o por contrato que solicitó su definitividad y es ratificado por el consejo técnico, se dará al interesado una oportunidad para que participe en un nuevo concurso de oposición para promoción, el que deberá efectuarse al año de celebrado el anterior. Si no fuese aprobado en éste, se dará por terminada su relación con la Universidad.</li>
						  </ol></ol>


              <h4>CAPÍTULO IV</h4> <h5>De los Nombramientos Efectuados por el Consejo Universitario</h5>




              <p align="justify"><em>Artículo 80.-</em> (Modificado en la sesión del Consejo Universitario del 5 de enero de 1978, como sigue):</p>




              <p align="justify"><em>Artículo 80.-</em> El Consejo Universitario, a propuesta de los consejos técnicos, podrá acordar que excepcionalmente, personas de manifiesta distinción en una especialidad, acreditada por varios años de labor y por la realización y publicación de obras, aun cuando no satisfagan alguno o algunos de los requisitos estatutarios, presenten concurso de oposición para ingreso como profesores o investigadores.</p>



              <h4>TÍTULO SEXTO</h4>
              <h5>De los Órganos que Intervienen en el Ingreso y Promoción del Personal Académico</h5>



              <p align="justify"><em>Artículo 81.-</em> En el ingreso y promoción del personal académico intervendrán:</p>
              <ol><ol>
              <li>El Consejo Universitario;</li>

              <li>Los consejos técnicos;</li>

              <li>Los directores;</li>

              <li>Los consejos internos;</li>

              <li>Las comisiones dictaminadoras;</li>

              <li>Los jurados calificadores, y</li>

              <li>g) Los consejos asesores.</li>
						  </ol></ol>


              <h4>CAPÍTULO I</h4> <h5>De las Comisiones Dictaminadoras</h5>



              <p align="justify"><em>Artículo 82.-</em> Para calificar los concursos de oposición de los profesores e investigadores, se integrarán una o varias comisiones dictaminadoras según lo establezca el consejo técnico respectivo.</p>




              <p align="justify"><em>Artículo 83.-</em> Las comisiones dictaminadoras de cada dependencia se formarán con seis miembros designados de preferencia entre los profesores e investigadores definitivos de otras dependencias de la Universidad, que se hayan distinguido en la disciplina de que se trate.</p>

              El director y los miembros del consejo técnico, interno o asesor, no podrán pertenecer a las comisiones dictaminadoras de su dependencia.




              <p align="justify"><em>Artículo 84.-</em> El Rector, el consejo técnico, interno o asesor y las asociaciones o colegios académicos de la dependencia, o los claustros de profesores o investigadores, designarán respectivamente a dos miembros de las comisiones.</p>

              La integración de estas comisiones en todo caso deberá ser ratificada por el Consejo Universitario.




              <p align="justify"><em>Artículo 85.-</em> Cada dos años se revisará la integración de las comisiones para modificarlas cuando así convenga a juicio del consejo técnico, interno o asesor de las dependencias.</p>

              En caso de renuncia, los miembros de la comisión serán sustituidos por quien hizo la designación. Las nuevas designaciones deberán ser también ratificadas por el Consejo Universitario.




              <p align="justify"><em>Artículo 86.-</em> Las comisiones dictaminadoras se organizarán y funcionarán de acuerdo con las reglas siguientes:</p>
              <ol><ol>
              <li>Fungirá como presidente el miembro de mayor antigüedad académica en la UNAM. En caso de inasistencia del presidente a una reunión, será sustituido por el que le siga en antigüedad;</li>

              <li>La comisión designará de entre sus miembros al que deba fungir como secretario. En caso de inasistencia de éste a una reunión, la misma comisión elegirá a quien deba sustituirlo;</li>

              <li>Podrá sesionar con la asistencia de cuatro de sus miembros, y</li>

              <li>Los acuerdos se tomarán por mayoría.</li>
						  </ol></ol>


              <h4>CAPÍTULO II</h4> <h5>De los Jurados Calificadores</h5>




              <p align="justify"><em>Artículo 87.-</em> Para calificar los concursos de oposición de los profesores de asignatura, el respectivo consejo técnico, si lo considera conveniente, podrá integrar un jurado calificador con un máximo de cinco sinodales y un mínimo de tres. Los jurados calificadores serán órganos auxiliares del consejo técnico y de las comisiones dictaminadoras. Los propios consejos reglamentarán su funcionamiento, de acuerdo con las normas aplicables del presente Estatuto.</p>



              <h4>CAPÍTULO III</h4> <h5>Del Personal Académico en las Dependencias Administrativas</h5>



              <p align="justify"><em>Artículo 88.-</em> (Modificado en las sesiones del Consejo Universitario del 14 de abril de 1983 y 11 de septiembre de 1986, publicado en Gaceta UNAM el 22 de septiembre de 1986, como sigue):</p>




              <p align="justify"><em>Artículo 88.-</em> Los Centros de Extensión Universitaria y las siguientes dependencias podrán contar con los servicios de personal académico: Dirección General de Difusión Cultural, Dirección General de Bibliotecas, Dirección General de Actividades Deportivas y Recreativas, Dirección General de Cursos Temporales, Dirección General del Programa Universitario de Cómputo y sus direcciones: de Cómputo para la Docencia, de Cómputo para la Investigación, de Cómputo para la Administración Académica y de Cómputo para la Administración Central, Comisión de Nuevos Métodos de Enseñanza, Coordinación de la Universidad Abierta y Coordinación General de Estudios de Posgrado. En cada una de ellas habrá un consejo asesor y una comisión dictaminadora.</p>



              <p align="justify"><em>Artículo 89.-</em> El consejo asesor estará integrado por las siguientes personas:</p>
              <ol>
              <li>El director o presidente;</li>

              <li>El subdirector o secretario, y</li>

              <li>El número de vocales que establezca el reglamento interior de la dependencia que serán nombrados por el Rector y ratificados por el Consejo Universitario.</li>
						  </ol>



              <p align="justify"><em>Artículo 90.-</em> El consejo asesor tendrá respecto a la selección y promoción del personal académico, las atribuciones que este Estatuto señala a los consejos internos. Cuando en el área respectiva no exista consejo técnico que deba aprobar las resoluciones del consejo asesor, éstas serán sometidas a la resolución del consejo técnico afín que señale el Secretario General de la UNAM.</p>

              La comisión dictaminadora se regirá por las normas establecidas en este Estatuto.




              <p align="justify"><em>Artículo 91.-</em> En las dependencias administrativas en que exista personal académico, pero cuyo número no justifique la integración de una comisión dictaminadora propia, el Secretario General de la UNAM decidirá cuál será la comisión dictaminadora que califique los nombramientos y promociones respectivos, de acuerdo con las reglas señaladas en el Capítulo I del Título Sexto de este Estatuto.</p>



              <h4>TÍTULO SÉPTIMO</h4>
              <h5>Cambios de Adscripción y de Medio Tiempo a Tiempo Completo o Viceversa</h5>





              <p align="justify"><em>Artículo 92.-</em> El personal académico que lo solicite puede quedar adscrito, temporalmente, a dependencia diversa de la de su principal adscripción, de acuerdo con las siguientes reglas:</p>
              <ol><ol>
              <li>El interesado presentará a la aprobación de los directores de ambas dependencias el programa de actividades académicas o académico-administrativas que se proponga desempeñar, con indicación del tiempo necesario para su realización;</li>

              <li>Si ambos directores opinan favorablemente sobre el programa presentado, lo turnarán para su aprobación a los respectivos consejos técnicos, y</li>

              <li>Si los consejos lo aprueban, el solicitante quedará adscrito, por el tiempo que dure su programa, a la dependencia de que se trate. Al concluir ese tiempo, se reintegrará automáticamente a su dependencia de origen.</li>
						  </ol></ol>
              En el caso del personal académico que solicite quedar adscrito definitivamente a otra dependencia, además de las opiniones y aprobaciones a que se refieren los incisos precedentes, se requiere la aprobación de la comisión dictaminadora correspondiente.




              <p align="justify"><em>Artículo 93.-</em> Los miembros del personal académico que sean designados funcionarios en una dependencia diversa a la de su adscripción, quedarán adscritos a ésta por el tiempo que dure su función.</p>





              <p align="justify"><em>Artículo 94.-</em> El cambio de un miembro del personal académico de medio tiempo a tiempo completo o viceversa, dentro de la misma categoría y nivel, se podrá efectuar en el primer supuesto, cuando exista partida presupuestal y en ambos, cuando así convenga a los planes de la dependencia.</p>

              El interesado deberá presentar la correspondiente solicitud por escrito al director, y éste la enviará al consejo técnico junto con su opinión, así como la de la comisión dictaminadora, y la del consejo interno o asesor en su caso, para que dicho consejo resuelva lo conducente dentro de los 30 días hábiles siguientes a la fecha de recepción de la solicitud.



              <h4>TÍTULO OCTAVO</h4>
              <h5>Comisiones, Licencias y Jubilaciones</h5>




              <p align="justify"><em>Artículo 95.-</em> Los directores de las dependencias podrán:</p>
              <ol><ol>
              <li>Conceder a los miembros del personal académico de la dependencia a su cargo, permisos para faltar a sus labores con goce de sueldo, hasta por tres días consecutivos.</li>

              Estos permisos no podrán exceder de tres en un semestre, y

              <li>Conferirles, con la aprobación del consejo técnico, comisiones para realizar estudios o investigaciones en instituciones nacionales o extranjeras, siempre que éstos puedan contribuir al desarrollo de la docencia o de la investigación, y llenen una necesidad de la dependencia.</li>
						  </ol></ol>
              El propio consejo técnico determinará la duración de las comisiones, que no podrá exceder de dos años, susceptibles de prórroga en casos excepcionales, por un año más.




              <p align="justify"><em>Artículo 96.-</em> Cuando los miembros del personal académico gocen de una beca otorgada por institución diversa de la Universidad para realizar estudios o investigaciones, el consejo técnico correspondiente determinará si se justifica que el becario disfrute de la totalidad o de una parte de su sueldo.</p>

              En su caso, esta prestación se concederá por un periodo de un año, prorrogable sólo por un año más.




              <p align="justify"><em>Artículo 97.-</em> Podrán concederse licencias a los miembros del personal académico:</p>
              <ol><ol>
              <li>Por enfermedad, en los términos de la ley respectiva;</li>

              <li>Con el fin de dictar cursillos o conferencias en otras instituciones académicas;</li>

              <li>Para asistir a reuniones culturales;</li>

              <li>d) Por haber sido nombrado rector de cualquier universidad de la República;</li>

              <li>Por haber sido designado o electo, para desempeñar un cargo público de importancia;</li>

              <li>Por desempeñar funciones administrativas, dentro de la propia UNAM, que no le permitan ejercer las docentes o de investigación, y</li>

              <li>Por motivos personales.</li>
						  </ol></ol>
              Con excepción de las previstas en los incisos b) y c), las licencias serán sin goce de sueldo.

              Tendrán derecho a disfrutar de las licencias a que se refiere el presente artículo, salvo el caso previsto en el inciso a), los miembros del personal académico con una antigüedad mínima de dos años.




              <p align="justify"><em>Artículo 98.-</em> El interesado deberá presentar la solicitud de licencia al director de la dependencia de su adscripción, quien la enviará con su opinión al consejo técnico, el que fijará las condiciones en que deba concederse, de acuerdo con las siguientes reglas:</p>
              <ol><ol>
              <li>Se podrá conceder licencia por motivos personales en una o varias ocasiones, pero sin que la suma de los días exceda de 15 durante un semestre o de 30 durante un año, siempre que los intereses de la dependencia no resulten afectados;</li>

              <li>La duración de las licencias a que se refieren los incisos b) y c) del artículo anterior, no podrá exceder de 45 días en un año;</li>

              <li>La duración de las licencias que se mencionan en el inciso f) del artículo anterior será igual a la permanencia en la función de que se trate;</li>

              <li>Las licencias a que se refieren los incisos d) y e) del artículo precedente, no podrán exceder de 8 y 6 años, respectivamente.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 99.-</em> Cuando se haya concedido a un profesor licencia sin goce de sueldo, la remuneración correspondiente al periodo comprendido entre la terminación de los exámenes y la iniciación del nuevo periodo lectivo, se dividirá proporcionalmente entre aquél y quien lo haya sustituido.</p>




              <p align="justify"><em>Artículo 100.-</em> Sólo las licencias concedidas por las causas mencionadas en los incisos a), b), c) y f) del artículo 97, se computarán como tiempo efectivo de servicios a la UNAM.</p>




              <p align="justify"><em>Artículo 101.-</em> (Con motivo de la adición de este artículo en la sesión del Consejo Universitario del 5 de septiembre de 1974, la numeración se recorre y queda como  sigue):</p>



              <p align="justify"><em>Artículo 101.-</em> El personal que se separe de la Universidad sin causa justificada durante un lapso mayor de los especificados podrá reincorporarse a ella, con las categorías y niveles que tuvo a la fecha de la separación, si los consejos técnicos correspondientes consideran que es conveniente para la Institución y exista posibilidad presupuestal.</p>




              <p align="justify"><em>Artículo 102.-</em> Cuando un miembro del personal académico alcance la edad de 70 años dejará su plaza; pero si la Institución requiere de sus servicios, el consejo técnico respectivo tomando en cuenta la opinión del consejo interno o asesor en su caso, podrá acordar anualmente que continúe en funciones.</p>




              <p align="justify"><em>Artículo 103.-</em> Cuando un miembro del personal académico de la Universidad se haya jubilado, el consejo técnico respectivo podrá autorizar que continúe laborando por contrato, sin cargo directivo en las dependencias a que estaba adscrito. Dicha autorización podrá ser renovada anualmente.</p>



              <h4>TÍTULO NOVENO</h4>
              <h5>De los Recursos</h5>




              <h4>CAPÍTULO I </h4><h5>De la Reconsideración</h5>



              <p align="justify"><em>Artículo 104.-</em> Los miembros del personal académico que se consideren afectados en su situación académica por las decisiones de las autoridades universitarias, podrán impugnarla dentro de los 10 días hábiles siguientes a la fecha en que les hayan sido dadas a conocer.</p>

              Este recurso se da sin perjuicio de los que establezcan otros ordenamientos universitarios.




              <p align="justify"><em>Artículo 105.-</em> El recurso deberá interponerse ante el director de la dependencia de adscripción del recurrente, por escrito y estar debidamente fundado. Cuando el director no haya dictado la resolución impugnada correrá traslado a la autoridad señalada en el escrito de reconsideración.</p>

              El recurso deberá resolverse en un plazo no mayor de 30 días hábiles contados a partir de la fecha de recepción de los documentos relativos.



              <h4>CAPÍTULO II</h4> <h5>De la Revisión en los Concursos de Oposición</h5>



              <p align="justify"><em>Artículo 106.-</em> Si la resolución del consejo técnico fuere desfavorable al concursante, éste tendrá derecho a pedir la revisión de la misma, de acuerdo con el siguiente procedimiento:</p>
              <ol><ol>
              <li>El recurso deberá interponerse ante el director de la dependencia de adscripción, dentro de los 10 días hábiles siguientes a la fecha en que se le haya dado a conocer la resolución;</li>

              <li>El recurso deberá presentarse por escrito debidamente fundamentado y ofreciendo pruebas si es el caso;</li>

              <li>El consejo técnico, la comisión dictaminadora y la asociación o colegio académico al que pertenezca el recurrente, designarán a uno de sus miembros para formar una comisión especial;</li>

              Si el recurrente no pertenece a ninguna asociación o colegio académico, podrá designar a uno de los profesores o investigadores definitivos de la dependencia de su adscripción;

              <li>Dicha comisión examinará el expediente, desahogará las pruebas, oirá al interesado, recabará los informes que juzgue pertinentes, y oirá las opiniones del director de la dependencia y del consejo interno o asesor en su caso; y</li>

              <li>La comisión emitirá una opinión razonada, en un término máximo de 15 días hábiles, que será sometida al consejo técnico para su resolución definitiva.</li>
						  </ol></ol>


              <h4>TÍTULO DÉCIMO</h4>
              <h5>Terminación de las Relaciones entre la Universidad y su Personal Académico</h5>



              <p align="justify"><em>Artículo 107.-</em> Las relaciones entre la Universidad y su personal académico terminarán, sin responsabilidad para la Institución, por:</p>
              <ol><ol>
              <li>Renuncia;</li>

              <li>Mutuo consentimiento;</li>

              <li>Muerte del miembro del personal académico;</li>

              <li>Conclusión del término pactado;</li>

              <li>Inasistencia del miembro del personal académico a sus labores sin causa justificada, por más de tres veces consecutivas o por más de cinco no consecutivas en un periodo de 30 días.</li>

              En el caso de los profesores de asignatura, el cómputo se hará por cada grupo escolar;

              <li>Haber sido sancionado con destitución, de acuerdo con la Legislación Universitaria.</li>
						  </ol></ol>


              <h4>TÍTULO DÉCIMO PRIMERO</h4>
              <h5>Sanciones</h5>




              <p align="justify"><em>Artículo 108.-</em> Además de las previstas en la Legislación Universitaria, son causas de sanción las siguientes:</p>
              <ol><ol>
              <li>El incumplimiento de las obligaciones establecidas en el presente Estatuto, y</li>

              <li>La deficiencia en las labores docentes o de investigación, objetivamente comprobada.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 109.-</em> Las sanciones que pueden aplicarse al personal académico son:</p>
              <ol><ol>
              <li>Extrañamiento escrito;</li>

              <li>Suspensión;</li>

              <li>Destitución.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 110.-</em> Cuando se considere que un miembro del personal académico ha incurrido en alguna causa de sanción:</p>
              <ol><ol>
              <li>El director de la dependencia lo comunicará por escrito y en forma razonada al consejo técnico, acompañando las pruebas que estime conducentes;</li>

              <li>Dicho consejo correrá traslado al interesado para que dentro de los 15 días hábiles siguientes a la recepción de los documentos, conteste por escrito lo que a su derecho convenga, anexando las pruebas que tenga en su favor;</li>

              <li>El consejo podrá ordenar que se practique cualquier diligencia o se desahogue cualquier prueba, antes de dictar su resolución. Ésta deberá producirse a más tardar en 15 días hábiles, a partir de la recepción de la última prueba.</li>
						  </ol></ol>



              <p align="justify"><em>Artículo 111.-</em> En las dependencias donde exista consejo interno, antes de resolver el consejo técnico turnará a aquél la documentación para que, en un plazo no mayor de 10 días hábiles a partir de la recepción, le entregue un dictamen razonado.</p>




              <p align="justify"><em>Artículo 112.-</em> Las resoluciones del consejo técnico dictadas de conformidad con los artículos precedentes, podrán ser recurridas ante el Tribunal Universitario, siempre que el interesado presente por escrito su inconformidad debidamente fundada dentro de los 10 días hábiles siguientes a la fecha en que se le dé a conocer la resolución. La revisión por parte del Tribunal Universitario se sujetará a las normas reglamentarias que rigen su funcionamiento.</p>



              <h4>TÍTULO DÉCIMO SEGUNDO</h4>
              <h5>De las Asociaciones</h5>




              <p align="justify"><em>Artículo 113.-</em> La Universidad reconoce la libertad de su personal académico para organizarse en asociaciones o colegios de acuerdo con los principios de la Legislación Universitaria, principalmente la autonomía y la libertad de cátedra y de investigación.</p>




              <p align="justify"><em>Artículo 114.-</em> Las asociaciones o colegios a que se refiere el artículo anterior, podrán agrupar a profesores, investigadores, ayudantes y técnicos de una o varias facultades, escuelas, institutos o centros según la libre decisión del propio personal académico.</p>




