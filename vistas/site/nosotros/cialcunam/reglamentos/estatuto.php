              <ol>
              <li>Ser profesor, investigador o técnico académico con más de seis años de antigüedad académica en la entidad o dependencia universitaria de adscripción, salvo que se trate de establecimientos de reciente creación, en cuyo caso la antigüedad se computará desde el ingreso a la Universidad;</li>

              <li>Realizar, en el caso de investigadores y técnicos académicos, funciones docentes en la Universidad;</li>

              <li>Manifestar por escrito, en el momento de su registro como candidato, su compromiso y disponibilidad para participar en las tareas encomendadas en el Consejo Universitario, en caso de resultar electo;</li>

              <li>No ocupar un puesto administrativo o académico-administrativo en la Universidad al momento de la elección, ni durante el desempeño del cargo de consejero, y</li>

              <li>No haber cometido faltas graves contra la disciplina universitaria que hubieren sido sancionadas.</li>
					  	</ol>
              <p align="justify">Los alumnos de los programas de posgrado designarán cada 2 años en elección directa, mediante voto universal, libre y secreto, a un consejero propietario y su suplente por cada área del conocimiento mencionada en el último párrafo del artículo 16.</p>

              <p align="justify"><em>Artículo 20.-</em> (Modificado en las sesiones del Consejo Universitario del 23 de marzo de 2001 y 5 de julio de 2005, publicado en Gaceta UNAM el 1 de agosto de 2005, como sigue):</p>

              <p align="justify"><em>Artículo 20.-</em> Para ser consejero representante de los alumnos será menester llenar los siguientes requisitos:</p>
              <ol>
              <li>Se deroga;</li>

              <li>Pertenecer a los tres últimos años de estudios en la facultad o escuela correspondiente;</li>

              <li>Haber obtenido, en los años anteriores, un promedio de calificaciones mínimo de 8;</li>

              <li>Haber estudiado, por lo menos los dos años anteriores, en alguno de los planteles a que se refiere el artículo 8o., de este Estatuto, y</li>

              <li>o haber cometido faltas graves contra la disciplina universitaria, que hubieren sido sancionadas.</li>
						  </ol>
              <p align="justify">Para ser consejero representante de los alumnos de los programas de posgrado del área en la que se está inscrito, además de los requisitos establecidos en las fracciones I y V, será necesario: tener en su historia académica un registro de inscripción en los dos semestres inmediatos anteriores en algún programa de posgrado de la Universidad; estar inscrito en el momento de la elección y haber realizado las actividades académicas asignadas en los plazos establecidos; no tener evaluaciones desfavorables y, en su caso, haber obtenido un promedio mínimo de 8.</p>


              <p align="justify"><em>Artículo 21.-</em> (Modificado en las sesiones del Consejo Universitario del 12 de septiembre de 1986, y26 de agosto de 2011, publicado en Gaceta UNAM el 12 de septiembre del mismo año, como sigue):</p>

              <p align="justify"><em>Artículo 21.-</em> Para ser consejero por los centros de extensión universitaria, se requerirá:</p>
              <ol>
              <li>Tener seis años de antigüedad académica en la Universidad;</li>

              <li>Ser profesor en ejercicio;

              <li>Manifestar por escrito, en el momento de su registro como candidato, su compromiso y disponibilidad para participar en las tareas encomendadas en el Consejo Universitario, en caso de resultar electo;</li>

              <li>No ocupar un puesto administrativo o académico-administrativo en la Universidad al momento de la elección, ni durante el desempeño del cargo de consejero, y</li>

              <li>No haber cometido faltas graves contra la disciplina universitaria, que hubieren sido sancionadas.</li>
					  	</ol>
              <p align="justify"><em>Artículo 22.-</em> (Modificado en las sesiones del Consejo Universitario del 5 de julio de 2005, y 26 de agosto de 2011, publicado en Gaceta UNAM el 12 de septiembre del mismo año, como sigue):</p>

              <p align="justify"><em>Artículo 22.-</em> Los empleados de la Universidad en elección directa y en sesión convocada al efecto por el Secretario General de la Universidad, elegirán cada cuatro años un representante propietario y otro suplente, así como a los cinco invitados permanentes a que alude la fracción XI del artículo 16 del presente Estatuto.</p>

              <p align="justify"><em>Artículo 23.-</em> (Modificado en la sesión del Consejo Universitario del 26 de agosto de 2011, publicado en Gaceta UNAM el 12 de septiembre del mismo año, como sigue):</p>

              <p align="justify"><em>Artículo 23.-</em> Para ser consejero o invitado permanente por los empleados administrativos, será necesario cubrir los siguientes requisitos:</p>
              <ol>
              <li>Haber terminado la enseñanza básica;</li>

              <li>Haber servido a la Universidad más de seis años;</li>

              <li>Manifestar por escrito, en el momento de su registro como candidato, su compromiso y disponibilidad para participar en las tareas encomendadas en el Consejo Universitario, en caso de resultar electo, y</li>

              <li>No haber cometido faltas graves contra la disciplina universitaria que hubieren sido sancionadas.</li>
						  </ol>
              <p align="justify"><em>Artículo 24.-</em> (Modificado en las sesiones del Consejo Universitario del 5 de julio de 2005; 26 de agosto de 2011, y 11 de diciembre de 2013 ,publicado en Gaceta UNAM el 20 de enero de 2014, como sigue):</p>

              <p align="justify"><em>Artículo 24.-</em> La Comisión Especial Electoral del Consejo Universitario calificará la elección de los consejeros universitarios a que se refieren los artículos 17,18 y 20 del presente Estatuto.</p>

              <p align="justify"><em>Artículo 25.-</em> (Modificado en las sesiones del Consejo Universitario del 30 de agosto de 1946; 26 de febrero de 1968; 27 de febrero de 1973, y 19 de febrero de 1974, como sigue):</p>


              <p align="justify"><em>Artículo 25.-</em> El Consejo trabajará en pleno o en comisiones que podrán ser permanentes o especiales; son permanentes las que siguen:</p>
              <ol>
              <li>De Difusión Cultural;</li>

              <li>De Honor;</li>

              <li>De Incorporación y Revalidación de Estudios;</li>

              <li>Del Mérito Universitario;</li>

              <li>e Presupuestos;</li>

              <li>De Legislación Universitaria;</li>

              <li>De Títulos y Grados;</li>

              <li>De Trabajo Académico, y</li>

              <li>De Vigilancia Administrativa.</li>
						  </ol>
              <p align="justify">Las comisiones especiales serán las que el Consejo designe para estudiar y dictaminar otros asuntos de su competencia.</p>

              <p align="justify"><em>Artículo 26.-</em> El Consejo celebrará sesiones ordinarias cada tres meses durante cinco días como máximo y extraordinarias cuando lo juzgue necesario el Rector o un grupo de consejeros que represente cuando menos un tercio de los votos computables. En este último caso, se presentará por los interesados una solicitud al Rector, en la que deberá indicarse el asunto o asuntos materia de la convocatoria, y si ésta no se expide en el término de una semana, podrá lanzarla directamente el grupo solicitante.</p>

              <p align="justify"><em>Artículo 27.-</em> En cada periodo ordinario de sesiones, las comisiones a que se refiere el artículo 25 tendrán la obligación ineludible de rendir al Consejo un informe detallado acerca de los asuntos de su competencia. El Rector dará amplia publicidad a los informes de comisiones que le indique el Consejo.</p>

              <p align="justify"><em>Artículo 28.-</em> Cuando el Consejo funcione en pleno, actuará válidamente con la asistencia de más de la mitad de sus miembros, a menos que se trate de tomar decisiones para las cuales se exija una mayoría especial. Si por falta de quórum se suspendiere alguna sesión, se citará para una segunda que podrá efectuarse, cualquiera que sea el asunto de que se trate, con los consejeros concurrentes. Salvo prevención de este Estatuto o de los reglamentos, el Consejo adoptará sus resoluciones por simple mayoría de votos. Las votaciones serán económicas, a menos que el Rector o dos consejeros pidan que sean nominales, por cédulas o secretas.</p>

              <p align="justify">Sólo tendrán derecho a votar los consejeros presentes sin que puedan computarse en ningún caso, los votos escritos de consejeros que no concurran a la asamblea.</p>

              <p align="justify"><em>Artículo 29.-</em> El Consejo se renovará totalmente cada cuatro años, salvo lo dispuesto en el artículo 19 y cuando deban sustituirse las ausencias que no puedan cubrir los suplentes respectivos. En todo caso deberán renovarse los representantes alumnos que dejen de tener el carácter de consejeros, por terminación de sus estudios o por otra causa.</p>

              <p align="justify">En ningún caso podrán ser reelectos los consejeros para el periodo inmediato al de su encargo.</p>

              <h4>CAPÍTULO III</h4>
              <h5>Del Rector</h5>


              <p align="justify"><em>Artículo 30.-</em> (Modificado en la sesión del Consejo Universitario del 11 de diciembre de 1985, publicado en Gaceta UNAM el 6 de enero de 1986, como sigue):</p>

              <p align="justify"><em>Artículo 30.-</em> El Rector será el jefe nato de la Universidad, su representante legal y Presidente del Consejo Universitario, durará en su encargo cuatro años y podrá ser reelecto una vez.</p>

              <p align="justify">En asuntos contenciosos y judiciales, la representación de la Universidad corresponderá al Abogado General, quien podrá delegarla cuando lo juzgue necesario para la defensa de los intereses de la Institución y otorgar poderes generales o especiales para el mismo fin.</p>

              <p align="justify"><em>Artículo 31.-</em> El Secretario General de la Universidad será designado libremente por el Rector y colaborará con él en los asuntos de carácter docente, de orientación, de dirección de la Universidad y de difusión de la cultura.</p>

              <p align="justify"><em>Artículo 32.-</em> Para ser Rector se requerirá:</p>
              <ol>
              <li>Ser mexicano por nacimiento;</li>

              <li>Ser mayor de treinta y cinco años y menor de setenta años en el momento de la elección;</li>

              <li>Poseer un grado universitario superior al de bachiller;</li>

              <li>Tener cuando menos diez años de servicios docentes o de investigación en la Universidad, y</li>

              <li>Haberse distinguido en su especialidad mediante la publicación o ejecución de obras de reconocido mérito, y gozar de estimación general como persona honorable y prudente.</li>
						  </ol>
              <p align="justify"><em>Artículo 33.-</em> El Rector será sustituido en sus faltas, que no excedan de dos meses, por el Secretario General; pero si la ausencia fuere mayor, la Junta de Gobierno designará un Rector provisional.</p>

              <p align="justify"><em>Artículo 34.-</em> (Modificado en las sesiones del Consejo Universitario del 4 de diciembre de 1963; 22 de septiembre de 1967, y 28 de marzo de 1969, como sigue):</p>

              <p align="justify"><em>Artículo 34.-</em> Son obligaciones y facultades del Rector:</p>
              <ol>
              <li>Tener, con la salvedad que fija el artículo 30, la representación legal de la Universidad y delegarla, para casos concretos, cuando lo juzgue necesario;</li>

              <li>Convocar al Consejo y presidir sus sesiones;</li>

              <li>Proponer al Consejo la designación de los miembros de las comisiones permanentes y especiales y actuar como Presidente ex oficio de las mismas;</li>

              <li>Cuidar del exacto cumplimiento de las disposiciones de la Junta de Gobierno y de las que dicte el Consejo Universitario, salvo el caso de veto;</li>

              <li>Vetar conforme a lo dispuesto en el artículo siguiente, los acuerdos generales o relativos a asuntos concretos que dicte el Consejo Universitario y que no tengan carácter técnico;</li>

              <li>Formar las ternas para las designaciones de directores de facultades y escuelas y someterlas a los consejos técnicos y a la Junta de Gobierno conforme a lo establecido por el artículo 11 de la Ley;</li>

              <li>Formar las ternas para las designaciones de directores de institutos y someterlas a la Junta de Gobierno;</li>

              <li>Hacer en los términos de los reglamentos respectivos las designaciones, cambios o remociones del personal docente, técnico y administrativo que no estén reservados a otras autoridades de la Universidad;</li>

              <li>Tener, en las materias no reservadas al Patronato, la dirección general del gobierno de la Universidad y ser el conducto necesario para las relaciones entre la Junta de Gobierno y el Patronato y las restantes autoridades universitarias;</li>

              <li>Velar por el cumplimiento de este Estatuto, de sus reglamentos, de los planes y programas de trabajo y, en general, de las disposiciones y acuerdos que normen la estructura y el funcionamiento de la Universidad, dictando las medidas conducentes;</li>

              <li>Velar por la conservación de un orden libre y responsable en la Universidad, dictar las medidas y aplicar las sanciones correspondientes, en los términos del Estatuto General y los reglamentos;</li>

              <li>Profesar, potestativamente en alguna de las facultades o escuelas de la Universidad o realizar, en cualquiera de sus Institutos, labores de investigación;</li>

              <li>Expedir y firmar, en unión del Secretario General, los títulos profesionales y los diplomas que acrediten la obtención de un grado universitario;</li>

              <p align="justify">Los certificados de estudios serán firmados por el Secretario General, quien podrá delegar esta función en el Director General de Servicios Escolares, en el Director General de Incorporación y Revalidación de Estudios y en los subdirectores de esas dependencias, y</p>

              <li>En general, cumplir las demás funciones que el Estatuto General y los reglamentos universitarios le impongan.</li>
						  </ol>
              <p align="justify"><em>Artículo 35.-</em> El Rector deberá anunciar la interposición del veto a que se refiere la fracción V del artículo anterior ante el Consejo Universitario, en la misma sesión en que se haya dictado el acuerdo o en la inmediatamente posterior, que deberá celebrarse en un plazo no mayor de cinco días. Si anunciada la interposición del veto el Consejo Universitario decide no reconsiderar el acuerdo respectivo, el Rector elevará el asunto, también en un plazo de cinco días, a la Junta, y suspenderá la ejecución de la providencia impugnada.</p>

              <p align="justify">Una vez que la Junta dicte su resolución, el Rector deberá comunicarla al Consejo y proceder a su inmediato cumplimiento.</p>

              <h4>CAPÍTULO IV</h4>
              <h5>Del Patronato</h5>


              <p align="justify"><em>Artículo 36.-</em> (Modificado en la sesión del Consejo Universitario del 12 de septiembre de 1986, publicado en Gaceta UNAM el 29 de septiembre de 1986, como sigue):</p>

              <p align="justify"><em>Artículo 36.-</em> El Patronato de la Universidad se integrará conforme a lo dispuesto en el artículo 10 de la Ley Orgánica y tendrá las facultades que el mismo precepto señala. Cada uno de sus miembros será designado por la Junta de Gobierno, de una terna propuesta por el Consejo Universitario.</p>

              <h4>CAPÍTULO V</h4>
              <h5>De los Directores de Facultades y Escuelas</h5>


              <p align="justify"><em>Artículo 37.-</em> Los directores de facultades y escuelas serán designados por la Junta de Gobierno de ternas que formará el Rector, quien previamente las someterá a la aprobación de los consejos técnicos; éstos sólo podrán impugnar la terna, total o parcialmente, en el caso de que los candidatos no llenen los requisitos que señala el artículo 39, a fin de que el Rector proceda a hacer las sustituciones a que haya lugar.</p>

              <p align="justify">Los directores de facultades y escuelas durarán en su encargo cuatro años y podrán ser reelectos una vez.</p>

              <p align="justify"><em>Artículo 38.-</em> El Rector podrá solicitar en todo tiempo a la Junta de Gobierno la remoción, por causa grave, de los directores de facultades y escuelas; si la remoción se pide por motivo que comprometa el honor o el prestigio personal del director, éste será oído por la Junta.</p>

              <p align="justify"><em>Artículo 39.-</em> Los directores de facultades o escuelas deberán reunir los siguientes requisitos:</p>
              <ol>
              <li>Ser mexicano por nacimiento, mayor de treinta y menor de setenta años;</li>

              <li>Haberse distinguido en la labor docente, de investigación o de divulgación científica y llevar una vida honorable;</li>

              <li>Haber prestado servicios docentes en la facultad o escuela de que se trate por lo menos ocho años y estar sirviendo en ella una cátedra, y</li>

              <li>Poseer uno de los títulos que otorgue la facultad o escuela respectiva o un grado equivalente.</li>
					  	</ol>
              <p align="justify"><em>Artículo 40.-</em> Los directores de las facultades y escuelas serán sustituidos, si la falta no excediere de dos meses, por el más antiguo de los miembros profesores del consejo técnico, y en caso contrario por la persona que designe la Junta de Gobierno conforme al procedimiento establecido por la Ley para elección de directores definitivos.</p>

              <p align="justify"><em>Artículo 41.-</em> (Modificado en la sesión del Consejo Universitario del 24 de enero de 1966, publicado en Gaceta UNAM el 7 de febrero de 1966, como sigue):</p>

              <p align="justify"><em>Artículo 41.-</em> Corresponderá a los directores de facultades y escuelas:</p>
              <ol>
              <li>Representar a su facultad o escuela;</li>

              <li>Concurrir a las sesiones del Consejo Universitario, con voz y voto;</li>

              <li>Nombrar al secretario con aprobación del Rector y proponer a éste la designación de personal técnico y administrativo. El secretario deberá tener, por lo menos, tres años de servicios docentes y profesar una cátedra en el momento de su designación;</li>

              <li>Proponer el nombramiento del personal docente una vez satisfechas las disposiciones del Estatuto y los reglamentos;</li>

              <li>Convocar a los consejos técnicos y a los colegios de profesores y presidir, con voz y voto, las sesiones de los primeros;</li>

              <li>Velar dentro de la facultad o escuela, por el cumplimiento de este Estatuto, de sus reglamentos, de los planes y programas de trabajo, y en general de las disposiciones y acuerdos que normen la estructura y el funcionamiento de la Universidad, dictando las medidas conducentes;</li>

              <li>Cuidar que dentro de la facultad o escuela se desarrollen las labores ordenada y eficazmente, aplicando las sanciones que sean necesarias, conforme al Estatuto General y sus reglamentos, y</li>

              <li>Profesar una cátedra en la facultad o escuela.</li>
						  </ol>
              <p align="justify"><em>Artículo 42.-</em> Cuando el director de una facultad o escuela no esté de acuerdo con algún dictamen del consejo técnico, pondrá el caso en conocimiento del Rector, quien lo turnará al Consejo Universitario o a la Junta de Gobierno, según la naturaleza del asunto.</p>

              <p align="justify"><em>Artículo 43.-</em> (Modificado en la sesión del Consejo Universitario del 2 de diciembre de 1997, publicado en Gaceta UNAM el 8 del mismo mes y año, como sigue):</p>

              <p align="justify"><em>Artículo 43.-</em> La conducción académica, la administración y las facultades disciplinarias de los planteles en que haya de distribuirse la población escolar del bachillerato estarán a cargo de directores de planteles dependientes de los directores generales de la Escuela Nacional Preparatoria y del Colegio de Ciencias y Humanidades. Los directores de los planteles serán nombrados y removidos por el Rector, en los términos que se señalen en los reglamentos respectivos; durarán en su encargo cuatro años, deberán poseer título superior al de bachiller y reunir los requisitos que señala el artículo 18, en sus fracciones I, II y IV de este ordenamiento.</p>

              <p align="justify">Los directores de los planteles tendrán las facultades establecidas en el artículo 41, fracciones III, IV, VI, VII y VIII de este ordenamiento; tratándose de proposiciones deberán hacerlas por conducto del director general.</p>

              <p align="justify"><em>Artículo 44.-</em> (Adicionado en la sesión del Consejo Universitario del 2 de marzo de 1971, como sigue):</p>

              <p align="justify"><em>Artículo 44.-</em> Los directores de facultades y escuelas de la Universidad constituirán un colegio con las atribuciones que señala el reglamento respectivo.</p>

              <h4>CAPÍTULO VI</h4>
              <h5>De los Consejos técnicos de las Facultades y Escuelas</h5>


              <p align="justify"><em>Artículo 45.-</em> Las facultades y escuelas de la Universidad tendrán como órganos de consulta necesaria, a los consejos técnicos, que estarán integrados de la siguiente manera:</p>
              <ol>
              <li>Los profesores tendrán un representante propietario y un suplente de cada una de las especialidades que se impartan en la facultad o escuela;</li>

              <li>Los técnicos académicos tendrán un representante propietario y un suplente por toda la entidad académica, y</li>

              <li>Los alumnos tendrán dos representantes propietarios y sus respectivos suplentes por toda la entidad académica.</li>
						  </ol>
              <p align="justify"><em>Artículo 46.-</em> (Modificado en la sesión del Consejo Universitario del 12 de septiembre de 1986, publicado en Gaceta UNAM el 29 de septiembre de 1986, como sigue):</p>

              <p align="justify"><em>Artículo 46.-</em> Los representantes profesores y técnicos académicos, propietario y suplente, durarán en su cargo cuatro años, deberán ser definitivos, satisfacer los requisitos establecidos en el artículo 19 de este ordenamiento y no podrán ser reelectos para el periodo inmediato, no obstante que se postulen por una especialidad distinta a la que representaron. Serán designados en elección directa, mediante voto universal, libre y secreto, por los académicos con antigüedad mayor de tres años en funciones docentes en la entidad respectiva.</p>


              <p align="justify"><em>Artículo 47.-</em> (Modificado en la sesión del Consejo Universitario del 12 de septiembre de 1986, y 11 de diciembre de 2013, publicado en Gaceta UNAM el 20 de enero de 2014, como sigue):</p>

              <p align="justify"><em>Artículo 47.-</em> Los alumnos designarán dos representantes propietarios y sus respectivos suplentes, en elección directa, mediante voto universal, libre y secreto; deberán cumplir con los requisitos establecidos en el artículo 17 del presente Estatuto, según corresponda al nivel y grado de estudios. Estos representantes durarán en su encargo dos años y no podrán ser reelectos.</p>

              <p align="justify">Los consejos técnicos calificarán las elecciones de los consejeros a que se refiere este artículo y el anterior.</p>

              <p align="justify"><em>Artículo 48.-</em> (Modificado en la sesión del Consejo Universitario del 24 de enero de 1966, publicado en Gaceta UNAM el 7 de febrero de 1966, como sigue):</p>

              <p align="justify"><em>Artículo 48.-</em> Los consejos técnicos de las facultades y escuelas serán presididos con voz y voto, por el director del plantel y, en su ausencia, por el más antiguo de los consejeros profesores.</p>

              <p align="justify"><em>Artículo 49.-</em> (Modificado en la sesión del Consejo Universitario del 4 de diciembre de 1963, como sigue):</p>

              <p align="justify"><em>Artículo 49.-</em> Serán obligaciones y facultades de los consejos técnicos:</p>
              <ol>
              <li>Estudiar y dictaminar los proyectos o iniciativas que les presenten el Rector, el director, los profesores y los alumnos o que surjan de su seno;</li>

              <li>Formular los proyectos de reglamento de la facultad o escuela y someterlos, por conducto del director, a la aprobación del Consejo Universitario;</li>

              <li>Estudiar los planes y programas de estudios para someterlos por conducto del director, a la consideración y aprobación, en lo general, del Consejo Universitario;</li>

              <li>Aprobar o impugnar las ternas que para director del plantel le sean enviadas por el Rector;</li>

              <li>Hacer observaciones a las resoluciones del Consejo Universitario o del Rector que tengan carácter técnico o legislativo y afecten a la facultad o escuela. Dichas observaciones deberán hacerse por mayoría de dos tercios de los votos computables del consejo técnico y no producirán otro efecto que el de someter el asunto a la decisión o reconsideración del Consejo Universitario, y</li>

              <li>Dictaminar sobre el nombramiento de profesores extraordinarios, elaborar los reglamentos especiales complementarios del Estatuto del Personal Académico y ejercer las facultades que éste les confiere.</li>
						  </ol>
              <p align="justify"><em>Artículo 50.-</em> Las decisiones de los consejos técnicos, salvo disposición en contrario de este Estatuto o de sus reglamentos, se tomarán a simple mayoría de votos.</p>

              <h4>CAPÍTULO VII</h4>
              <p align="justify">De los Consejos técnicos de la Investigación Científica y de Humanidades</p>


              <p align="justify"><em>Artículo 51.-</em> (Modificado en las sesiones del Consejo Universitario del 22 de septiembre de 1967, y 30 de mayo de 1985, publicado en Gaceta UNAM el 13 de junio de 1985, como sigue):</p>

              <p align="justify"><em>Artículo 51.-</em> El Consejo Técnico de la Investigación Científica estará integrado por:</p>
              <ol>
              <li>El Coordinador de la Investigación Científica, quien será su Presidente;</li>

              <li>El Director de la Facultad de Ciencias;</li>

