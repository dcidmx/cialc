							<p align="justify"><em>Artículo 126.-</em> El Consejo Académico del Bachillerato tendrá un Coordinador que será designado por el Rector, previa consulta al Consejo, en los términos de su reglamento interno, al cual corresponderá:</p>


              <ol>
              <li>Convocar y presidir con voz y voto al Consejo;</li>

              <li>Proponer al Consejo la designación de miembros de las comisiones permanentes y especiales y actuar como presidente ex oficio de las mismas;</li>

              <li>Cuidar del cumplimiento de las disposiciones que dicte el Consejo y ejecutar las decisiones de éste;</li>

              <li>Apoyar el enlace con los consejos académicos de área, así como con las autoridades y las dependencias universitarias;</li>

              <li>Asistir al Colegio de Directores del Bachillerato y al de Directores de Facultades y Escuelas, y</li>

              <li>Las demás que le confieran la Legislación Universitaria y el Rector.</li>
						  </ol>

              <p align="justify"><em>Artículo 127.-</em> Para desempeñar el cargo de Coordinador del Consejo Académico del Bachillerato se deberán reunir los siguientes requisitos:</p>


              <ol>
              <li>Tener cuando menos 30 años de edad y no más de 70 en el momento de la designación;</li>

              <li>Ser reconocido en su especialidad y haberse distinguido en las labores de investigación, de docencia y de difusión;</li>

              <li>Poseer un grado superior al de bachiller;</li>

              <li>Ser profesor de carrera o investigador, titular, definitivo, y</li>

              <li>No haber sido sancionado por cometer faltas graves contra la disciplina universitaria.</li>
						  </ol>

              <h5>SECCIÓN C</h5>

              <h5>De la Designación y Requisitos</h5>

              <p align="justify"><em>Artículo 128.-</em> Los profesores de la Escuela Nacional Preparatoria y los del Colegio de Ciencias y Humanidades, de cada una de las cuatro áreas que conforman al Consejo Académico del Bachillerato y que tengan más de tres años de antigüedad, elegirán, respectivamente, en forma directa, mediante voto universal, libre y secreto, cada cuatro años a dos miembros por cada una de las áreas.</p>


              <p align="justify"><em>Artículo 129.-</em> Los alumnos inscritos en la Escuela Nacional Preparatoria y en el Colegio de Ciencias y Humanidades elegirán, respectivamente, en forma directa, mediante voto universal, libre y secreto, cada dos años a cuatro miembros del Consejo Académico del Bachillerato.</p>


              <p align="justify"><em>Artículo 130.-</em> Por cada consejero profesor, técnico académico o alumno propietario se elegirá un suplente que deberá cumplir con los mismos requisitos que aquél. Los suplentes podrán asistir a las sesiones del pleno y participar con voz pero sin voto cuando acuda el propietario, y con voz y voto en ausencia del mismo. Los consejeros suplentes formarán parte de alguna de las comisiones permanentes con voz y voto, de acuerdo con las bases para el funcionamiento de las comisiones que expida en Consejo Académico del Bachillerato. En el caso de que el propietario quede impedido de forma permanente para terminar el periodo, el suplente asumirá la titularidad en las sesiones plenarias.</p>


              <p align="justify"><em>Artículo 131.-</em> Para ser consejero académico por los profesores o técnicos académicos será necesario cumplir los siguientes requisitos:</p>


              <ol>
              <li>Ser reconocido en su especialidad y haberse distinguido en sus labores docentes, técnicas o de difusión;</li>

              <li>Poseer un grado superior al de bachiller;</li>

              <li>En el caso de los representantes profesores, ser profesor de carrera, titular, definitivo, con más de seis años de servicios docentes en la Escuela Nacional Preparatoria o en el Colegio de Ciencias y Humanidades, según sea el caso, en el área correspondiente; o ser profesor de asignatura B definitivo, con amplio reconocimiento profesional, con más de seis años de servicios docentes;</li>

              <li>En el caso de los representantes técnicos académicos, ser técnico académico titular, definitivo, con más de seis años de antigüedad en la Escuela Nacional Preparatoria o en la Escuela Nacional "Colegio de Ciencias y Humanidades", según sea el caso.</li>

              <li>No ocupar en la Universidad ningún puesto administrativo o académico-administrativo al momento de la elección, ni durante el desempeño de su cargo, y</li>

              <li>No haber sido sancionado por cometer faltas graves contra la disciplina universitaria.</li>
						  </ol>

              <p align="justify"><em>Artículo 132.-</em> Para ser consejero académico por los alumnos será necesario cumplir los siguientes requisitos:</p>


              <ol>
              <li>Estar inscrito por lo menos en el quinto año del plan de estudios de la Escuela Nacional Preparatoria, o estar inscrito por lo menos en el tercer semestre del plan de estudios del Colegio de Ciencias y Humanidades;</li>

              <li>Tener acreditadas todas las materias de los años o semestres anteriores, según corresponda, al momento de la elección;</li>

              <li>Tener un promedio de calificaciones mínimo de 8.5;</li>

              <li>No ocupar en la Universidad ningún puesto administrativo o académico-administrativo al momento de la elección ni durante el desempeño de su cargo, y</li>

              <li>No haber sido sancionado por cometer faltas graves contra la disciplina universitaria.</li>
						  </ol>
              <h5>SECCIÓN D</h5>

              <h5>Del Funcionamiento</h5>

              <p align="justify"><em>Artículo 133.-</em> El Consejo Académico del Bachillerato trabajará en pleno o en comisiones que podrán ser permanentes o especiales; son permanentes las que siguen:



              Planeación y Evaluación;
              Planes y Programas de Estudios;
              Personal Académico, y
              Difusión.</p>


              <p align="justify"><em>Artículo 134.-</em> Las comisiones especiales serán las que el Consejo designe para estudiar y dictaminar otros asuntos de su competencia. Algunas de éstas incluirán expertos designados por el propio Consejo quienes tendrán carácter de asesores invitados.</p>


              <p align="justify"><em>Artículo 135.-</em> El Consejo Académico del Bachillerato celebrará sesiones ordinarias cada dos meses, y extraordinarias cuando lo considere necesario el Coordinador o un grupo de consejeros que represente cuando menos una cuarta parte del total de éstos.</p>


              <p align="justify"><em>Artículo 136.-</em> En cada sesión ordinaria las comisiones permanentes tendrán la obligación de rendir un informe de trabajo al pleno del Consejo acerca de los asuntos de su competencia.</p>


              <p align="justify"><em>Artículo 137.-</em> El funcionamiento del Consejo Académico del Bachillerato se realizará de conformidad a lo siguiente:</p>


              <ol>
              <li>Cuando el Consejo funcione en pleno actuará válidamente con más de la mitad de sus miembros, a menos que se traten casos en los que se requiera un quorum especial;</li>

              <li>Si por falta de quorum se suspende una sesión, se citará para una segunda que podrá efectuarse con los consejeros concurrentes;</li>

              <li>Salvo prevención de este ordenamiento o de los reglamentos, el Consejo adoptará sus resoluciones por mayoría simple de votos, y</li>

              <li>Las votaciones serán económicas, a menos que el Coordinador o dos consejeros pidan que sean nominales, por cédulas o secretas. Sólo tendrán derecho a votar los consejeros presentes en la sesión.</li>
						  </ol>

              <p align="justify"><em>Artículo 138.-</em> Cuando el Consejo funcione por comisiones, éstas se considerarán legalmente instaladas con la mitad más uno de sus miembros y tomarán decisiones válidamente por mayoría de votos. Si no hay quorum se citará por segunda ocasión y la comisión tomará sus determinaciones por mayoría de los miembros que asistan, cualquiera que sea su número, considerándose legalmente instalada.</p>


              <p align="justify"><em>Artículo 139.-</em> Cuando los respectivos directores generales de la Escuela Nacional Preparatoria o del Colegio de Ciencias y Humanidades no puedan asistir a una sesión, podrá asistir, con voz y voto, un representante designado por ellos. En todo caso el representante deberá cumplir los requisitos académicos establecidos para los consejeros profesores, excepto la fracción IV del artículo 131.</p>

              <h4>CAPÍTULO III</h4>



              <h5>Del Consejo de Difusión Cultural</h5>

              <h5>SECCIÓN A</h5>

              <h5>Generalidades</h5>

              <p align="justify"><em>Artículo 140.-</em> El Consejo de Difusión Cultural es un órgano colegiado que tiene como objetivos fortalecer y articular las tareas, programas y actividades de las dependencias adscritas a la Coordinación de Difusión Cultural, coadyuvar a la articulación de difusión cultural que realiza la Universidad a fin de que contribuya a la formación integral de los universitarios y colaborar a difundir con la mayor amplitud posible los valores culturales, particularmente los artísticos.</p>


              <p align="justify"><em>Artículo 141.-</em> Las funciones del Consejo de Difusión Cultural serán las siguientes:</p>


              <ol>
              <li>Formular y proponer políticas generales de la Coordinación de Difusión Cultural;</li>

              <li>Participar en la elaboración y evaluación de los programas de trabajo de la Coordinación de Difusión Cultural;</li>

              <li>Proponer lineamientos de planeación y evaluación en materia de difusión cultural, particularmente artística, y asesorar, en relación con estos temas, a las instancias, entidades académicas y dependencias universitarias;</li>

              <li>Formular y proponer, al Consejo Universitario, los lineamientos generales para la creación, integración, desconcentración o supresión de dependencias académicas o programas adscritos a la Coordinación de Difusión Cultural;</li>

              <li>Propiciar proyectos de creación artística y coadyuvar a su realización y difusión;</li>

              <li>Coadyuvar con las entidades académicas en la formación integral de los alumnos de bachillerato, licenciatura y posgrado con el ofrecimiento de actividades culturales extracurriculares que podrán ser acreditadas en planes y programas de estudios;</li>

              <li>Apoyar a las comisiones de difusión cultural de los consejos académicos de área en la realización de sus tareas;</li>

              <li>Coadyuvar a la preservación del patrimonio artístico universitario;</li>

              <li>Recomendar criterios para que el presupuesto de la Coordinación de Difusión Cultural responda a las prioridades que surjan de la planeación de sus actividades;</li>

              <li>Proponer modificaciones a la integración del Consejo de Difusión Cultural y opinar sobre propuestas al respecto;</li>

              <li>Formular su reglamento interno y someterlo para su conocimiento y, en su caso aprobación, al Consejo Universitario, y</li>

              <li>Llevar a cabo todas aquellas funciones que le atribuya la Legislación Universitaria.</li>
						  </ol>

