              <p align="justify">(Este Título fue adicionado en la sesión extraordinaria del Consejo Universitario del día 26 de agosto de 2011, publicado en Gaceta UNAM el día 12 de septiembre del mismo año, modifica o deroga al Título Transitorio de los Consejos Académicos de Área y el Consejo Académico del Bachillerato, y al Título Transitorio del Consejo de Difusión Cultural de este ordenamiento, como sigue):</p>


              <h4>TÍTULO OCTAVO</h4>
              <h5>De los Consejos Académicos de Área, el Consejo Académico del Bachillerato y el Consejo de Difusión Cultural</h5>
              <h4>CAPÍTULO I</h4>



              <h5>De los Consejos Académicos de Área</h5>
              <h5>SECCIÓN A</h5>

              <h5>De su Naturaleza, Objetivos y Funciones</h5>

              <p align="justify"><em>Artículo 103.-</em> Los consejos académicos de área son cuerpos colegiados con funciones propias por acuerdo y aprobación del Consejo Universitario, que los asume como órganos de carácter propositivo, de planeación, evaluación y decisión académicas. Tienen como objetivos fortalecer las tareas sustantivas de la Universidad, promover la articulación entre sus diversos niveles, disciplinas y funciones académicas, y propiciar el óptimo aprovechamiento y desarrollo de sus recursos.</p>

              <p align="justify"><em>Artículo 104.-</em> Los consejos académicos de área tendrán las siguientes funciones:</p>


              <ol>
              <li>Formular y proponer las políticas académicas generales del área y coordinar las acciones conducentes a la elaboración de los programas generales de docencia, investigación, extensión y difusión del área;</li>

              <li>Evaluar los programas de trabajo y las actividades académicas del área, y proponer medidas para su coordinación y fortalecimiento;</li>

              <li>Formular y proponer, al Consejo Universitario, los lineamientos generales para la creación, integración, desconcentración o supresión de dependencias académicas o programas del área;</li>

              <li>Opinar sobre la creación, integración, desconcentración o supresión de entidades académicas o programas de su área;</li>

              <li>Promover la generación de proyectos multi e interdisciplinarios y coadyuvar a su realización, mediante la participación de diversas entidades y dependencias universitarias con personal académico, con el fin de evitar la creación de estructuras que impliquen costos innecesarios a la Institución;</li>

              <li>Recomendar criterios para que la elaboración del presupuesto de la Universidad responda a las necesidades que surjan de la planeación de las actividades académicas;</li>

              <li>Formular, con fundamento en el Estatuto del Personal Académico, normas generales dirigidas a los órganos colegiados para la selección, evaluación y promoción del personal académico del área;</li>

              <li>Designar, a través de su Comisión Permanente de Personal Académico, a dos miembros de cada Comisión Dictaminadora del área correspondiente;</li>

              <li>Ratificar la integración de las Comisiones Dictaminadoras de su área;</li>

              <li>Proponer al Consejo Universitario lineamientos generales para la creación, modificación, actualización y cancelación de planes y programas de estudio, e incorporar en ellos el diseño de estructuras curriculares flexibles entre planes de una misma entidad, que vinculen a diversas entidades académicas. Asimismo, generar mecanismos que faciliten el tránsito entre los niveles educativos que ofrece la Institución, incluidos los programas de educación continua;</li>

              <li>Revisar y, en su caso, aprobar la creación de planes y programas de estudio ya establecidos en una o más entidades académicas de la Universidad, así como la modificación de planes vigentes o su adecuación, la modificación de programas de posgrado y la creación de campos de conocimiento en los programas de posgrado existentes. Asimismo, la cancelación de planes y programas de estudio en concordancia con los ordenamientos correspondientes; su constante actualización, de acuerdo con los lineamientos y criterios de la Comisión de Trabajo Académico del Consejo Universitario;</li>

              <li>Emitir los acuerdos de aprobación de planes y programas de estudio interdisciplinarios o combinados, para la titulación y graduación simultánea o conjunta; de la creación de Orientaciones Interdisciplinarias de Posgrado; del otorgamiento de un nuevo grado, o del cambio de denominación de un plan de estudios de licenciatura o posgrado, los cuales podrán ser aprobados por el pleno del Consejo Universitario;</li>

              <li>Coadyuvar a la definición de políticas generales de ingreso y de orientación educativa de los alumnos del área;</li>

              <li>Establecer los criterios académicos para la creación y asignación de becas;</li>

              <li>Establecer los criterios académicos para movilidad, internacionalización y cooperación en el área, a partir de los lineamientos y criterios de la Comisión de Trabajo Académico del Consejo Universitario;</li>

              <li>Promover y coordinar los estudios necesarios para mantener actualizada la información y el conocimiento del área;</li>

              <li>Coadyuvar a la determinación y continua evaluación de los propósitos educativos y de los conocimientos fundamentales para la enseñanza de la educación media superior, así como de los estudios técnicos, profesionales y de posgrado que la Universidad ofrece; y formular los instrumentos necesarios para dar cumplimiento a tales propósitos;</li>

