              <h4>ORGANIZACIÓN GENERAL DE LA UNAM</h4>

              <h5>ESTATUTO GENERAL DE LA
              UNIVERSIDAD NACIONAL AUTÓNOMA DE MÉXICO</h5>

              <h6>Modificado el nombre en la sesión del Consejo Universitario de 23 de octubre de 1962.</h6>



              <h4>TÍTULO PRIMERO</h4>
              <h5>Personalidad y Fines</h5>


              <p align="justify"><em>Artículo 1o.-</em> La Universidad Nacional Autónoma de México es una corporación pública -organismo descentralizado del Estado- dotada de plena capacidad jurídica y que tiene por fines impartir educación superior para formar profesionistas, investigadores, profesores universitarios y técnicos útiles a la sociedad; organizar y realizar investigaciones principalmente acerca de las condiciones y problemas nacionales, y extender con la mayor amplitud posible los beneficios de la cultura.</p>

              <p align="justify"><em>Artículo 2o.-</em> (Adicionado en la sesión del Consejo Universitario del 31 de marzo de 2005, publicado en Gaceta UNAM el 14 de abril del mismo año, como sigue):</p>

              <p align="justify"><em>Artículo 2o.-</em> Para realizar sus fines, la Universidad se inspirará en los principios de libre investigación y libertad de cátedra y acogerá en su seno, con propósitos exclusivos de docencia e investigación, todas las corrientes del pensamiento y las tendencias de carácter científico y social; pero sin tomar parte en las actividades de grupos de política militante, aun cuando tales actividades se apoyen en aquellas corrientes o tendencias.</p>

              <p align="justify">En todos los casos las mujeres y los hombres en la Universidad gozarán de los mismos derechos, obligaciones y prerrogativas, reconocidos y garantizados por las normas y disposiciones que integran la legislación universitaria.</p>


              <p align="justify"><em>Artículo 3o.-</em> El propósito esencial de la Universidad, será estar íntegramente al servicio del país y de la humanidad, de acuerdo con un sentido ético y de servicio social, superando constantemente cualquier interés individual.</p>

              <p align="justify"><em>Artículo 4o.-</em> La educación superior que la Universidad imparta, comprenderá el bachillerato, la enseñanza profesional, los cursos de graduados, los cursos para extranjeros y los cursos y conferencias para la difusión de la cultura superior y la extensión universitaria. Para realizar su función docente y de investigación, la Universidad establecerá las facultades, escuelas, institutos y centros de extensión universitaria que juzgue conveniente, de acuerdo con las necesidades educativas y los recursos de que pueda disponer.</p>

              <p align="justify"><em>Artículo 5o.-</em> La Universidad otorgará el certificado, diploma, título o grado correspondiente a las personas que hayan concluido los ciclos de bachillerato, técnico especializado, técnico profesional, licenciatura o de posgrado y satisfecho, además, las condiciones que fijen los reglamentos respectivos. Los que no hubieren concluido algunos de los ciclos mencionados, tendrán derecho a recibir un certificado de los estudios que hubieren aprobado.</p>

              <p align="justify"><em>Artículo 6o.-</em> La Universidad tendrá derecho a otorgar, para fines académicos, validez a los estudios que se hagan en otros establecimientos educativos, nacionales o extranjeros, de acuerdo con el Reglamento de Grados y Revalidación de Estudios, y para incorporar enseñanzas de bachilleratos o profesionales, siempre que los planteles en que se realizan tengan identidad de planes, programas y métodos para estimar el aprovechamiento, en relación con los que estén vigentes en la Universidad, comprobándose esta identidad en la forma que lo indiquen los reglamentos respectivos. Por lo que se refiere a otros tipos de enseñanza que no se imparten en las escuelas de bachillerato o profesionales, se exigirá el certificado de revalidación que corresponda, expedido, por la autoridad respectiva.</p>

