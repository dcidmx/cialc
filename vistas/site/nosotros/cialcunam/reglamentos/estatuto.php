              <h4>CAPÍTULO I</h4>
              <h5>De la Junta de Gobierno</h5>


              <p align="justify"><em>Artículo 13.-</em> La Junta de Gobierno se integrará y renovará en la forma establecida en los artículos 4o. y 5o. de la Ley y tendrá las facultades que enumera el artículo 6o. de la misma.</p>

              <p align="justify"><em>Artículo 14.-</em> Las relaciones entre la Junta de Gobierno y las restantes autoridades universitarias se mantendrán por conducto del Rector, sin perjuicio de la facultad de aquélla para hacer comparecer a sus sesiones a cualquier miembro de la comunidad universitaria.</p>

              <h4>CAPÍTULO II</h4>
              <h5>Del Consejo Universitario</h5>


              <p align="justify"><em>Artículo 15.-</em> El Consejo Universitario se integrará en la forma establecida en el artículo 7o. de la Ley y tendrá las facultades que enumera el artículo 8o. de la Ley.</p>

