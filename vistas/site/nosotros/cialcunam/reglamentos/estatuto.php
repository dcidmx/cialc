              <h4>TRANSITORIOS</h4>
              <p align="justify"><em>PRIMERO.-</em> El presente Estatuto entrará en vigor el día 12 de marzo de 1945.</p>



              <p align="justify"><em>SEGUNDO.-</em> El Instituto de Geofísica empezará a funcionar cuando lo permitan las condiciones económicas de la Universidad.</p>



              <p align="justify"><em>TERCERO.-</em> (Modificado en la sesión del Consejo Universitario del 4 de septiembre de 1962, como sigue):</p>



              <p align="justify"><em>TERCERO.-</em> Para ser nombrado director de la Escuela Nacional de Artes Plásticas, no es aplicable la fracción IV del artículo 38, en tanto que en dicha escuela no haya por lo menos, diez profesores con los grados que en ella se otorgan y que cumplan con los demás requisitos exigidos por el citado artículo 38 del Estatuto General de la Universidad.</p>



              <p align="justify">Mientras, bastará con la comprobación de la obra artística o de crítica de arte realizada.</p>



              <p align="justify"><em>CUARTO.-</em> (Este artículo fue derogado en la sesión del Consejo Universitario del 4 de septiembre de 1962. Con motivo de esta derogación se recorre la numeración de los siguientes):</p>



              <p align="justify"><em>CUARTO.-</em> Los directores de las facultades y escuelas convocarán a Junta de Profesores en ejercicio para que en ellas se formule la lista de materias afines, a que se refiere el artículo 44, y que servirá de base para la primera elección de los consejos técnicos. Si no se reúne el quórum correspondiente en la primera sesión se convocará a una segunda, pudiéndose tomar las determinaciones cualquiera que sea el número de asistencia. Las Juntas de Profesores deberán dictar su resolución en un plazo de diez días a partir de la primera convocatoria.</p>



              <p align="justify"><em>QUINTO.-</em> La designación de los Consejeros Universitarios representantes de los profesores de las facultades y escuelas así como de los miembros profesores de los consejos técnicos se hará, por esta vez aplicando en lo conducente el artículo primero del reglamento de la cuarta base aprobada por la junta de ex Rectores para el Gobierno provisional de la Institución, de fecha 6 de septiembre de 1944. Las actas serán levantadas por las personas que designe el Rector.</p>



              <p align="justify"><em>SEXTO.-</em> Los electores a que se refieren los artículos 18 y 45 del presente Estatuto se elegirán por votación escrita en cédulas y será supervisada por el representante que nombre la Rectoría. En todo caso se exigirá la presentación de la credencial para que los alumnos ejerzan su derecho de voto.</p>



              <p align="justify">Nombrados los electores, la designación de los consejeros universitarios y de los miembros alumnos al consejo técnico se hará conforme a la fracción V del artículo 2º del reglamento a que se refiere el artículo anterior. En primer término se llevará a cabo la elección de los consejeros propietario y suplente al Consejo Universitario y después la de los representantes a los consejos técnicos.</p>



              <p align="justify"><em>SÉPTIMO.-</em> Las direcciones de las facultades y escuelas que queden vacantes al entrar en vigor el Estatuto quedarán provisionalmente a cargo del más antiguo de los profesores cuya edad no exceda el límite fijado en el artículo 38, en tanto se provee la designación de los titulares.</p>



              <p align="justify"><em>OCTAVO.-</em> Una comisión formada por dos profesores de la actual Escuela Nacional Preparatoria y dos de la actual Escuela de Iniciación Universitaria, nombrados por el Consejo Universitario, a propuesta del Rector y encabezada por el Presidente de la Comisión del Trabajo Docente, formará en un plazo no mayor de tres meses que se contarán a partir del día de su nombramiento, un escalafón único de la Escuela Nacional Preparatoria que se someterá a la aprobación del Consejo Universitario. Entre tanto continuarán los actuales escalafones para los efectos del inciso b) de la fracción III del artículo 64.</p>



              <p align="justify"><em>NOVENO.-</em> Dentro de los seis meses siguientes a la vigencia del presente Estatuto se formularán los reglamentos a que se refiere el artículo 13 de la Ley.</p>



              <p align="justify"><em>DÉCIMO.-</em> Quedan derogados el Estatuto de 1938 y todos los reglamentos y demás disposiciones legislativas de la Universidad en cuanto se opongan a este ordenamiento.</p>



              <p align="justify">El Estatuto fue aprobado en las sesiones del Consejo Universitario de los días 12, 14, 16, 19, 21, 23 y 26 de febrero; 2, 5, 7, 8 y 9 de marzo de 1945.</p>



              <p align="justify"><em>ONCE.-</em> (Adicionado como transitorio único en la sesión del Consejo Universitario del 25 de enero de 1957, con motivo de las reformas aprobadas por el Consejo Universitario en la sesión del 23 de octubre de 1962, se le designa once transitorio):</p>



              <p align="justify"><em>ONCE.-</em> El Rector proveerá la incorporación de los actuales profesores de la Escuela de Graduados a otras facultades o escuelas y el reconocimiento de los estudios realizados en dicha escuela.</p>



              <p align="justify">(En la sesión del Consejo Universitario del 23 de octubre de 1962, se aprueba la integración de los artículos transitorios del doce al veintidós).</p>

              <p align="justify"><em>DOCE.-</em> (Adicionado en la sesión del Consejo Universitario del 23 de octubre de 1962, derogado en la sesión del Consejo Universitario del 22 de septiembre de 1967, recorriendo la numeración, para quedar como sigue):</p>



              <p align="justify"><em>DOCE.-</em> Lo dispuesto en el artículo 65 de este Estatuto, no se aplicará a los profesores, que al concluir el año lectivo de 1962, hayan prestado servicios docentes durante seis o más años, si el consejo técnico de la facultad o escuela respectiva, en vista del expediente de los mismos, considera a su juicio que su curriculum académico es satisfactorio, que en el ejercicio de su cátedra han demostrado aptitud suficiente y que han cumplido con puntualidad y eficacia sus labores docentes.</p>



              <p align="justify">En los casos en que se reúnan estas circunstancias, el consejo técnico, propondrá el nombramiento como profesor titular de la materia que imparte, a favor del interesado.</p>



              <p align="justify"><em>TRECE.-</em> Quienes hayan cumplido más de tres años de prestar servicios docentes al concluir el año lectivo de 1962, pero sin haber alcanzado los seis años a que se refiere el artículo anterior, mediante acuerdo del consejo técnico podrán ser propuestos para ser profesores titulares de la materia que imparten, sin quedar sujetos a lo dispuesto en el artículo 65 de este Estatuto, si existen suficientes vacantes definitivas en dicha materia y satisfacen los siguientes requisitos:</p>


              <ol>
              <li>Que la persona propuesta tenga un grado superior al de bachiller, o un título de los que se obtienen en la escuela correspondiente, que haga presumir el conocimiento suficiente de la materia que imparte;</li>



              <li>Que el interesado se haya distinguido en su profesión o en sus actividades académicas, lo cual puede resultar de una o varias de estas circunstancias:</li>


              <ol>
              <li>De que haya obtenido el grado de doctor o maestro en la disciplina que corresponda a la asignatura que imparte;</li>



              <li>Que haya presentado anteriormente oposición a la cátedra, y, sin haberla obtenido, haya sido encontrado apto para la docencia;</li>



              <li>De que haya publicado libros, monografías, ensayos o trabajos sobre la materia que imparte, que se estimen de calidad satisfactoria;</li>



              <li>De que haya obtenido el carácter de profesor definitivo de la misma materia o de una similar, en otra facultad o escuela universitaria, o del mismo nivel;</li>



              <li>De que haya realizado los estudios de la maestría o del doctorado que corresponda a la asignatura que imparte aun sin haber llegado a obtener el grado respectivo, y</li>



              <li>De que haya desempeñado o esté desempeñando puestos técnicos que impliquen el reconocimiento general de su sobresaliente reputación, dentro de la profesión a que pertenezca y que disfrute también de la fama pública de desempeñar o haber desempeñado satisfactoriamente su cometido.</li>
						  </ol>


              <li>Que la persona de quien se trate haya desempeñado con puntualidad y eficiencia sus labores docentes.</li>
						  </ol>


              <p align="justify"><em>CATORCE.-</em> Las personas que tengan la antigüedad que señala el artículo anterior, mediante acuerdo del consejo técnico podrán ser propuestas como profesores adjuntos, sin que cumplan con el requisito previsto en la fracción I, si imparten su enseñanza en la Escuela Nacional Preparatoria. Del mismo beneficio disfrutarán los profesores de otra escuela o de una facultad, pero sólo en materia de adiestramiento en actividades manuales, dibujo u otras similares e idiomas extranjeros. En tales casos el consejo técnico podrá apreciar, sin sujeción estricta a las reglas señaladas en la fracción II del artículo anterior, si la persona que ha venido desempeñando el cargo tiene antecedentes académicos suficientes, que hagan presumir el conocimiento de la materia que imparte.</p>



              <p align="justify">Quienes teniendo la antigüedad señalada, no hayan concluido el bachillerato, podrán ser nombrados profesores interinos, durante el año lectivo de 1963, si poseen antecedentes académicos suficientes y manifiestan estar en posibilidad de acreditar, al finalizar dicho año, que han terminado el bachillerato. En este caso, al acreditarlo, le será aplicable lo dispuesto en el primer párrafo de este artículo.</p>



