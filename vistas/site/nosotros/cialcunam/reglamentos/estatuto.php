              <h5>Del Consejo Académico del Bachillerato</h5>

              <h5>SECCIÓN A</h5>

              <h5>De la Naturaleza, Objetivos y Funciones</h5>

              <p align="justify"><em>Artículo 123.-</em> El Consejo Académico del Bachillerato es un órgano colegiado propositivo, de planeación, evaluación y decisión académicas, que tiene como objetivos fortalecer las tareas sustantivas del bachillerato, así como propiciar el óptimo aprovechamiento y desarrollo de sus recursos. Tendrá además la función de articular las actividades académicas de la Escuela Nacional Preparatoria con las del Colegio de Ciencias y Humanidades, y las de éstos con los consejos académicos de área y las dependencias que los conforman.</p>


              <p align="justify"><em>Artículo 124.-</em> El Consejo Académico del Bachillerato tendrá las siguientes funciones:</p>


              <ol>
              <li>Formular y proponer las políticas académicas generales del bachillerato, y coordinar las acciones conducentes a la elaboración de sus programas académicos;</li>

              <li>Evaluar los programas de trabajo y las actividades académicas del bachillerato y proponer medidas para su coordinación y fortalecimiento;</li>

              <li>Recomendar criterios para que la elaboración del presupuesto del bachillerato responda a las necesidades que surjan de la planeación de las actividades académicas;</li>

              <li>Formular, con fundamento en el Estatuto del Personal Académico, normas generales para la selección, evaluación y promoción del personal académico del bachillerato;</li>

              <li>Designar dos miembros de cada Comisión Dictaminadora del bachillerato;</li>

              <li>Revisar y, en su caso, aprobar la creación, modificación y cancelación de planes y programas de estudio del bachillerato, y llevar a cabo su constante actualización, de acuerdo con los lineamientos generales que establezca el Consejo Universitario con fundamento en los ordenamientos correspondientes;</li>

              <li>Coadyuvar con los consejos técnicos del bachillerato a la definición de políticas generales de ingreso y de orientación educativa de los alumnos de este ciclo de estudios;</li>

              <li>Promover y coordinar los estudios necesarios para mantener actualizada la información y el conocimiento sobre el bachillerato en su razón de ser y su sentido histórico;</li>

              <li>Coadyuvar a la definición y continua evaluación de los objetivos educativos de los conocimientos fundamentales para la enseñanza de la educación media superior;</li>

              <li>Coadyuvar a la formulación de los instrumentos de evaluación necesarios para el cumplimiento de los propósitos educativos del bachillerato;</li>

              <li>Coadyuvar a la definición de las características generales del personal académico requerido por el bachillerato, de manera que se dé cumplimiento a las tareas académicas programadas;</li>

              <li>Formular, con fundamento en el Estatuto del Personal Académico, requisitos generales para ser miembro de las Comisiones Dictaminadoras del bachillerato;</li>

              <li>Impulsar la creación de comisiones de trabajo académico para la atención de asuntos específicos;</li>

              <li>Formular su reglamento interno y someterlo, para su aprobación, al Consejo Universitario;</li>

              <li>Establecer vínculos con las coordinaciones de la Investigación Científica, de Humanidades y de Difusión Cultural y los cuerpos colegiados y dependencias que puedan coadyuvar a la consecución de sus funciones;</li>

              <li>Informar anualmente al Consejo Universitario sobre las labores realizadas y los resultados obtenidos, y</li>

              <li>Llevar a cabo todas aquellas funciones académicas que le delegue el Consejo Universitario, y las demás que le confieran la Legislación Universitaria.</li>
