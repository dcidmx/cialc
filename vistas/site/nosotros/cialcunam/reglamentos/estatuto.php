              <h4>CAPÍTULO IX</h4>
              <h5>De la Administración</h5>

              <p align="justify"><em>Artículo 63.-</em> (Modificado en las sesiones del Consejo Universitario del 27 de febrero de 1947, y 4 de diciembre de 1963, como sigue):</p>

              <p align="justify"><em>Artículo 63.-</em> Para formular el plan de arbitrios, el Patronato estimará los ingresos probables del ejercicio siguiente y tomará en cuenta los efectivamente habidos en los tres años anteriores.</p>

              <p align="justify">Para fijar los ingresos probables, se considerará el subsidio del Gobierno Federal y lo que haya de percibirse conforme al acuerdo que dicte el Consejo en el mes de octubre de cada año, respecto de cuotas por inscripción, exámenes ordinarios, extraordinarios o profesionales, por expedición de títulos y certificados, y cualquier otro ingreso, ordinario o extraordinario.</p>

              <p align="justify"><em>Artículo 64.-</em> El Consejo reglamentará los casos en que deba concederse a los alumnos el beneficio de diferición total o parcial del pago, con sujeción a las siguientes bases:</p>
              <ol>
              <li>Que no hayan sido reprobados más de una vez, que su promedio de calificaciones sea de 7 como mínimo en la facultad o escuela a que pertenezcan y que no hayan cometido faltas graves contra la disciplina;</li>

              <li>Que acrediten los requisitos de la fracción anterior, en los estudios hechos en la facultad o escuela de que procedan, en caso de ser alumnos de primer ingreso;</li>

              <li>Que su situación económica lo justifique;</li>

              <li>Que la solicitud se presente a más tardar durante los diez primeros días de la fecha de apertura de inscripciones, y</li>

              <li>Que los peticionarios o sus representantes legales suscriban los documentos de reconocimiento de obligación y promesa de pago que fijen los reglamentos respectivos.</li>
						  </ol>
              <p align="justify"><em>Artículo 65.-</em> El presupuesto contendrá el programa de actividades, obras y servicios a cargo de la Universidad, en forma de previsiones de egresos.</p>

              <p align="justify"><em>Artículo 66.-</em> (Modificado en las sesiones del Consejo Universitario del 27 de febrero de 1947, y 4 de diciembre de 1963, como sigue):</p>

              <p align="justify"><em>Artículo 66.-</em> El Patronato, oyendo a la Comisión de Presupuestos del Consejo Universitario y al Rector, formulará el proyecto del presupuesto en el mes de octubre de cada año y lo acompañará para su discusión en el Consejo, en el mes de diciembre siguiente, con los siguientes documentos:</p>
              <ol>
              <li>Informe de la situación hacendaria de la Universidad durante el ejercicio en curso y de las condiciones previstas para el siguiente;</li>

              <li>Estimación total de ingresos señalados en el plan de arbitrios para el ejercicio venidero;</li>

              <li>Previsiones de egresos destinados para cada ramo del presupuesto;</li>

              <li>Comparación de las estimaciones para el ejercicio siguiente con las recaudaciones habidas en los meses pasados del ejercicio en curso y las probables de los que falten del mismo;</li>

              <li>Comparación de las estimaciones de egresos del ejercicio venidero con los gastos hechos en los meses transcurridos del ejercicio en curso y la estimación de los que falten, y</li>

              <li>Comparación por ramos del proyecto de presupuesto de egresos que se presenta para el siguiente ejercicio con las autorizaciones originales y reformas que se hubieren hecho al presupuesto de egresos del año en curso.</li>
						  </ol>
              <p align="justify">El documento que el Patronato someta al Consejo irá suscrito por el Rector y por los miembros de la Comisión de Presupuestos del Consejo, con indicación de su conformidad o de los puntos concretos de discrepancia.</p>

              <p align="justify"><em>Artículo 67.-</em> Los gastos se clasificarán por ramos de la Administración de la Universidad y por facultades, escuelas e institutos. Además de esa agrupación funcional, las provisiones de autorización se calcularán por su naturaleza conforme a las bases siguientes:</p>
              <ol>
              <li>Como grupos fundamentales de autorización se considerarán los gastos, elaboraciones, construcciones, adquisiciones, inversiones, cancelaciones de pasivo y erogaciones especiales;</li>

              <li>Estos capítulos se subdividirán en conceptos, o sea en grupos de autorizaciones de naturaleza semejante, y</li>

              <li>Los conceptos se subdividirán, a su vez, en partidas representadas por las autorizaciones específicas del presupuesto.</li>
						  </ol>
              <p align="justify"><em>Artículo 68.-</em> La asignación de las partidas fija el límite de las erogaciones. Las partidas no son transferibles ni el presupuesto podrá ser objeto de modificaciones sino con autorización del Consejo, y previo dictamen de la Comisión de Presupuestos y del Patronato.</p>

              <p align="justify"><em>Artículo 69.-</em> El contador público a que se refiere la fracción III del artículo 10 de la Ley Orgánica, tendrá las más amplias facultades para vigilar los ingresos y los egresos y será auxiliado en sus funciones por el personal técnico y administrativo de la Universidad, pudiendo nombrar un auditor de su confianza para el desempeño de sus trabajos.</p>

              <p align="justify"><em>Artículo 70.-</em> (Modificado en las sesiones del Consejo Universitario del 27 de febrero de 1947, y 4 de diciembre de 1963, como sigue):</p>

              <p align="justify"><em>Artículo 70.- </em>El ejercicio del presupuesto abarcará el periodo comprendido entre el 1o. de enero y el 31 de diciembre de cada año y se ajustará a las disposiciones del reglamento que, a propuesta del Patronato, aprobará el Consejo y a las normas que en el mismo presupuesto se señalen.</p>

              <p align="justify"><em>Artículo 71.-</em> Ninguna persona podrá percibir en la Universidad retribución que no esté específicamente asignada o que no derive de partida expresa del presupuesto.</p>

              <p align="justify"><em>Artículo 72.-</em> Queda estrictamente prohibida la acumulación de empleos, y, en consecuencia, los funcionarios o empleados de la Universidad sólo podrán desempeñar un cargo administrativo y profesar en las facultades o escuelas de la Universidad, o realizar en los Institutos trabajos de investigación, a condición de que las horas de docencia o investigación sean compatibles con las de su trabajo en las oficinas.</p>

