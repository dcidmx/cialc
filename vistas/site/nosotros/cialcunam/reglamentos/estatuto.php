              <p align="justify"><em>SEGUNDO.-</em> Hasta en tanto se realicen las modificaciones al Reglamento para la Elección de Consejeros Universitarios y Técnicos Representantes de Profesores, Investigadores y Alumnos, para la elección de los Consejeros Universitarios con la ampliación correspondiente, se aplicará dicho ordenamiento con las modalidades descritas en este artículo.</p>



              Por fórmula se entiende la integración de un representante propietario y su respectivo suplente.


              <ol>
              <li>La elección de las cuatro fórmulas adicionales de representantes de alumnos del Bachillerato, dos para la Escuela Nacional Preparatoria y dos para la Escuela Nacional “Colegio de Ciencias y Humanidades”, se realizará de conformidad con el procedimiento establecido en el Reglamento para la Elección de Consejeros Universitarios y Técnicos Representantes de Profesores, Investigadores y Alumnos;</li>



              <li>En la elección de las cuatro fórmulas adicionales de representantes de alumnos que corresponderán a las Facultades de Estudios Superiores por cada área del conocimiento, el Secretario General de la Universidad realizará los actos que estén asignados al director de la entidad académica y los Consejos Académicos de Área se constituirán, sólo para este efecto, en el cuerpo colegiado que ejercerá las funciones atribuidas a los Consejos Técnicos de Facultades y Escuelas;</li>



              <li>En la elección de las dos fórmulas adicionales de representantes de alumnos de los Programas de Posgrado, una por cada subsistema de investigación, los Coordinadores de dichos subsistemas realizarán los actos que estén asignados a los directores de la entidad académica y los Consejos Técnicos respectivos, sólo para este efecto, se constituirán en el cuerpo colegiado que ejercerá las funciones atribuidas a los Consejos Técnicos de Facultades y Escuelas;</li>



              <li>La elección de las dos fórmulas adicionales de representantes de profesores del Bachillerato, una para la Escuela Nacional Preparatoria y una para la Escuela Nacional “Colegio de Ciencias y Humanidades”, se realizará de conformidad con el procedimiento establecido en el Reglamento para la Elección de Consejeros Universitarios y Técnicos Representantes de Profesores, Investigadores y Alumnos;</li>



              <li>En la elección de las cuatro fórmulas adicionales de representantes de profesores, dos para las Facultades y Escuelas del campus de Ciudad Universitaria y dos para las Facultades de Estudios Superiores, el Secretario General de la Universidad realizará los actos que estén asignados al director de la entidad académica y dos representantes de cada uno de los Consejos Académicos de Área se constituirán, sólo para este efecto, en el cuerpo colegiado que ejercerá las funciones atribuidas a los Consejos Técnicos de Facultades y Escuelas;</li>



              <li>En la elección de las dos fórmulas de representantes de los académicos adscritos a las dependencias administrativas, el Secretario General de la Universidad realizará los actos que estén asignados al director de la entidad académica, y dos representantes de los Consejos Asesores de las dependencias administrativas involucradas se constituirán, sólo para este efecto, en el cuerpo colegiado que ejercerá las funciones atribuidas a los Consejos Técnicos de Facultades y Escuelas;</li>



              <li>En la elección de las cinco fórmulas de representantes de Técnicos Académicos a que se refiere la fracción VIII del artículo 16 de este Estatuto, el Secretario General de la Universidad realizará los actos que estén asignados al director de la entidad académica. Los Consejos Académicos de Área y del Bachillerato se constituirán, sólo para este efecto, en el cuerpo colegiado que ejercerá las funciones atribuidas a los Consejos Técnicos de Facultades y Escuelas;</li>



              <li>En la elección de las dos fórmulas de representantes de investigadores de Centros, una por cada subsistema de investigación, los Coordinadores de dichos subsistemas realizarán los actos que estén asignados al director de la entidad académica y los Consejos Técnicos respectivos, sólo para este efecto, se constituirán en el cuerpo colegiado que ejercerá las funciones atribuidas a los Consejos Técnicos de Facultades y Escuelas, y</li>



              <li>La elección de los cinco invitados permanentes de los empleados de la UNAM se realizará de conformidad con el procedimiento previsto en el artículo 2º del Reglamento del H. Consejo Universitario.</li>
						  </ol>


              <p align="justify"><em>TERCERO.-</em> Los alumnos, profesores, investigadores y técnicos académicos que cumplan con los requisitos que establece el Estatuto General para ser elegibles ante el Consejo Universitario, únicamente podrán registrarse en una fórmula. El Secretario General de la Universidad estará facultado para verificar el estricto cumplimiento de esta disposición transitoria.</p>
