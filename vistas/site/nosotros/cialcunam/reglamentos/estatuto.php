              Ayudantes;
              Profesores e investigadores.</p>

              <p align="justify"><em>Artículo 74.-</em> Son técnicos académicos ordinarios quienes hayan demostrado tener la experiencia y las aptitudes suficientes en una determinada especialidad, materia o área, para realizar tareas específicas y sistemáticas de los programas académicos o de servicios técnicos de una dependencia de la UNAM.</p>

              <p align="justify"><em>Artículo 75.-</em> Son ayudantes quienes auxilian a los profesores, a los investigadores y a los técnicos académicos en sus labores. La ayudantía debe capacitar al personal para el desempeño de funciones docentes, técnicas o de investigación.</p>

              <p align="justify"><em>Artículo 76.-</em> Los profesores e investigadores podrán ser:

              Ordinarios;
              Visitantes;
              Extraordinarios;
              Eméritos.</p>

              <p align="justify"><em>Artículo 77.-</em> Son profesores o investigadores ordinarios quienes tienen a su cargo las labores permanentes de docencia e investigación.</p>

              <p align="justify">Los profesores ordinarios podrán ser de asignatura o de carrera. Los investigadores serán siempre de carrera.</p>

              <p align="justify"><em>Artículo 78.-</em> Son profesores de asignatura quienes de acuerdo con la categoría que fije su nombramiento, sean remunerados en función del número de horas que impartan; podrán impartir una o varias materias, ser interinos o definitivos y ocupar cualquiera de las siguientes categorías: A o B.</p>

              <p align="justify"><em>Artículo 79.-</em> Son profesores o investigadores de carrera quienes dedican a la Universidad medio tiempo o tiempo completo en la realización de labores académicas. Podrán ocupar cualquiera de las categorías siguientes: asociado o titular. En cada una de éstas habrá tres niveles: A, B y C.</p>

              <p align="justify"><em>Artículo 80.-</em> Son profesores, investigadores y técnicos visitantes los que con tal carácter desempeñan funciones académicas específicas por un tiempo determinado, las cuales podrán ser remuneradas por la Universidad.</p>

              <p align="justify"><em>Artículo 81.-</em> Son profesores o investigadores extraordinarios los provenientes de otras universidades del país o del extranjero, que de conformidad con el Reglamento del Reconocimiento al Mérito Universitario, hayan realizado una eminente labor docente o de investigación en la UNAM o en colaboración con ella.</p>

              <p align="justify"><em>Artículo 82.-</em> Son profesores o investigadores eméritos, aquellos a quienes la Universidad honre con dicha designación por haberle prestado cuando menos 30 años de servicios, con gran dedicación y haber realizado una obra de valía excepcional.</p>

              <p align="justify">El personal emérito será designado de acuerdo con el procedimiento que señalen el Estatuto del Personal Académico y el Reglamento del Reconocimiento al Mérito Universitario.</p>

              <p align="justify"><em>Artículo 83.-</em> El ingreso y promoción de los miembros del personal académico deberá ajustarse a los procedimientos que señale el Estatuto del Personal Académico, el cual también consignará sus derechos y obligaciones.</p>

              <p align="justify">Para el ingreso y promoción de los miembros del personal académico se crearán comisiones dictaminadoras de acuerdo con las disposiciones del Estatuto del Personal Académico, las que serán órganos auxiliares de los respectivos consejos técnicos.</p>

              <p align="justify"><em>Artículo 84.-</em> El personal académico de carrera designado por la Junta de Gobierno o por el Rector de la Universidad para el desempeño de un cargo académico-administrativo de tiempo completo, no perderá sus derechos de antigüedad o cualesquiera otros que le pertenezcan. Tendrán derecho además a conservar su remuneración al terminar sus funciones y reintegrarse a su dependencia los profesores e investigadores de carrera que expresamente señale el Estatuto del Personal Académico.</p>

              <p align="justify"><em>Artículo 85.-</em> (Modificado en la sesión del Consejo Universitario del 5 de enero de 1978, como sigue):</p>

              <p align="justify"><em>Artículo 85.-</em> El Consejo Universitario, a propuesta de los consejos técnicos, podrá acordar que excepcionalmente, personas de manifiesta distinción en una especialidad, acreditada por varios años de labor y por la realización o publicación de obras, aun cuando no satisfagan alguno o algunos de los requisitos estatutarios presenten concurso de oposición para ingreso como profesores o investigadores.</p>

              <p align="justify"><em>Artículo 86.-</em> (Adicionado en la sesión del Consejo Universitario del 10 de diciembre de 1974, como sigue):</p>

              <p align="justify"><em>Artículo 86.-</em> El Estatuto del Personal Académico señalará qué dependencias administrativas podrán contar con los servicios de personal académico, así como las características de su adscripción.</p>

              <h4>TÍTULO QUINTO</h4>
              <h5>De los Alumnos</h5>


              <p align="justify"><em>Artículo 87.-</em> (Modificado en las sesiones del Consejo Universitario del 28 de noviembre de 1969 y 27 de febrero de 1973, como sigue):</p>

              <p align="justify"><em>Artículo 87.-</em> Con los reglamentos especiales se determinarán los requisitos y condiciones para que los alumnos se inscriban y permanezcan en la Universidad, así como sus deberes y derechos, de acuerdo con las siguientes bases:</p>
              <ol>
              <li>En el momento de la inscripción firmarán la protesta universitaria, por la cual se comprometen a hacer en todo tiempo honor a la Institución, a cumplir sus compromisos académicos y administrativos, a respetar los reglamentos generales sin pretender excepción alguna y a mantener la disciplina;</li>

              <li>Ningún alumno podrá ser inscrito más de dos veces en una asignatura. El Reglamento General de Exámenes establecerá la forma de acreditar la materia de que se trate, cuando exceda ese límite;</li>

              <li>El Reglamento General de Inscripciones determinará los límites máximos de tiempo en que un alumno podrá terminar los ciclos correspondientes al bachillerato y las carreras profesionales. Tales lapsos se fijarán señalando un margen adicional a la duración normal que establezcan los planes de estudio respectivos;</li>

              <li>Las personas que no concluyan sus estudios en los lapsos señalados por el Reglamento General de Inscripciones, podrán acreditar las materias que les falten en la forma que establezca el Reglamento General de Exámenes, aunque ya no serán inscritos como alumnos de la Universidad;</li>

              <li>Los alumnos podrán expresar libremente, dentro de la Universidad, sus opiniones sobre todos los asuntos que a la institución concierne sin más limitaciones que el no perturbar las labores universitarias y ajustarse a los términos del decoro y del respeto debidos a la Universidad y a sus miembros. Para toda reunión dentro de los planteles de la Universidad deberán llenarse los requisitos que señale el reglamento relativo;</li>

              <li>Los alumnos podrán organizar libremente las sociedades que estimen convenientes y las autoridades mantendrán con todas ellas las relaciones de cooperación para fines culturales, deportivos, sociales y de asistencia mutua que se propongan los organizadores, en los términos que fije el reglamento; pero no aceptarán la representación de los alumnos en el arreglo de asuntos académicos o administrativos, los que, invariablemente, deberán gestionar los interesados;</li>

              <li>Las observaciones de carácter técnico deberán presentarlas los alumnos por conducto de sus representantes en el Consejo Universitario y en los consejos técnicos.</li>
						  </ol>
              <p align="justify"><em>Artículo 88.-</em> No podrán desempeñar ningún puesto o comisión remunerado dentro de la Universidad, los alumnos cuyo promedio sea inferior a ocho; los que desempeñen algún puesto en sociedad estudiantil y los que tengan representación en el Consejo Universitario y en los consejos técnicos. El Patronato y las comisiones de Presupuestos y de Vigilancia Administrativa del Consejo Universitario, cuidarán la exacta observancia de esta disposición.</p>

              <p align="justify"><em>Artículo 89.-</em> La Universidad promoverá, con periodicidad fija, diversas formas de estímulo y distinción para los alumnos distinguidos por su aprovechamiento y conducta.</p>

              <h4>TÍTULO SEXTO</h4>
              <h5>De las Responsabilidades y Sanciones</h5>


              <p align="justify"><em>Artículo 90.-</em> Los miembros de la Universidad son responsables por el incumplimiento de las obligaciones que específicamente les imponen la Ley Orgánica, el Estatuto y sus reglamentos.</p>

              <p align="justify"><em>Artículo 91.-</em> El Rector sólo será responsable ante la Junta de Gobierno. El Secretario General sólo será responsable ante el Rector.</p>

              <p align="justify"><em>Artículo 92.-</em> Los directores de facultades, escuelas e institutos, sólo serán responsables ante la Junta de Gobierno y ante el Rector.</p>

              <p align="justify">Los miembros del Consejo Universitario y de los consejos técnicos, sólo serán responsables ante estas autoridades, en lo que toca a sus actividades en esos cuerpos, en la forma que establezcan los reglamentos respectivos.</p>

              <p align="justify"><em>Artículo 93.-</em> (Modificado en las sesiones del Consejo Universitario del 2 de marzo de 1971; 1° de diciembre de 1998, publicado en Gaceta UNAM, el 17 del mismo mes y año, como sigue):</p>

              <p align="justify"><em>Artículo 93.-</em> Los miembros del personal académico y los alumnos serán responsables ante el Tribunal Universitario, previa remisión que de los casos se haga por las autoridades señaladas en los artículos 3o. incisos 3 y 5 de la Ley Orgánica, así como los directores de Plantel y de Centros a que aluden respectivamente los artículos 43 y 52-E de este Estatuto, y 7o. del Reglamento General de los Centros de Extensión Universitaria.</p>

              <p align="justify">Tratándose de casos de indisciplina de los alumnos, el Rector y los directores de las entidades académicas a que se refiere el párrafo anterior, podrán sancionarlos de manera inmediata con amonestación, asimismo podrán suspenderlos o expulsarlos provisionalmente con la finalidad de salvaguardar el orden y la disciplina universitaria.</p>

              <p align="justify">El Rector y los directores de las entidades señaladas en el presente artículo deberán remitir el caso al Tribunal Universitario dentro de un plazo que no exceda de tres días a la suspensión o expulsión provisional, para que resuelva de acuerdo con lo previsto en el Reglamento del Tribunal Universitario y de la Comisión de Honor, convirtiéndose en interesados para todos los efectos legales.</p>

              <p align="justify">Para la revisión de la sanción de amonestación, el interesado deberá solicitarla ante el Tribunal.</p>

              <p align="justify"><em>Artículo 94.-</em> El Tesorero de la Universidad y los empleados que directamente estén a sus órdenes; el Auditor Interno y los empleados que de él dependan serán responsables ante el Patronato.</p>

              <p align="justify">El personal técnico, los empleados y la servidumbre, serán sancionados directamente, por sus jefes respectivos, con acuerdo del Rector.</p>

              <p align="justify"><em>Artículo 95.-</em> (Modificado en la sesión del Consejo Universitario del 16 de abril de 1986, publicado en Gaceta UNAM el 19 de mayo de 1986, como sigue):</p>

              <p align="justify"><em>Artículo 95.-</em> Son causas especialmente graves de responsabilidad, aplicables a todos los miembros de la Universidad:</p>
              <ol>
              <li>La realización de actos concretos que tiendan a debilitar los principios básicos de la Universidad, y las actividades de índole política que persigan un interés personalista;</li>

              <li>La hostilidad por razones de ideología o personales, manifestada por actos concretos, contra cualquier universitario o grupo de universitarios;</li>

              <li>La utilización de todo o parte del patrimonio para fines distintos de aquéllos a que está destinado;</li>

              <li>Ocurrir a la Universidad en estado de ebriedad o bajo los efectos de algún estupefaciente, psicotrópico o inhalante; ingerir o usar, vender, proporcionar u ofrecer gratuitamente a otro, en los recintos universitarios, bebidas alcohólicas y las sustancias consideradas por la ley como estupefacientes o psicotrópicos, o cualquier otra que produzca efectos similares en la conducta del individuo que los utiliza;</li>

              <li>Portar armas de cualquier clase en los recintos universitarios;</li>

              <li>La comisión en su actuación universitaria, de actos contrarios a la moral y al respeto que entre sí se deben los miembros de la comunidad universitaria.</li>
						  </ol>
              <p align="justify"><em>Artículo 96.-</em> Los profesores serán responsables, particularmente, por el incumplimiento de sus obligaciones en los términos siguientes:</p>
              <ol>
              <li>El profesor que falte sin causa justificada a más de cinco clases consecutivas y ocho durante el mes, será sancionado en la forma prevista por el artículo 98. Si en el siguiente año escolar persiste en su impuntualidad, será separado de su cargo, y</li>

              <li>El profesor que al concluir el año escolar, no haya dado como mínimo de clases el 85%, estará obligado a completarlas, si no ha sido sustituido por un profesor interino. Si omite el cumplimiento de este deber, clausurando su curso sin dar las clases que le falten, será separado de su cargo.</li>
						  </ol>
              <p align="justify"><em>Artículo 97.-</em> (Modificado en las sesiones del Consejo Universitario del 15 de diciembre de 1948; 28 de noviembre de 1969, y 16 de abril de 1986, publicado en Gaceta UNAM el 19 de mayo de 1986, como sigue):</p>

              <p align="justify"><em>Artículo 97.-</em> Los alumnos serán responsables particularmente por el incumplimiento de las obligaciones que les señalen los reglamentos que menciona el artículo 87, y por actos contra la disciplina y el orden universitario:</p>
              <ol>
              <li>Los alumnos que participen en desórdenes dentro de la escuela o falten al respeto a los profesores, serán sancionados según la gravedad de la falta;</li>

              <li>El alumno que haya prestado o recibido ayuda fraudulenta en las pruebas de aprovechamiento, será suspendido hasta por un año, sin perjuicio de la nulidad del examen sustentado;</li>

              <li>El alumno que falsifique certificados, boletas de exámenes y documentos análogos, o use o aproveche los propios documentos cuando la falsificación sea imputable a terceros, será expulsado de la Universidad, y</li>

              <li>Los alumnos que incurran en las conductas previstas, en las fracciones IV y V del artículo 95, serán suspendidos hasta por un año, y en caso de reincidencia, serán expulsados definitivamente de la Universidad.</li>
						  </ol>
              <p align="justify">Estas sanciones se aplicarán con independencia de las que correspondan por otras faltas universitarias cometidas por el alumno en forma individual y colectivamente y sin perjuicio de las responsabilidades que deriven de la legislación común.</p>

              <p align="justify"><em>Artículo 98.-</em> (Modificado en la sesión del Consejo Universitario del 2 de marzo de 1971, como sigue):</p>

              <p align="justify"><em>Artículo 98.-</em> Las sanciones que podrán imponerse, en los casos que no tengan expresamente señalada una pena, serán las siguientes:</p>
              <ol>
              <li>A los miembros del personal académico:</li>
              <ol>
              <li>Extrañamiento escrito;</li>

              <li>Suspensión, y</li>

              <li>Destitución.</li>
					   </ol>
              <li>A los alumnos:</li>
              <ol>
              <li>Amonestación;</li>

              <li>Negación de créditos o cancelación de los concedidos respecto al pago de cuotas;</li>

              <li>Suspensión o separación de cargos o empleos que desempeñen;</li>

              <li>Suspensión hasta por un año en sus derechos escolares, y</li>

              <li>Expulsión definitiva de la facultad o escuela.</li>
						  </ol></ol>
              <p align="justify"><em>Artículo 99.-</em> (Modificado en la sesión del Consejo Universitario del 1o. de diciembre de 1998, publicado en Gaceta UNAM, el 17 del mismo mes y año, como sigue):</p>

              <p align="justify"><em>Artículo 99.-</em> El Tribunal Universitario conocerá exclusivamente de las faltas a la Legislación Universitaria presuntamente cometidas por los miembros del personal académico y los alumnos, a quienes se considerarán interesados para todos los efectos legales, y estará integrado de la siguiente forma:</p>
              <ol>
              <li>Para todos los casos:</li>
              <ol>
              <li>Por un Presidente, que será el más antiguo de los profesores del Consejo Técnico de la Facultad de Derecho;</li>

              <li>Un Vocal Permanente, que será el investigador de mayor antigüedad del Consejo Interno del Instituto de Investigaciones Jurídicas;</li>

              <li>Un Vocal Académico, que será:</li>
						  <ol>
              <li>Para el caso de bachillerato, licenciatura e investigación, el profesor o investigador más antiguo del Consejo Técnico o Interno de la entidad de que se trate el asunto, salvo en la Facultad de Derecho y en el Instituto de Investigaciones Jurídicas, en que será designado por el Consejo Técnico o Interno respectivo de entre sus miembros, y</li>

              <li>Para los programas de posgrado será el académico designado por su Comité Académico, o quien haga sus veces, de entre sus miembros.</li>
					  	</ol>
              <li>Un Secretario, que será el Abogado General de la Universidad, quien tendrá derecho a voz y no a voto.</li>
						  </ol>
              <li>Para responsabilidades de alumnos, se integrará además:</li>
              <ol>
              <li>Tratándose del bachillerato y licenciatura, con los dos alumnos propietarios elegidos para el Consejo Técnico de la entidad a que pertenezca el presunto infractor, y</li>

              <li>En programas de posgrado, con los dos alumnos elegidos para el Comité Académico respectivo, o para el que haga sus veces.</li>
						  </ol></ol>
              <p align="justify">Las reglas para la suplencia en caso de vacancia, ausencia o impedimento serán fijadas en el Reglamento del Tribunal Universitario y de la Comisión de Honor.</p>

              <p align="justify"><em>Artículo 100.-</em> (Modificado en la sesión del Consejo Universitario del 1o. de diciembre de 1998, publicado en Gaceta UNAM, el 17 del mismo mes y año, como sigue):</p>

              <p align="justify"><em>Artículo 100.-</em> El Tribunal Universitario dictará sus resoluciones oyendo previamente al presunto infractor, en la forma y términos que establezca el Reglamento del Tribunal Universitario y de la Comisión de Honor. Las resoluciones del Tribunal serán revisadas por la Comisión de Honor a solicitud de cualesquiera de los interesados, a excepción de la sanción de amonestación impuesta en los términos del segundo párrafo del artículo 93 de este Estatuto.</p>

              <p align="justify">Cuando se trate del personal académico que tenga más de tres años de servicios, la resolución que le separe de su cargo será revisada de oficio por la Comisión de Honor, en términos del Reglamento del Tribunal Universitario y de la Comisión de Honor, no surtiendo entre tanto sus efectos.</p>

              <p align="justify"><em>Artículo 101.-</em> El Tribunal Universitario y la Comisión de Honor apreciarán libremente las pruebas, dictarán sus fallos de acuerdo con el derecho universitario y la equidad y aplicarán discrecionalmente las sanciones, salvo en los casos en que estén expresamente señaladas.</p>

              <p align="justify">Si al investigar las faltas de carácter universitario aparecen responsabilidades penales, deberá hacerse la consignación respectiva, sin perjuicio de que se impongan las sanciones previstas en este título.</p>

              <h4>TÍTULO SÉPTIMO</h4>
              <h5>De la Reforma del Estatuto General</h5>


              <p align="justify"><em>Artículo 102.-</em> Para reformar el Estatuto se requiere:</p>
              <ol>
              <li>Que se convoque al Consejo Universitario exclusivamente con este objeto;</li>

              <li>Que se ponga en conocimiento de los miembros del propio Consejo con anticipación mínima de ocho días a la fecha en que deba reunirse este cuerpo, el texto de la reforma proyectada, y</li>

              <li>Que la reforma se apruebe cuando menos por dos tercios de los votos del Consejo y sea acordada en votación nominal y sin perjuicio del derecho de veto otorgado al Rector.</li>
						  </ol>

