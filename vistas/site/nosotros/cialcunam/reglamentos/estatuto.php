              <h5>SECCIÓN C</h5>

              <h5>Del Funcionamiento</h5>

              <p align="justify"><em>Artículo 116.-</em> Los consejos académicos de área trabajarán en pleno o en comisiones que podrán ser permanentes o especiales; son permanentes las que siguen:



              Planeación y Evaluación;
              Planes y Programas de Estudios;
              Personal Académico, y
              Difusión y Extensión.</p>


              <p align="justify"><em>Artículo 117.- </em> Las comisiones especiales serán las que los consejos académicos designen para estudiar y dictaminar otros asuntos de su competencia. Algunas de éstas incluirán expertos designados por los propios consejos, quienes tendrán carácter de asesores invitados.</p>


              <p align="justify"><em>Artículo 118.-</em> Cada consejo académico de área celebrará sesiones ordinarias cada dos meses, y extraordinarias cuando lo considere necesario el coordinador o un grupo de consejeros que represente cuando menos una cuarta parte del total de éstos.</p>


              <p align="justify"><em>Artículo 119.-</em> En cada sesión ordinaria las comisiones permanentes tendrán la obligación de rendir un informe de trabajo al pleno del respectivo consejo académico acerca de los asuntos de su competencia.</p>


              <p align="justify"><em>Artículo 120.-</em> El funcionamiento de los consejos académicos de área se realizará de conformidad a lo siguiente:</p>


              <ol>
              <li>Cuando un consejo académico de área funcione en pleno actuará válidamente con más de la mitad de sus miembros, a menos que se traten casos en los que se requiera un quorum especial;</li>



              <li>Si por falta de quorum se suspende una sesión, se citará para una segunda que podrá efectuarse con los consejeros concurrentes;</li>



              <li>Salvo prevención de este ordenamiento o de los reglamentos, los consejos académicos adoptarán sus resoluciones por mayoría simple de votos, y</li>



              <li>Las votaciones serán económicas, a menos que el coordinador o dos consejeros pidan que sean nominales, por cédulas o secretas. Sólo tendrán derecho a votar los consejeros presentes en la sesión.</li>

						  </ol>
              <p align="justify"><em>Artículo 121.-</em> Cuando un consejo funcione por comisiones, éstas se considerarán legalmente instaladas con la mitad más uno de sus miembros y tomarán decisiones válidamente por mayoría de votos. Si no hay quorum se citará por segunda ocasión y la comisión tomará sus determinaciones por mayoría de los miembros que asistan, considerándose legalmente instalada.</p>


              <p align="justify"><em>Artículo 122.-</em> Cuando alguno de los consejeros directores de escuelas, facultades, institutos o centros no pueda asistir a una sesión, podrá asistir, con voz y voto, un representante designado por ellos. En todo caso el representante deberá cumplir los requisitos académicos establecidos para los consejeros profesor o investigador, según sea el caso.</p>

