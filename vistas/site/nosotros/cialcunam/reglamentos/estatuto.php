              <p align="justify"><em>Artículo 16.-</em> Cada una de las facultades y escuelas tendrá en el Consejo Universitario un representante propietario y otro suplente, por sus profesores, y un representante propietario y otro suplente, por sus alumnos.</p>

              <p align="justify">Los profesores y alumnos de los cursos vespertinos y nocturnos del conjunto de los planteles que integren la Escuela Nacional Preparatoria y el Colegio de Ciencias y Humanidades elegirán además otro representante propietario y otro suplente, respectivamente.</p>

              <p align="justify">Cada uno de los institutos tendrá en el seno del Consejo Universitario representantes investigadores que realicen funciones docentes en la UNAM, uno propietario y uno suplente, por su personal académico.</p>

              <p align="justify">Los alumnos de los programas de posgrado tendrán en el Consejo Universitario un representante propietario, con su respectivo suplente, por cada una de las siguientes áreas:</p>
              <ol>
              <li>Ciencias Físico Matemáticas y de las Ingenierías;</li>

              <li>iencias Biológicas y de la Salud;</li>

              <li>Ciencias Sociales, y</li>

              <li>Humanidades y de las Artes.</li>
						</ol>

              <p align="justify"><em>Artículo 17.-</em> (Modificado en la sesión del Consejo Universitario del 12 de septiembre de 1986, publicado en Gaceta UNAM el 29 de septiembre de 1986, como sigue):</p>

              <p align="justify"><em>Artículo 17.-</em> Los alumnos elegirán a sus representantes cada dos años en elección directa, mediante voto universal, libre y secreto. Dichos representantes deberán cumplir los siguientes requisitos:</p>

              <p align="justify">A) Consejeros representantes de los alumnos de Bachillerato:</p>
              <ol>
              <li>Estar inscrito a partir del primer año del plan de estudios del bachillerato de la Escuela Nacional Preparatoria o del primer semestre del plan de estudios de la Escuela Nacional “Colegio de Ciencias y Humanidades”;</li>

              <li>Tener acreditadas, en su caso, todas las materias de los años o semestres anteriores, según corresponda a cada subsistema, al momento de la elección;</li>

              <li>Tener, en su caso, un promedio de calificaciones mínimo de 8;</li>

              <li>Manifestar por escrito, en el momento de su registro como candidato, su compromiso y disponibilidad para participar en las tareas encomendadas en el Consejo Universitario, en caso de resultar electo;</li>

              <li>No ocupar en la Universidad ningún puesto administrativo o académico-administrativo al momento de la elección ni durante el desempeño de su cargo, y</li>

              <li>No haber cometido faltas graves contra la disciplina universitaria que hubieren sido sancionadas.</li>
						  </ol>
              <p align="justify">B) Consejeros representantes de los alumnos de Licenciatura:</p>
              <ol>
              <li>Estar inscrito en la Facultad, Escuela o entidad académica correspondiente;</li>

              <li>Haber concluido por lo menos un año de estudios, en la Escuela, Facultad o Programa correspondiente;</li>

              <li>Tener acreditado el número de materias que corresponda al año o semestre anterior al que se encuentre inscrito,</li> y que no cuente con materias no aprobadas al momento de la elección con independencia de que, en el plan de estudios semestral, las materias acreditadas correspondan a semestres posteriores.

              <p align="justify">Para efectos estrictamente de elegibilidad, la anotación “NP” en cualquier asignatura no será considerada como “no acreditada”;</p>

              <li>Tener un promedio de calificaciones mínimo de 8;</li>

              <li>Manifestar por escrito, en el momento de su registro como candidato, su compromiso y disponibilidad para participar en las tareas encomendadas en el Consejo Universitario, en caso de resultar electo;</li>

              <li>No ocupar en la Universidad ningún puesto administrativo o académico-administrativo al momento de la elección ni durante el desempeño de su cargo, y</li>

              <li>No haber cometido faltas graves contra la disciplina universitaria que hubieren sido sancionadas.</li>
						  </ol>
              <p align="justify">C) Consejeros representantes de los alumnos de Posgrado:</p>
              <ol>
              <li>Estar inscrito en algún programa de posgrado de la Universidad en el momento de la elección;</li>

              <li>Tener un promedio mínimo de 8 en el caso de especialidad y maestría;</li>

              <li>Tener evaluaciones favorables para el caso de doctorado;</li>

              <li>Manifestar por escrito, en el momento de su registro como candidato, su compromiso y disponibilidad para participar en las tareas encomendadas en el Consejo Universitario, en caso de resultar electo;</li>

              <li>No ocupar en la Universidad ningún puesto administrativo o académico-administrativo al momento de la elección ni durante el desempeño de su cargo, y</li>

              <li>No haber cometido faltas graves contra la disciplina universitaria, que hubieren sido sancionadas.</li>
					  	</ol>
              <p align="justify"><em>Artículo 18.-</em> (Modificado en las sesiones del Consejo Universitario del 5 de julio de 2005, y 26 de agosto de 2011, publicado en Gaceta UNAM el 12 de septiembre del mismo año , como sigue):</p>

              <p align="justify"><em>Artículo 18.-</em> Los profesores, investigadores y técnicos académicos, con más de tres años de antigüedad en la Universidad, elegirán a sus representantes cada cuatro años en elección directa, mediante voto universal, libre y secreto.</p>

