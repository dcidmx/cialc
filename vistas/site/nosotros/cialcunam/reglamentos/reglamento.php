            <h5 class="center">CAPÍTULO I</h5>
            <h4 class="center">Objetivo</h4><br>
            <p align="justify"><em>Artículo 1°.</em> El Centro de Investigaciones sobre América Latina y el Caribe (CIALC) es una entidad académica adscrita a la Coordinación de Humanidades y tiene por objetivo realizar investigaciones inter y multidisciplinarias para el desarrollo del conocimiento en las áreas de Filosofía e historia de las ideas en América Latina y el Caribe; Literatura y ensayo latinoamericanos y del Caribe; Historia de América Latina y el Caribe; y Política, economía y sociedad en América Latina y el Caribe, cuyos resultados coadyuven a resolver los problemas de la región.
            </p><br>

            <h5 class="center">CAPÍTULO II</h5>
            <h4 class="center">De las funciones</h4><br>

            <p align="justify"><em>Artículo 2°.</em> El Centro tendrá las siguientes funciones:</p>

            <ol>
            	<li><p align="justify">Realizar investigaciones sobre América Latina y el Caribe;</p></li>
            	<li><p align="justify">Promover el desarrollo de los Estudios Latinoamericanos en el país y en el extranjero;</p></li>
            	<li><p align="justify">Participar en los programas de estudios profesionales y de posgrado sobre Latinoamérica y el Caribe, en la UNAM y en otras instituciones de educación superior;</p></li>
            	<li><p align="justify">Contribuir a la formación de investigadores y técnicos académicos en materia de los Estudios Latinoamericanos en la Universidad Nacional Autónoma de México, en el interior del país y en el extranjero;</p></li>
            	<li><p align="justify">Coordinar aquellas actividades académicas que le sean demandadas por la Sociedad Latinoamericana de Estudios sobre América Latina y el Caribe (SOLAR) y la Federación Internacional de Estudios sobre América Latina y el Caribe (FIEALC), a través del Centro de Investigaciones sobre América Latina y el Caribe (CIALC);</p></li>
            	<li><p align="justify">Difundir los estudios que sobre América Latina y el Caribe se lleven a cabo en esta Universidad, especialmente con la presentación de sus resultados en los congresos que auspicia el Centro a través de la SOLAR y la FIEALC;</p></li>
            	<li><p align="justify">Difundir el resultado de sus investigaciones a través de la producción editorial, de la organización de seminarios, cursos, mesas redondas, coloquios, foros de discusión y medios de comunicación;</p></li>
            	<li><p align="justify">Asesorar a alumnos y docentes, organismos e instituciones de investigación, sobre los Estudios Latinoamericanos, y</p></li>
            	<li><p align="justify">Contribuir a la reforma permanente de la UNAM destinada a mantenerla como una institución con un proyecto cultural significativo en México y el mundo.</p></li>
            </ol><br>


            <h5 class="center">CAPÍTULO III</h5>
            <h4 class="center">De la estructura</h4><br>

            <p align="justify"><em>Artículo 3°.</em> El Centro estará integrado por:</p>
            <ol>
            	<li><p align="justify">El Director y sus auxiliares;</p></li>
            	<li><p align="justify">El Consejo Interno;</p></li>
            	<li><p align="justify">El personal académico, y</p></li>
            	<li><p align="justify">El personal administrativo.</p></li>
            </ol>

            <p align="justify"><em>Artículo 4°.</em> Para el cabal cumplimiento de sus funciones, el Director del Centro contará con los siguientes órganos de apoyo:</p>
            <ol>
            	<li><p align="justify">Secretaría Académica;</p></li>
            	<li><p align="justify">Secretaría Técnica;</p></li>
            	<li><p align="justify">Secretaría Técnica de Planeación y Evaluación, y</p></li>
            	<li><p align="justify">Secretaría Administrativa</p></li>
            </ol>

            <p align="justify"><em>Artículo 5°.</em> Para el desarrollo de sus actividades de investigación, el Centro se organiza en cuatro áreas de estudio:</p>
            <ol>
            	<li><p align="justify">Filosofía e historia de las ideas en América Latina y el Caribe;</p></li>
            	<li><p align="justify">Literatura y ensayo latinoamericanos y del Caribe;</p></li>
            	<li><p align="justify">Historia de América Latina y el Caribe;</p></li>
            	<li><p align="justify">Política, economía y sociedad en América Latina y el Caribe.</p></li>
            </ol>

            <p align="justify"><em>Artículo 6°.</em> En el Centro funcionarán las siguientes comisiones y comités:</p>
            <ol>
            	<li><p align="justify">Comisión dictaminadora;</p></li>
            	<li><p align="justify">Comisión evaluadora del Programa de Primas al Desempeño del Personal Académico (PRIDE);</p></li>
            	<li><p align="justify">Comisión de biblioteca;</p></li>
            	<li><p align="justify">Comité editorial, y</p></li>
            	<li><p align="justify">Subcomisión de superación académica del personal académico.</p></li>
            </ol><br>


            <h5 class="center">CAPÍTULO IV</h5>
            <h4 class="center">Del Director</h4><br>

            <p align="justify"><em>Artículo 7°.</em> El Director es la autoridad ejecutiva del Centro y su representante, recibirá su nombramiento del Rector, de conformidad con lo dispuesto en el artículo 52-E del Estatuto General de la UNAM. Preside el Consejo Interno y el Claustro del Personal Académico. Tendrá las atribuciones que establezcan la legislación universitaria y este Reglamento.</p>

			<p align="justify"><em>Artículo 8°.</em> Son atribuciones y obligaciones del Director:</p>
			<ol>
				<li><p align="justify">Formar parte del Consejo Técnico de Humanidades, como consejero-director, con derecho a voz y voto en sus sesiones;</p></li>
				<li><p align="justify">Convocar al Consejo Interno y presidirlo, con voz y voto; en caso de empate, tendrá voto de calidad;</p></li>
				<li><p align="justify">Formar parte del Consejo Académico del Área de las Humanidades y de las Artes, con voz y voto;</p></li>
				<li><p align="justify">Formar parte de los comités académicos de los programas de posgrado en los que participa el Centro;</p></li>
				<li><p align="justify">Realizar actividades de investigación;</p></li>
				<li><p align="justify">Promover ante el Consejo Técnico de Humanidades y ante el Consejo Académico del Área de las Humanidades y de las Artes todos los asuntos y trámites oficiales del Centro, cuando sean de su competencia;</p></li>
				<li><p align="justify">Dentro del Centro, velar por el cumplimiento de la legislación universitaria, de los planes y programas académicos y, en general, de las disposiciones y acuerdos que normen la estructura y funcionamiento de la universidad, dictando las medidas conducentes;</p></li>
				<li><p align="justify">Elaborar los planes y proyectos de trabajo del Centro y someterlos a la consideración del Consejo Interno, del Coordinador de Humanidades y del Consejo Técnico de Humanidades;</p></li>
				<li><p align="justify">Elaborar el proyecto de presupuesto del Centro y presentarlo para su opinión al Consejo Interno;</p></li>
				<li><p align="justify">Presentar un informe anual de trabajo del Centro ante el personal académico y administrativo del Centro, el cual tendrá carácter público;</p></li>
				<li><p align="justify">Proponer al Coordinador de Humanidades la designación del Secretario Académico, de acuerdo con lo establecido en la legislación universitaria y los demás reglamentos universitarios;</p></li>
				<li><p align="justify">Proponer ante el Patronato Universitario y la Secretaría Administrativa de la Universidad una terna de candidatos a ocupar el cargo de Secretario Administrativo;</p></li>
				<li><p align="justify">Designar a las personas que ocuparán los puestos académico-administrativos del Centro, de acuerdo con lo establecido en la legislación universitaria y los demás reglamentos universitarios;</p></li>
				<li><p align="justify">Velar por el cumplimiento de las funciones de sus auxiliares y evaluar su desempeño;</p></li>
				<li><p align="justify">Cuidar que en el Centro se desarrollen las labores en forma ordenada y eficaz, dictando, en su caso, las medidas procedentes;</p></li>
				<li><p align="justify">Escuchar las opiniones emitidas por el Consejo Interno de los asuntos de su competencia;</p></li>
				<li><p align="justify">Ejercer el presupuesto en los términos aprobados por el Consejo Universitario;</p></li>
				<li><p align="justify">Presentar al Consejo Interno, para su opinión, y al Consejo Técnico de Humanidades, para su aprobación, el programa anual de actividades del Centro;</p></li>
				<li><p align="justify">Presentar al Consejo Interno, para su evaluación y opinión, los informes anuales de las actividades de cada uno de los miembros del personal académico, y</p></li>
				<li><p align="justify">Las demás que le confiera la legislación universitaria y el presente reglamento.</p></li>
			 </ol><br>


			<h5 class="center">CAPÍTULO V</h5>
            <h4 class="center">Del Consejo Interno</h4><br>
            <h5 class="center">SECCIÓN PRIMERA</h5>
            <h4 class="center">Integración y funciones</h4><br>

            <p align="justify"><em>Artículo 9°.</em> El Consejo Interno será el órgano de consulta del Director en los casos que dispongan la legislación universitaria y este reglamento.</p>

            <p align="justify"><em>Artículo 10.</em> Integran el Consejo Interno:</p>

            <ol>
            	<li><p align="justify">El Director del Centro, quien será su presidente, con voz y voto de calidad;</p></li>
            	<li><p align="justify">El Secretario Académico, quien será su secretario, con voz y sólo con derecho a voto en ausencia del Director;</p></li>
            	<li><p align="justify">Dos consejeros representantes de los investigadores, y </p></li>
            	<li><p align="justify">Un consejero representante de los técnicos académicos. </p></li>
			</ol>
			<p align="justify">El representante propietario del personal académico ante el Consejo Técnico de Humanidades y el representante propietario de los investigadores del Centro ante el Consejo Académico del Área de las Humanidades y de las Artes, serán invitados, con voz informativa sin voto, a las sesiones del Consejo Interno.</p>

			<p align="justify"><em>Artículo 11.</em> El Consejo Interno tendrá las siguientes atribuciones:</p>

            <ol>
            	<li><p align="justify">Conocer y opinar respecto de los asuntos académicos que le presente el Director del Centro;</p></li>
            	<li><p align="justify">Presentar al Director iniciativas en materia de planes y proyectos de investigación;</p></li>
            	<li><p align="justify">Evaluar los informes anuales del personal académico y remitirlos con su opinión fundamentada al Consejo Técnico de Humanidades; </p></li>
            	<li><p align="justify">Conocer y opinar sobre el proyecto de presupuesto anual del Centro; </p></li>
            	<li><p align="justify">Constituir comisiones permanentes o especiales para resolver asuntos académicos;</p></li>
            	<li><p align="justify">Elaborar el proyecto de reglamento del Centro, así como promover sus reformas, y presentarlo para su aprobación ante el Consejo Técnico de Humanidades;</p></li>
            	<li><p align="justify">Conocer y opinar sobre los planes de trabajo y el programa anual de actividades del Centro; </p></li>
            	<li><p align="justify">Solicitar al personal académico informes sobre el avance de sus actividades; </p></li>
            	<li><p align="justify">Emitir su opinión en los respectivos concursos de oposición y de los programas de estímulos;</p></li>
            	<li><p align="justify">Opinar sobre las comisiones con goce de sueldo que se propongan al Consejo Técnico de Humanidades;</p></li>
            	<li><p align="justify">Difundir oportunamente sus acuerdos entre el personal académico del Centro;</p></li>
            	<li><p align="justify">Aprobar los reglamentos específicos, manuales e instructivos internos del Centro, y</p></li>
            	<li><p align="justify">Las demás que le confiera la legislación universitaria y el presente reglamento.</p></li>
			</ol>

			<p align="justify"><em>Artículo 12.</em> Para que el Consejo Interno pueda celebrar sesiones válidas, en primera convocatoria, se requiere de la asistencia de más de la mitad de sus miembros; en segunda convocatoria sesionará con los consejeros presentes. La segunda convocatoria se entenderá transcurridos quince minutos después de la primera.</p>

			<p align="justify"><em>Artículo 13.</em> Los acuerdos del Consejo Interno se tomarán por mayoría simple de votos. El director tendrá voto de calidad en caso de empate. Sólo se tomarán en cuenta los votos de los consejeros asistentes.</p>

			<p align="justify"><em>Artículo 14.</em> El Consejo Interno celebrará sesiones ordinarias una vez al mes, y extraordinarias cuando lo juzguen necesario el Director o un grupo de consejeros que constituya, por lo menos, la tercera parte del Consejo. El Director, a través del Secretario del Consejo Interno, será el encargado de convocar a las sesiones de dicho cuerpo colegiado.</p><br>

			<h4 class="center">SECCIÓN SEGUNDA</h4>
            <h4 class="center">Elección de los consejeros internos</h4><br>

            <p align="justify"><em>Artículo 15.</em> Para ser elegible al cargo de consejero representante de los investigadores, se deberán satisfacer los siguientes requisitos:</p>

            <ol>
            	<li><p align="justify">Ser investigador o profesor del Centro;</p></li>
				<li><p align="justify">Haber cumplido con sus programas de trabajo;</p></li>
				<li><p align="justify">Haber prestado servicios académicos ininterrumpidos en el Centro, cuando menos durante un año;</p></li>
				<li><p align="justify">No desempeñar algún cargo de carácter académico-administrativo o administrativo en la Universidad al momento de ser electo, ni durante el desempeño del cargo;</p></li>
				<li><p align="justify">No haber sido sancionado por incurrir en alguna de las causas graves de responsabilidad que establece la legislación universitaria, y</p></li>
				<li><p align="justify">No fungir como representante del personal académico del Centro ante el Consejo Técnico de Humanidades.</p></li>
            </ol>

			    <p align="justify"><em>Artículo 16.</em> Para ser elegible al cargo de consejero representante de los técnicos académicos, se deberán satisfacer los siguientes requisitos:</p>

            <ol>
            	<li><p align="justify">Ser técnico académico del Centro;</p></li>
				<li><p align="justify">Haber cumplido con sus programas de trabajo;</p></li>
				<li><p align="justify">Haber prestado servicios académicos ininterrumpidos en el Centro, cuando menos durante un año;</p></li>
				<li><p align="justify">No desempeñar algún cargo de carácter académico-administrativo o administrativo en la Universidad al momento de ser electo, ni durante el desempeño del cargo, y</p></li>
				<li><p align="justify">No haber sido sancionado por incurrir en alguna de las causas graves de responsabilidad que establece la legislación universitaria.</p></li>
            </ol>

            	<p align="justify"><em>Artículo 17.</em> La elección de los consejeros representantes del personal académico será mediante votación universal, mayoritaria, libre, directa y secreta por parte del personal académico que cuente con un nombramiento vigente aprobado por el Consejo Técnico de Humanidades. La elección se efectuará conforme al siguiente procedimiento:</p>

            <ol>
            	<li><p align="justify">El Consejo Interno expedirá la convocatoria respectiva con quince días hábiles de anticipación a la fecha que deban celebrarse las elecciones, la cual se fijará en lugares visibles del Centro. Dicha convocatoria incluirá el padrón de elegibles y la lista de electores.</p></li>
				<li><p align="justify">El Consejo Interno nombrará a una comisión de dos miembros que instale y vigile las urnas, las cuales permanecerán abiertas el día de la elección de las 9:00 a las 15:00 hrs. También designará dos escrutadores.</p></li>
				<li><p align="justify">El escrutinio de la votación será de carácter público.</p></li>
				<li><p align="justify">El Director dará a conocer los resultados de la elección.</p></li>
				<li><p align="justify">En caso de que se declare un empate, el Consejo Interno fijará una nueva fecha para realizar una elección de desempate, de conformidad con las siguientes disposiciones:</p></li>

				<ol>
            		<li><p align="justify">El Director comunicará el empate a la comunidad y emitirá una nueva convocatoria en los mismos términos que se señalan en el presente artículo;</p></li>
					<li><p align="justify">Únicamente intervendrán los académicos que hubieren empatado; y</p></li>
					<li><p align="justify">La elección se efectuará a más tardar el quinto día hábil posterior a la publicación de la convocatoria.</p></li>
        		 </ol>
				<p align="justify">La elección se llevará a cabo de acuerdo con lo dispuesto en el presente reglamento.</p>
            </ol>

            <p align="justify"><em>Artículo 18.</em> Los consejeros representantes de los investigadores y de los técnicos académicos durarán en su cargo dos años, a partir de que tomen posesión del cargo, no pudiendo reelegirse para el período inmediato al de su encargo. </p><br>

            <h4 class="center">SECCIÓN TERCERA</h4>
            <h4 class="center">Elecciones extraordinarias</h4><br>

            <p align="justify"><em>Artículo 19.</em> Habrá lugar a elecciones extraordinarias cuando se declare nula una elección ordinaria o cuando alguno de los consejeros: </p>

            <ol>
            	<li><p align="justify">Deje de cumplir alguno de los requisitos previstos en los artículos 15 o 16 del presente reglamento, según corresponda; </p></li>
				<li><p align="justify">Cuando opte por el ejercicio de otro derecho que sea incompatible con el desempeño del cargo, o</p></li>
				<li><p align="justify">Renuncie o fallezca.</p></li>
            </ol>

		 <p align="justify"><em>Artículo 20.</em> Cuando falte alguno de los consejeros, representante de los investigadores o de los técnicos académicos, el Director lo hará del conocimiento al sector que corresponda para que se proceda a designar al sustituto, la elección se llevará a cabo de acuerdo con lo previsto en el presente reglamento. </p>

		 <p align="justify"><em>Artículo 21.</em> Los consejeros electos de esta forma durarán en su cargo hasta que se verifique la siguiente elección ordinaria. </p><br>

		 <h5 class="center">CAPÍTULO VI</h5>
         <h4 class="center">De los auxiliares del Director</h4><br>

		 <p align="justify"><em>Artículo 22.</em> Auxiliarán al Director en el desempeño de sus funciones: </p>

            <ol>
            	<li><p align="justify">El Secretario Académico;</p></li>
				<li><p align="justify">El Secretario Técnico;</p></li>
				<li><p align="justify">El Secretario Técnico de Planeación y Evaluación;</p></li>
				<li><p align="justify">El Secretario Administrativo, y</p></li>
				<li><p align="justify">Los jefes de los diferentes departamentos que integran el Centro.</p></li>
			</ol><br>

			<h4 class="center">SECCIÓN PRIMERA</h4>
            <h4 class="center">Del Secretario Académico</h4><br>

            <p align="justify"><em>Artículo 23.</em> El Secretario Académico será designado por el Coordinador de Humanidades, a propuesta del Director del Centro, de acuerdo con lo establecido por la legislación universitaria. </p>

            <p align="justify"><em>Artículo 24.</em> El Secretario Académico tendrá la responsabilidad de apoyar al Director en la coordinación y ejecución de los asuntos de su competencia. </p>

            <p align="justify"><em>Artículo 25.</em> El Secretario Académico tendrá las siguientes atribuciones:</p>

            <ol>
            	<li><p align="justify">Sustituir al Director en caso de licencias o comisiones de éste que no excedan de sesenta días;</p></li>
				<li><p align="justify">Sustituir al Director, con voz pero sin voto en el Consejo Técnico de Humanidades.</p></li>
				<li><p align="justify">Participar en la planeación y coordinación de las actividades académicas y académico-administrativas del Centro;</p></li>
				<li><p align="justify">Instrumentar la aplicación de las normas, políticas, procedimientos internos y líneas de acción para el control de las actividades académicas y vigilar que se apeguen a la legislación universitaria y a la normatividad establecida por el Titular, por el Consejo Técnico y por el Consejo Interno del Centro;</p></li>
				<li><p align="justify">Coordinar y apoyar el proceso de planeación y evaluación de la Secretaría Académica y las áreas que la conforman, desde la etapa del diagnóstico e integración de objetivos, metas mínimas y programas específicos,hasta la autoevaluación interanual y anual;</p></li>
				<li><p align="justify">Desarrollar un sistema de información que permita conocer el grado de avance de los programas y subprogramas, la identificación oportuna de las desviaciones y la toma de medidas correctivas inmediatas;</p></li>
				<li><p align="justify">Participar, con voz pero sin voto, en las reuniones del Consejo Académico del Área de las Humanidades y de las Artes en sustitución del Director;</p></li>
				<li><p align="justify">Informar a las  diversas comisiones y al personal académico de los acuerdos que el Consejo Técnico de Humanidades tome respecto a las disposiciones y asuntos académicos;</p></li>
				<li><p align="justify">Diseñar y desarrollar estrategias, políticas, procedimientos internos e instrumentos que permitan la mejora continua de los procesos académicos y académico-administrativos;</p></li>
				<li><p align="justify">Coordinar y supervisar el avance de los programas de trabajo del personal académico y proporcionarle el apoyo necesario para su realización, conforme al presupuesto del Centro;</p></li>
				<li><p align="justify">Coordinar el seguimiento de las actividades realizadas por los becarios y prestadores de servicio social adscritos al Centro;</p></li>
				<li><p align="justify">Supervisar la actualización de la base de datos curricular del personal académico del sistema integral de evaluación académica, en coordinación con el Área de Sistemas, con el fin de apoyar al Consejo Interno en el proceso de evaluación de los proyectos de investigación y en la toma de decisiones;</p></li>
				<li><p align="justify">Servir de enlace para llevar a cabo los trámites entre el personal académico y el Sistema Nacional de Investigadores (SNI); la Dirección General de Asuntos del Personal Académico (DGAPA); la Dirección Genera de Intercambio Académico (DGIA) y con otras dependencias universitarias e instituciones afines;</p></li>
				<li><p align="justify">Apoyar a la Dirección en las gestiones para el ingreso y repatriación de investigadores extranjeros formados en las áreas  de  especialidad del Centro, a fin de conformar la plantilla académica;</p></li>
				<li><p align="justify">Instrumentar programas de intercambio y colaboración con instituciones afines en el ámbito nacional e internacional;</p></li>
				<li><p align="justify">Colaborar con la dirección en la estructuración del Plan de Desarrollo, del programa anual de trabajo y del informe anual de actividades del Centro;</p></li>
				<li><p align="justify">Representar al Director del Centro en los diferentes foros nacionales e internacionales, cuando así le sea requerido;</p></li>
				<li><p align="justify">Verificar la correcta elaboración de los dictámenes y las disposiciones emitidas por las comisiones y supervisar las acciones acordadas por cada uno de los cuerpos colegiados;</p></li>
				<li><p align="justify">Verificar la correcta elaboración de las actas de los concursos de oposición abiertos o cerrados para su revisión y eventual aprobación por el Consejo Técnico;</p></li>
				<li><p align="justify">Coordinar de manera permanente con la Secretaría Administrativa todos los aspectos relativos a la contratación, promoción y diversos trámites académico-administrativos;</p></li>
				<li><p align="justify">Organizar y coordinar, con la Secretaría Técnica, las actividades de difusión, divulgación, publicación, promoción y distribución;</p></li>
				<li><p align="justify">Organizar y coordinar el desarrollo de las reuniones del Consejo Interno, de la Comisión Dictaminadora, de la Comisión de Biblioteca, del Comité  Editorial y de la Subcomisión de Superación Académica del Personal Académico;</p></li>
				<li><p align="justify">Fungir como secretario en el Consejo Interno con voz pero sin voto, así como participar como miembro en la Comisión de Biblioteca, en el Comité Editorial, en la Subcomisión de Superación Académica;</p></li>
				<li><p align="justify">Formular e integrar con la Secretaría Administrativa la elaboración del proyecto de presupuesto anual y someterlo a consideración de la dirección, y supervisar su correcto ejercicio, y</p></li>
				<li><p align="justify">Desarrollar todas aquellas actividades que le sean encomendadas por el Director, de acuerdo con la naturaleza, atribuciones y funciones de la Secretaría.</p></li>
            </ol><br>

            <h4 class="center">SECCIÓN SEGUNDA</h4>
            <h4 class="center">Del Secretario Técnico</h4><br>

			<p align="justify"><em>Artículo 26.</em> El Centro contará con un Secretario Técnico nombrado y removido libremente por el Director, conforme a lo previsto en la fracción II del artículo 54-A del Estatuto General. El Secretario Técnico colabora con el Director en la ejecución y coordinación de los asuntos técnicos del Centro. </p>

			<p align="justify"><em>Artículo 27.</em> Serán atribuciones del Secretario Técnico: </p>

            <ol>
            	<li><p align="justify">Planear el proceso editorial de las obras aprobadas para su publicación;</p></li>
				<li><p align="justify">Supervisar el cumplimiento de la normatividad universitaria en materia editorial;</p></li>
				<li><p align="justify">Coordinar y supervisar la difusión de las actividades académicas del Centro, apoyando el desarrollo de las conferencias, mesas redondas, foros, presentaciones de libros, etc. y la distribución de los espacios disponibles para las diversas actividades que se desarrollen;</p></li>
				<li><p align="justify">Coordinar la promoción, distribución y comercialización de las publicaciones del Centro;</p></li>
				<li><p align="justify">Coordinar la elaboración de informes y reportes de la  áreas de publicaciones, difusión y distribución, y</p></li>
				<li><p align="justify">Desarrollar todas aquellas actividades que le sean encomendadas por el Director, de acuerdo con la naturaleza, atribuciones y funciones de la Secretaría.</p></li>
			</ol><br>


			<h4 class="center">SECCIÓN TERCERA</h4>
            <h4 class="center">Del Secretario Técnico de Planeación y Evaluación</h4><br>

			<p align="justify"><em>Artículo 28.</em> El Secretario Técnico de Planeación y Evaluación será nombrado y removido libremente por el Director, conforme a lo previsto en la fracción II del artículo 54-A del Estatuto General y se encargará de diseñar las estrategias que permitan apoyar y fortalecer el desarrollo del Centro, así como contribuir a la generación de procesos de evaluación y seguimiento que impulsen la gestión interinstitucional. </p>

			<p align="justify"><em>Artículo 29.</em> Serán atribuciones del Secretario Técnico de Planeación y Evaluación: </p>

            <ol>
            	<li><p align="justify">Integrar la formulación del plan de desarrollo del Centro, estableciendo registro de metas, autoevaluación de logros alcanzados, propuestas de acciones a tomar para impulsar y mejorar los programas establecidos;</p></li>
				<li><p align="justify">Dar seguimiento a los acuerdos tomados en las comisiones del Consejo Técnico de Humanidades que involucren tareas para el Centro;</p></li>
				<li><p align="justify">Colaborar en la formulación del proyecto de presupuesto del Centro de acuerdo con el programa de trabajo anual y el plan de desarrollo; a distribución de los espacios disponibles para las diversas actividades que se desarrollen;</p></li>
				<li><p align="justify">Dar seguimiento a las acciones concernientes al plan de desarrollo y del programa anual del Centro;</p></li>
				<li><p align="justify">Llevar a cabo el proceso de autoevaluación y generar los informes que den cuenta del cumplimiento de las actividades previstas y alcances logrados con relación al plan de desarrollo y al programa de trabajo anual del Centro, y </p></li>
				<li><p align="justify">Las demás que el Director le encomiende.</p></li>
			</ol><br>


			<h4 class="center">SECCIÓN CUARTA</h4>
            <h4 class="center">Del Secretario Administarivo</h4><br>

			<p align="justify"><em>Artículo 30.</em> El director del Centro propondrá ante el Patronato Universitario y la Secretaría Administrativa de la UNAM una terna de candidatos a ocupar el cargo de Secretario Administrativo del Centro. El Secretario Administrativo colabora con el Director en la planeación, organización, coordinación y control de la administración de los recursos humanos, financieros, materiales y servicios del Centro, conforme a la normatividad vigente en la Universidad. </p>

			<p align="justify"><em>Artículo 31.</em> Serán atribuciones del Secretario Administrativo:</p>

            <ol>
            	<li><p align="justify">Ejercer el presupuesto asignado al Centro;</p></li>
				<li><p align="justify">Vigilar la correcta aplicación de los recursos humanos, financieros y materiales asignados al Centro;</p></li>
				<li><p align="justify">Integrar la información que se requiera para la elaboración del proyecto de presupuesto del Centro;</p></li>
				<li><p align="justify">Vigilar el cumplimiento de la normatividad y lineamientos sobre desarrollo de personal y derechos laborales;</p></li>
				<li><p align="justify">Atender, tramitar y controlar los movimientos del personal administrativo, de funcionarios, académicos y de base asignados al Centro;</p></li>
				<li><p align="justify">Supervisar las labores del personal administrativo del Centro e informar oportunamente al Director de las mismas;</p></li>
				<li><p align="justify">Atender las solicitudes de apoyo y servicio para el buen funcionamiento del Centro, y</p></li>
				<li><p align="justify">Las demás que el Director le encomiende.</p></li>
			</ol><br>

			<h5 class="center">CAPÍTULO VII</h5>
            <h4 class="center">De las áreas de apoyo a la investigación</h4><br>

			<p align="justify"><em>Artículo 32.</em>En el Centro existirán los siguientes Departamentos, Sección y Oficina cuyos objetivos serán:</p>

			<ol>
            	<li><p align="justify">Difusión: difundir las actividades académicas y publicaciones que genera el Centro, a nivel nacional e internacional, para captar al mayor número de estudiosos de temas latinoamericanos y del Caribe.</p></li>
				<li><p align="justify">Biblioteca: compilar, organizar y difundir la información bibliográfica sobre América Latina y el Caribe que apoye las líneas de investigación del Centro y contribuya a la realización de las actividades académicas de los investigadores adscritos, mediante el desarrollo de colecciones y el diseño de servicios especializados; asimismo, atender las solicitudes de información de la comunidad universitaria y el público en general interesado en los Estudios Latinoamericanos. Vigilar la correcta aplicación de los recursos humanos, financieros y materiales asignados a la biblioteca.</p></li>
				<li><p align="justify">Distribución y promoción: distribuir las publicaciones que genera el Centro, así como evaluar las condiciones de mercado para identificar y elegir nuevos canales de distribución.</p></li>
				<li><p align="justify">Publicaciones: coordinar y supervisar la edición de los trabajos de investigación y obras autorizadas por el Comité editorial, a fin de que éstos cumplan con los criterios editoriales y académicos del Centro.</p></li>
				<li><p align="justify">Sección académica de sistemas: proporcionar los servicios, espacios, equipamiento y soporte técnico de cómputo y telecomunicaciones que requieran las áreas y comunidad académica que conforman al Centro, para el apoyo de sus funciones sustantivas, así como incorporar y actualizar al interior el conocimiento de nuevos desarrollos tecnológicos.</p></li>
				<li><p align="justify">Oficina del Centro de Investigaciones sobre América Latina y el Caribe (antes Centro Coordinador y Difusor de Estudios Latinoamericanos, creado por acuerdo del Rector publicado el 13 de diciembre de 1979 en la Gaceta UNAM) tiene como objetivo coordinar aquellas actividades académicas demandadas por la Sociedad Latinoamericana de Estudios sobre América Latina y el Caribe (SOLAR) y la Federación Internacional de Estudios sobre América Latina y el Caribe (FIEALC), así como de otras asociaciones similares, en concordancia con el espíritu y legislación de nuestra universidad.</p></li>
			</ol><br>


			<h5 class="center">CAPÍTULO VIII</h5>
			<h4 class="center">De las comisiones y comités</h4><br>

			<p align="justify"><em>Artículo 33.</em>En el Centro funcionarán las siguientes comisiones y comités:</p>

			<ol>
            	<li><p align="justify">La Comisión dictaminadora;</p></li>
				<li><p align="justify">La Comisión evaluadora PRIDE;</p></li>
				<li><p align="justify">La Comisión de biblioteca;</p></li>
				<li><p align="justify">El Comité editorial;</p></li>
				<li><p align="justify">La Subcomisión de superación académica, y</p></li>
				<li><p align="justify">Las demás que acuerde el Consejo Interno.</p></li>
			</ol><br>

			<h4 class="center">SECCIÓN PRIMERA</h4>
			<h4 class="center">De la Comisión dictaminadora</h4><br>

			<p align="justify"><em>Artículo 34.</em>La Comisión dictaminadora se formará con seis miembros, dos nombrados por el Consejo Interno, dos por el Consejo Académico de Área de las Humanidades y de las Artes y dos más por el personal académico del Centro, serán designados de preferencia entre los profesores e investigadores definitivos de otras entidades o dependencias de la Universidad. </p>

			<p align="justify"><em>Artículo 35.</em>Cada dos años se revisará la integración de la comisión para modificarla cuando así convenga a juicio del Consejo Técnico de Humanidades o del Consejo Interno del Centro. </p>
			<p align="justify">En caso de renuncia, los miembros de la comisión serán sustituidos por quien hizo la designación. Las nuevas designaciones deberán ser también ratificadas por el Consejo Académico de Área de las Humanidades y de las Artes.</p><br>


			<h4 class="center">SECCIÓN SEGUNDA</h4>
			<h4 class="center">De la Comisión evaluadora PRIDE</h4><br>

			<p align="justify"><em>Artículo 36.</em>Para el Programa de Primas al Desempeño del Personal Académico de Tiempo Completo (PRIDE), existirá en el Centro una Comisión evaluadora cuya integración y funcionamiento se regirá de acuerdo con la convocatoria y lineamientos vigentes del Programa. Dicha Comisión deberá presentar sus dictámenes ante el Consejo Técnico de Humanidades para su resolución definitiva.</p><br>

			<h4 class="center">SECCIÓN TERCERA</h4>
			<h4 class="center">De la Comisión de biblioteca</h4><br>

			<p align="justify"><em>Artículo 37.</em>La Comisión de biblioteca tendrá como objetivo definir las políticas de organización interna, de préstamo y de adquisiciones de la Biblioteca, y vigilar su buen funcionamiento, conforme a las directrices generales contenidas en el Reglamento General del Sistema Bibliotecario y de Información de la UNAM y en su propio reglamento.</p>

			<p align="justify"><em>Artículo 38.</em>La Comisión de biblioteca estará integrada por: </p>

			<ol>
            	<li><p align="justify">El Director del Centro, quien la presidirá; en su ausencia, lo suplirá el Secretario Académico;</p></li>
				<li><p align="justify">El Jefe del Departamento de la biblioteca, quien fungirá como secretario;</p></li>
				<li><p align="justify">Tres miembros del personal académico del Centro nombrados por el Consejo Interno;</p></li>
				<li><p align="justify">Un representante del personal académico que labore en la biblioteca nombrado por el Consejo Interno, y</p></li>
				<li><p align="justify">Un representante del personal bibliotecario-administrativo que labore en la biblioteca nombrado por el Consejo Interno.</p></li>
			</ol>

			<p align="justify">La Comisión de biblioteca tendrá sesiones ordinarias cada tres meses y extraordinarias cada vez que sea necesario.</p>

			<p align="justify"><em>Artículo 39.</em>Serán atribuciones de la Comisión de biblioteca:</p>

			<ol>
            	<li><p align="justify">Auxiliar al Director del Centro, a la Secretaría Académica y al responsable o encargado de biblioteca en los asuntos referentes a la misma;</p></li>
				<li><p align="justify">Opinar sobre las políticas de desarrollo y crecimiento de la biblioteca;</p></li>
				<li><p align="justify">Colaborar en las tareas de diseño, operación y evaluación de los servicios bibliotecarios y de información y vigilar su aplicación;</p></li>
				<li><p align="justify">Seleccionar el material documental en cualquier formato, a partir de las bibliografías básicas que le haga llegar el personal académico y demás usuarios;</p></li>
				<li><p align="justify">Asegurar que las publicaciones que edita el Centro formen parte del acervo;</p></li>
				<li><p align="justify">Prever las necesidades presupuestarias de la biblioteca para la adquisición de material documental en cualquier formato, compra de mobiliario y equipo especializado, así como la ampliación de espacios y servicios;</p></li>
				<li><p align="justify">Aprobar los planes, proyectos, programas, estrategias, procedimientos, propuestas y acciones que se presenten para el mejor funcionamiento de la biblioteca, para la mejora continua de la calidad de los procesos y servicios;</p></li>
				<li><p align="justify">Opinar sobre las necesidades del personal que labore en la biblioteca, incluidos los alumnos de servicio social;</p></li>
				<li><p align="justify">Coadyuvar en la vigilancia de los recursos destinados la  biblioteca y así éstos sean utilizados para los fines a los cuales fueron asignados;</p></li>
				<li><p align="justify">Presentar al Consejo Interno el reglamento interno de la biblioteca y de la propia Comisión, así como las modificaciones a los mismos;</p></li>
				<li><p align="justify">Determinar las medidas generales para garantizar los servicios mencionados en el artículo 17 del Reglamento General de Servicios Bibliotecarios de la UNAM;</p></li>
				<li><p align="justify">Conocer y supervisar el plan de capacitación, formación y desarrollo profesional del personal bibliotecario;</p></li>
				<li><p align="justify">Vigilar el cumplimiento de las observaciones que la Dirección General de Bibliotecas realice a la biblioteca correspondiente, y</p></li>
				<li><p align="justify">Las demás que se desprendan de su naturaleza y las que le confiera la legislación universitaria.</p></li>
			</ol>

			<p align="justify">Cada dos años se revisará la integración de la comisión para modificarla cuando así convenga a juicio del Consejo Interno.</p><br>

			<h4 class="center">SECCIÓN CUARTA</h4>
			<h4 class="center">Del Comité editorial</h4><br>

			<p align="justify"><em>Artículo 40.</em> El Comité editorial será la instancia responsable de diseñar y ejecutar la política editorial del Centro y evaluará los originales de las obras que serán sometidos a su consideración, por conducto del Director, determinando si los mismos reúnen los requisitos para publicarse. </p>

			<p align="justify"><em>Artículo 41.</em> El Comité editorial estará integrada por: </p>

			<ol>
            	<li><p align="justify">El titular del Centro;</p></li>
				<li><p align="justify">El Secretario Académico; </p></li>
				<li><p align="justify">El Jefe del Departamento de Publicaciones y</p></li>
				<li><p align="justify">Cuatro investigadores del Centro, quienes representarán a cada una de las cuatro áreas de investigación, designados por el Consejo Interno.</p></li>
			</ol>

			<p align="justify"><em>Artículo 42.</em> Serán atribuciones del Comité editorial: </p>

			<ol>
            	<li><p align="justify">Establecer las políticas y prioridades para la publicación de los proyectos editoriales;</p></li>
				<li><p align="justify">Establecer los lineamientos para la selección de proyectos editoriales, tomando en consideración los objetivos del Centro, los factores de naturaleza académica, el mercado editorial, las necesidades de difusión y las disposiciones del Consejo Editorial de la UNAM;</p></li>
				<li><p align="justify">Determinar los criterios y convenciones para la recepción y dictamen de originales presentados a su consideración;</p></li>
				<li><p align="justify">Sugerir a los académicos o especialistas que dictaminarán las obras;</p></li>
				<li><p align="justify">Proponer la edición de libros, catálogos y otras publicaciones. Cuando lo considere necesario, podrá solicitar un dictamen técnico editorial a la Dirección General de Publicaciones y Fomento Editorial que lo auxilie en las decisiones al respecto;</p></li>
				<li><p align="justify">Analizar las coediciones con instituciones y editoriales externas;</p></li>
				<li><p align="justify">Analizar las colaboraciones en materia editorial con dependencias y entidades universitarias;</p></li>
				<li><p align="justify">Analizar las reimpresiones y reediciones de las obras que se encuentren agotadas;</p></li>
				<li><p align="justify">Fijar el tiraje y las características del material que se empleará en cada obra, considerando para tal efecto el tipo de lector al que está destinada, sus posibilidades de comercialización y la suficiencia presupuestal;</p></li>
				<li><p align="justify">Determinar el uso de las nuevas tecnologías de impresión en los proyectos editoriales: tiraje por demanda, impresión digital, publicaciones electrónicas en disco óptico, publicaciones electrónicas en red y las que vayan surgiendo;</p></li>
				<li><p align="justify">Analizar, proponer y cancelar colecciones y/o series, y</p></li>
				<li><p align="justify">Mantener al día el estado del programa de publicaciones del Centro, respetando la periodicidad de los órganos de información de la dependencia.</p></li>
			</ol>

			<p align="justify">Cada dos años se revisará la integración del Comité para modificarlo cuando así convenga a juicio del Consejo Interno.</p><br>

			<h4 class="center">SECCIÓN QUINTA</h4>
			<h4 class="center">De la Subcomisión de superación académica del personal académico</h4><br>

			<p align="justify"><em>Artículo 43.</em> La Subcomisión de superación académica del personal académico estará conformada por el Director del Centro, quien la presidirá, dos académicos designados por el Director y tres académicos designados por el Consejo Interno. Todos ellos deberán ser investigadores titulares de carrera con nivel C o superior dentro del Programa de Primas al Desempeño del Personal Académico de Tiempo Completo, el Director designará al Secretario Técnico de la Subcomisión. </p>
			<p align="justify"><em>Artículo 44.</em> La Subcomisión de superación académica del personal académico se encargará de elaborar y presentar el Programa de Superación Académica del Centro; fungirá como el órgano encargado de la difusión y promoción del programa; analizará, preseleccionará y presentará las propuestas correspondientes y servirá de enlace con la DGAPA. </p>
			<p align="justify">Cada dos años se revisará la integración de la comisión para modificarla cuando así convenga a juicio del Consejo Interno.</p><br>


			<h5 class="center">CAPÍTULO IX</h5>
			<h4 class="center">Del personal académico</h4><br>

			<p align="justify"><em>Artículo 45.</em>El personal académico del Centro estará constituido por:</p>

			<ol>
            	<li><p align="justify">Investigadores</p></li>
				<li><p align="justify">Profesores</p></li>
				<li><p align="justify">Técnicos académicos.</p></li>
			</ol>

			<p align="justify"><em>Artículo 46.</em> El personal académico tendrá las siguientes obligaciones, a la par de las que establece el Estatuto del Personal Académico: </p>

			<ol>
            	<li><p align="justify">Declarar su adscripción al Centro en todas las actividades académicas en que participe;</p></li>
				<li><p align="justify">Realizar sus actividades de conformidad con los programas aprobados por los órganos competentes;</p></li>
				<li><p align="justify">Enriquecer y actualizar sus conocimientos;</p></li>
				<li><p align="justify">Someter a la consideración del Consejo Interno y a la aprobación del Consejo Técnico de Humanidades su programa anual de labores, llevarlo a cabo puntualmente y rendir informe sobre la realización del mismo;</p></li>
				<li><p align="justify">Supervisar el desempeño y el comportamiento de sus becarios y prestadores de servicio social dentro del Centro, y</p></li>
				<li><p align="justify">Las demás que establece la legislación universitaria.</p></li>
			</ol>

			<p align="justify"><em>Artículo 47.</em> Los investigadores y profesores tendrán las siguientes obligaciones: </p>

			<ol>
            	<li><p align="justify">Cumplir, salvo excusa fundada, las comisiones académicas que se le encomienden y rendir los informes correspondientes;</p></li>
				<li><p align="justify">Profesar cátedra de acuerdo con lo que señala el Estatuto del Personal Académico;</p></li>
				<li><p align="justify">Presentar al Director del Centro, al Consejo Interno y al Consejo Técnico de Humanidades el programa de actividades académicas que pretenda realizar durante el periodo sabático e informar al término del mismo sobre las tareas realizadas;</p></li>
				<li><p align="justify">Participar en las actividades de su área de investigación, y</p></li>
				<li><p align="justify">Contribuir con sus resultados de investigación a las publicaciones del Centro.</p></li>
			</ol>

			<p align="justify"><em>Artículo 48.</em> Los técnicos académicos tendrán la obligación de participar en el desarrollo del plan de trabajo de los departamentos del Centro o áreas donde estén adscritos. </p>

			<p align="justify"><em>Artículo 49.</em> Además de los que establece la legislación universitaria, el personal académico tendrá los siguientes derechos: </p>

			<ol>
            	<li><p align="justify">Participar con voz y voto, por sí o por medio de sus representantes, en los órganos colegiados del Centro;</p></li>
				<li><p align="justify">Elegir a sus representantes ante el Consejo Universitario, el Consejo Técnico de Humanidades, el Consejo Interno y el Consejo Académico de Área de las Humanidades y las Artes;</p></li>
				<li><p align="justify">Organizarse en asociaciones o colegios, de acuerdo con los principios contenidos en la legislación universitaria, principalmente la autonomía y libertad de cátedra e investigación;</p></li>
				<li><p align="justify">Obtener reconocimiento académico por su participación y colaboración en las actividades académicas y colegiadas que desarrollen en la UNAM;</p></li>
				<li><p align="justify">Participar en los eventos académicos organizados por el Centro, y</p></li>
				<li><p align="justify">Las demás que establece la legislación universitaria.</p></li>
			</ol><br>


			<h5 class="center">CAPÍTULO X</h5>
			<h4 class="center">De los representantes del personal académico ante el Consejo Técnico de Humanidades</h4><br>

			<p align="justify"><em>Artículo 50.</em> Los requisitos, las características de la elección, la duración del encargo y las funciones del representante del personal académico del Centro ante el Consejo Técnico de Humanidades se ajustarán a lo dispuesto en los artículos 2°, fracción IV, y penúltimo párrafo, y 3° a 9° del Reglamento Interno del Consejo Técnico de Humanidades y demás relativos de la legislación universitaria. </p><br>


			<h5 class="center">CAPÍTULO XI</h5>
			<h4 class="center">Del representante del personal académico ante el Consejo Académico del Área de las Humanidades y de las Artes </h4><br>

			<p align="justify"><em>Artículo 51.</em> Los requisitos, las características de la elección, la duración del encargo y las funciones del representante del personal académico del Centro ante el Consejo Académico del Área de las Humanidades y de las Artes se ajustarán a lo dispuesto por los artículos 111, 113 y 114 del Estatuto General de la UNAM y demás relativos de la legislación universitaria. </p><br>


			<h5 class="center">CAPÍTULO XII</h5>
			<h4 class="center">Del personal administrativo</h4><br>

			<p align="justify"><em>Artículo 52.</em> Los procedimientos para la admisión y promoción del personal administrativo del Centro, así como sus derechos y obligaciones, serán los establecidos en el Contrato Colectivo de Trabajo y en sus reglamentos. </p><br>


			<h5 class="center">CAPÍTULO XIII</h5>
			<h4 class="center">Del servicio social y los becarios</h4><br>

			<p align="justify"><em>Artículo 53.</em> Podrán prestar su servicio social en el Centro los estudiantes que satisfagan los requisitos previstos en el Reglamento General del Servicio Social de la UNAM y que sean aceptados por esta entidad. </p>
			<p align="justify"><em>Artículo 54.</em> Los derechos y obligaciones de quienes presten su servicio social en el Centro se ajustarán a lo dispuesto en el Reglamento General del Servicio Social de la UNAM y en la legislación universitaria. </p>
			<p align="justify"><em>Artículo 55.</em> Serán becarios del Centro quienes reciban ayuda económica o apoyos diversos de la UNAM o de otras instituciones, con el  objeto de lograr o perfeccionar su formación académica, sean de tesis de licenciatura, estudios y tesis de posgrado, estancias de investigación y posdoctorales.  </p>
			<p align="justify"><em>Artículo 56.</em> Los becarios ingresarán al Centro en los términos que señale la convocatoria o programa correspondiente, o de las condiciones que establezcan otras instituciones, asociaciones o fundaciones que apoyen la formación académica, así como a los criterios aprobados sobre la materia por el Consejo Interno del Centro. </p>


			<h5 class="center">TRANSITORIOS</h5><br>

			<p align="justify"><em>Primero.</em> Este reglamento entrará en vigor al día siguiente de su aprobación por el Consejo Técnico de Humanidades.</p>

			<p align="justify"><em>Segundo.</em> El presente reglamento deja sin efecto a su similar, aprobado por el Consejo Técnico de Humanidades el 16 de octubre de 1986.  </p>

