            <h4 class="center">Capitulo I</h4>
            <h5 class="center">NATURALEZA, FUNCIONES Y ESTRUCTURA</h5><br>
            <p align="justify"><em>Artículo 1°.</em> Este Reglamento tiene por objeto regular los servicios bibliotecarios así como los derechos y obligaciones que tendrán los usuarios.
            </p><br>

            <p align="justify"><em>Artículo 2°.</em> La biblioteca “Simón Bolívar” tiene como objetivo sustantivo apoyar con servicios de información documental al personal del Centro y a la comunidad universitaria en las actividades de investigación, docencia y difusión sobre América Latina. Es una biblioteca especializada en historia, literatura, filosofía, historia de las ideas y ciencias políticas y sociales de la región.
            </p><br>

			<p align="justify"><em>Artículo 3°.</em> La biblioteca “Simón Bolívar” tiene las siguientes funciones:
            </p><br>

            <ol>
            	<li>Seleccionar el material documental que es adquirido por el Centro.</li>
            	<li>Adquirir oportunamente el material documental que se requiera para mantener actualizado el acervo en las diferentes áreas de investigación del Centro.</li>
            	<li>Organizar, conservar y controlar el acervo de la biblioteca.</li>
            	<li>Difundir los servicios que proporciona la biblioteca entre el personal del Centro y en otras instituciones.</li>
            </ol>

            <p align="justify"><em>Artículo 4°.</em> Para efectos del presente Reglamento se entiende por usuarios y servicios bibliotecarios lo siguiente:
            </p><br>

            <ol>
            	<li>Usuarios son los beneficiarios de los servicios bibliotecarios proporcionados por esta biblioteca.</li>
            	<li>Servicios bibliotecarios son el conjunto de acciones académicas, técnicas y administrativas mediante las cuales se selecciona, adquiere, procesa, sistematiza, almacena, difunde, circula, controla y preserva el material documental que proporciona información para coadyuvar con los fines sustantivos del Centro y de la UNAM. </li>
            </ol>

            <p align="justify"><em>Artículo 5°.</em> La estructura organizacional de esta biblioteca es la siguiente:
            </p><br>
            <oL>
            	<li>La Comisión de biblioteca.</li>
            	<li>El responsable de biblioteca.</li>
            	<li>El área de procesos técnicos bibliográficos.</li>
            	<li>El área de procesos técnicos hemerográficos.</li>
            	<li>El área de servicios.</li>
            </ol>

            <p align="justify"><em>Artículo 6°.</em>Conforme lo establece el artículo 18 del Reglamento General del Sistema Bibliotecario de la UNAM, el Centro ha integrado una Comisión de Biblioteca, órgano de apoyo a las labores bibliotecarias, de la siguiente manera:
            </p><br>
            <ol>
            	<li> El Director del Centro, quien la preside; en su ausencia lo hará el Secretario Académico.</li>
            	<li>El Jefe del Departamento de biblioteca, quien funge como secretario.</li>
            	<li>Tres investigadores.</li>
            	<li>Un representante del personal académico que labore en la biblioteca.</li>
            	<li>Un representante del personal administrativo que labore en la Biblioteca</li>
            </ol>

            <p align="justify"><em>Artículo 7°.</em>Los integrantes de la Comisión de Biblioteca deben reunir los siguientes requisitos:
            </p><br>
            <ol>
            	<li>Investigadores:
            		<ol>
            			<li>Realizar actividades de investigación en la entidad académica.</li>
            			<li>Tener tres años de servicios académicos en el Centro.</li>
            			<li>No ocupar en la Universidad ningún puesto administrativo en el momento de la designación ni durante el desempeño de su cargo.</li>
            			<li>No haber sido sancionado por faltas graves contra la disciplina universitaria.</li>
            		</ol>
            	</li>
            	<li>Personal bibliotecario (Técnicos Académicos):
            		<ol>
            			<li>Tener nombramiento que lo acredite y que sea preferentemente en el área de bibliotecología;</li>
            			<li>Haber laborado, como mínimo, durante un año en actividades bibliotecarias, y</li>
            			<li> Haber obtenido el nombramiento por concurso.</li>
            		</ol>
            	</li>
            </ol>

            <p align="justify"><em>Artículo 8°.</em>Esta Comisión debe ser ratificada o renovada por el Consejo Interno.
            </p><br>

            <p align="justify"><em>Artículo 9°.</em>Son atribuciones de la Comisión:
            </p><br>
            <ol>
            	<li>Auxiliar al Director del Centro, a la Secretaría Académica y al responsable o encargado de biblioteca en los asuntos referentes a la misma</li>
            	<li>Opinar sobre las políticas de desarrollo y crecimiento de la biblioteca.</li>
            	<li>Colaborar en las tareas de diseño, operación y evaluación de los servicios bibliotecarios y supervisar su aplicación.</li>
            	<li>Seleccionar el material documental, a partir de las bibliografías básicas que le haga llegar el personal académico, y demás usuarios</li>
            	<li> Asegurar que las publicaciones que edita la dependencia formen parte del acervo.</li>
            	<li>Prever las necesidades presupuestarias de la biblioteca para la adquisición de material documental, compra de mobiliario y equipo especializado; así como la ampliación de espacios y servicios.</li>
            	<li>Aprobar los planes, proyectos, programas, estrategias, procedimientos, propuestas y acciones que se presenten para el mejor funcionamiento de la biblioteca, para la mejora continua de la calidad de los procesos y servicios.</li>
            	<li>Opinar sobre las necesidades del personal que labore en la biblioteca, incluidos los alumnos de servicio social.</li>
            	<li>Coadyuvar en la supervisión de los recursos asignados a la biblioteca y al logro de los fines sustantivos.</li>
            	<li>Presentar al Consejo Interno el Reglamento interno de la biblioteca y de la propia Comisión, así como las modificaciones a los mismos.</li>
            	<li>Determinar las medidas generales para garantizar los servicios mencionados en el artículo 17 del Reglamento General de Servicios Bibliotecarios de la UNAM.</li>
            	<li>Conocer y supervisar el plan de capacitación, formación y desarrollo profesional del personal bibliotecario.</li>
            	<li> Las demás que le confiera la Legislación Universitaria y las que se desprendan de su naturaleza.</li>
            </ol>


            <h4 class="center">Capitulo II</h4>
            <h5 class="center">DE LOS USUARIOS</h5><br>

            <p align="justify"><em>Artículo 10°.</em> Para efectos del presente Reglamento se entiende por: </p><br>

            <ol>
            	<li> Usuario interno a todo el personal del Centro:
            		<ol>
            			<li>Investigadores</li>
            			<li>Investigadores visitantes*</li>
            			<li>Becarios*</li>
            			<li>Alumnos de servicio social*</li>
            			<li>Técnicos académicos</li>
            			<li>Personal administrativo.</li>
            		</ol>
            		<p align="justify">* Los usuarios especificados en los incisos b),c) y d), deberán contar con el respaldo de una carta firmada por el investigador, el jefe de departamento o área al que se encuentren asociados y el visto bueno de la Secretaría Académica; en la carta, se indicará el periodo de tiempo en el que, bajo la responsabilidad del signatario, se autoriza el préstamo. Cuando se presenten cambios en la fecha de estancia, el responsable deberá notificar por escrito a la Biblioteca, la fecha de término con al menos diez días hábiles de anticipación, con el propósito de recuperar todos los materiales prestados.</p>
            	</li>
            	<li>Usuarios externos: la comunidad universitaria y el público en general.</li>
            </ol><br>

			<p align="justify"><em>Artículo 11°.</em> Para identificación de los usuarios:</p><br>
			<ol>
				<li>En el caso de los usuarios internos, la biblioteca cuenta con un registro actualizado.</li>
				<li>Los usuarios externos universitarios deben acreditarse con la credencial de la UNAM resellada.</li>
				<p align="justify">Asimismo, los usuarios de otras instituciones deben presentar una identificación vigente con fotografía de su escuela o centro de trabajo.</p>
			</ol>

			<p align="justify"><em>Artículo 12°.</em> Los usuarios internos tienen derecho a todos los servicios especificados en el artículo 14 del presente Reglamento. Todos los usuarios tienen además el derecho de presentar iniciativas, opiniones y quejas sobre los servicios bibliotecarios, ante las instancias correspondientes.</p><br>

			<p align="justify"><em>Artículo 13°.</em>Todos los usuarios tienen como obligaciones: </p><br>

			<ol>
				<li>Cumplir con las disposiciones del presente Reglamento.</li>
				<li>Responsabilizarse del material documental que les sea proporcionado.</li>
				<li> Contribuir a preservar los inmuebles, mobiliario y equipo de la biblioteca y sujetarse a los mecanismos de control, seguridad y vigilancia establecidos.</li>
				<li> Respetar las políticas internas establecidas para la devolución del material documental.</li>
				<li>Mantener silencio dentro de la sala de lectura.</li>
				<li>Guardar respeto y consideración a los usuarios y al personal de la biblioteca.</li>
				<li>Abstenerse de fumar.</li>
				<li>Los usuarios internos deben, además, responsabilizarse de la actualización de los datos de su registro personal.</li>
			</ol>

			<h4 class="center">Capitulo III</h4>
            <h5 class="center">DE LOS SERVICIOS</h5><br>

			<p align="justify"><em>Artículo 14°.</em>La biblioteca “Simón Bolívar” proporciona los servicios en horario continuo de 9:00 a 18:00 horas, de lunes a viernes en días hábiles.</p><br>

			<p align="justify"><em>Artículo 15°.</em>La biblioteca proporciona los siguientes servicios:</p><br>

			<ol>
				<li>Préstamo:
					<ol>
						<li>Préstamo interno o en sala.</li>
						<li>Préstamo externo o a domicilio.</li>
						<li>Préstamo interbibliotecario.</li>
					</ol>
				</li>
				<li>Consulta:
					<ol>
						<li>Orientación general a usuarios y respuesta a preguntas específicas.</li>
						<li>Asesoría sobre el uso de fuentes de información.</li>
						<li>Búsqueda a través de las bases de datos en CD-ROM y en línea.</li>
						<li> Elaboración de bibliografías.</li>
					</ol>
				</li>
				<li> Alerta bibliográfica.</li>
			</ol>

			<p align="justify"><em>Artículo 16°.</em>De acuerdo con el inciso I del artículo 17 del Reglamento General del Sistema Bibliotecario de la UNAM, el servicio de préstamo interno o en sala consiste en facilitar el material documental a los usuarios dentro de la sala de lectura de la biblioteca.</p>
			<p align="justify">Para hacer uso de este servicio, los usuarios deben:</p><br>

			<ol>
				<li> Llenar correctamente las formas de préstamo.</li>
				<li>En el caso de usuarios externos entregar una identificación actualizada conforme lo especifica el artículo 11 de este Reglamento.</li>
				<li> Los usuarios pueden solicitar hasta tres volúmenes a la vez.</li>
				<li> El material documental debe ser devuelto el mismo día y en el horario de servicio de la biblioteca.</li>
			</ol>

			<p align="justify"><em>Artículo 17°.</em>El servicio de préstamo externo o a domicilio es el que permite la consulta del material documental fuera de las instalaciones de la biblioteca y únicamente tienen derecho a él, los usuarios internos.</p>
			<p align="justify">Para hacer uso de este servicio deben cumplir con los siguientes lineamientos:</p><br>

			<ol>
				<li>Llenar correctamente las formas de préstamo.</li>
				<li>Los investigadores pueden solicitar hasta 15 volúmenes por 30 días, con excepción de los materiales de la colección de consulta (diccionarios, enciclopedias, atlas, etc.) y material reservado.</li>
				<li>Los investigadores visitantes hasta 15 volúmenes por 15 días, con las mismas restricciones de los materiales descritos en el párrafo anterior.</li>
				<li>Los técnicos académicos y trabajadores administrativos hasta 3 volúmenes por 15 días; únicamente de libros de la colección general.</li>
				<li>Los becarios y alumnos de servicio social, que cumplan con lo citado en el artículo 10, hasta 3 volúmenes por 7 días, igualmente sólo libros de la colección general.</li>
				<li>Devolver el material en la fecha de vencimiento.</li>
				<li>En caso de renovación presentar el material y seguir el trámite correspondiente.</li>
				<li>Si el material es solicitado por otro usuario, no se autorizará la renovación.</li>
			</ol>

			<p align="justify"><em>Artículo 18°.</em>El servicio de préstamo interbibliotecario se realiza con las instituciones con las que se ha firmado convenio.</p>
			<p align="justify">El servicio de préstamo interbibliotecario se otorga a los usuarios internos de acuerdo con los siguientes lineamientos:</p><br>

			<ol>
				<li>Las solicitudes se reciben durante toda la semana dentro del horario de servicios. La biblioteca se encarga de verificar, localizar y reservar el material; el usuario se responsabiliza de recoger y devolver el material.</li>
				<li>Si el material documental es localizado en las bibliotecas del circuito exterior de Ciudad Universitaria o del sur de la Ciudad, se ha designado un día a la semana para traer y devolver el material.</li>
				<li>La biblioteca se responsabiliza del buen uso y devolución oportuna del material prestado.</li>
			</ol>

			<p align="justify"><em>Artículo 19°.</em>El servicio de préstamo interbibliotecario que ofrece el Centro a otras bibliotecas se llevará a cabo de acuerdo a lo siguiente:</p>

			<ol>
				<li>Debe contar con su convenio vigente, llenar correctamente las formas de préstamo interbibliotecario y contener firma autorizada y sello. </li>
				<li>No se aceptarán papeletas llenadas a mano o alteradas.</li>
				<li>Se prestarán hasta 10 volúmenes durante 5 días hábiles.</li>
				<li>Se tendrá derecho a una renovación siempre y cuando el material no haya sido solicitado por otro usuario. La renovación puede ser personal o vía telefónica.</li>
				<li> No se autorizarán otros préstamos a las bibliotecas que adeuden materiales y se aplicarán, sin excepción, las mismas sanciones que a los usuarios, ver Capítulo VI. De las Sanciones.</li>
			</ol>

			<p align="justify"><em>Artículo 20°.</em>El servicio de consulta consiste en conocer las necesidades de información de los usuarios para proporcionarles el material que requieran o canalizarlos a otras bibliotecas.</p>

			<ol>
				<li> Para todos los usuarios se ofrece:
					<ol>
						<li>Orientación general y respuesta a preguntas específicas.</li>
						<li>Asesoría sobre el uso de fuentes de información.</li>
					</ol>
				</li>
			</ol>
			<ol>
				<li> Además, los usuarios internos podrán tener acceso a:
					<ol>
						<li>Búsquedas a través de bases de datos en CD-ROM y en línea.</li>
						<li> Elaboración de Bibliografías.</li>
					</ol>
				</li>
			</ol>

			<p align="justify"><em>Artículo 21°.</em>El servicio de alerta bibliográfica consiste en informar sobre el material recientemente adquirido y las últimas novedades; se proporciona a través de los medios electrónicos disponibles y está dirigido principalmente a los investigadores.</p><br>

			<h4 class="center">Capitulo IV</h4>
            <h5 class="center">DE LAS COLECCIONES</h5><br>


			<p align="justify"><em>Artículo 22°.</em>El acervo de la biblioteca está dividido, de acuerdo a su formato, en las siguientes colecciones:</p><br>

			<ol>
				<li>General: está integrada por los libros que cubren las áreas de investigación del Centro.</li>
				<li>Consulta: diccionarios, enciclopedias, atlas etc.</li>
				<li>Publicaciones periódicas: revistas especializadas en el área.</li>
				<li>Folletos: separatas, trípticos, artículos de revista y hojas sueltas.</li>
				<li>CD-ROM: bases de datos que contienen información referencial y de texto completo.</li>
				<li>Mapas.</li>
				<li>Discos L.P. conformada por las colecciones Voz Viva de México y Voz de América Latina.</li>
				<li>Fondo reservado: códices, ediciones únicas y colecciones especiales.</li>
			</ol>

			<h4 class="center">Capitulo V</h4>
            <h5 class="center">DEL PERSONAL</h5><br>


			<p align="justify"><em>Artículo 23°.</em>De conformidad con el artículo 27 del Reglamento General del Sistema Bibliotecario de la UNAM, el responsable de la biblioteca debe ser un profesional de la Bibliotecología o tener preparación equivalente.</p><br>
			<p align="justify"><em>Artículo 24°.</em>Para mejorar la calidad de los servicios bibliotecarios el responsable de la biblioteca procurará la formación, capacitación y actualización del personal de la biblioteca. </p><br>
			<p align="justify"><em>Artículo 25°.</em>El personal de la biblioteca debe cumplir el presente Reglamento y colaborar en la supervisión de la observancia de las disposiciones que del mismo se deriven y además:</p><br>
			<ol>
				<li>Difundir entre los usuarios el Reglamento interno de la biblioteca </li>
				<li>Atender al usuario con respeto y eficiencia.</li>
				<li>Proponer políticas, procedimientos y acciones para mejorar la calidad de los servicios.</li>
				<li>Notificar al usuario de las devoluciones y renovaciones del material en caso de fecha vencida o por vencerse.</li>
			</ol>


			<h4 class="center">Capitulo VI</h4>
            <h5 class="center">DE LAS SANCIONES</h5><br>

			<p align="justify"><em>Artículo 26°.</em>Conforme al artículo 32 del Reglamento General del Sistema Bibliotecario de la UNAM, la destrucción, mutilación o desaparición del patrimonio documental es considerado como causa grave de responsabilidad.</p>
			<p align="justify">Se aplicarán las sanciones correspondientes en los siguientes puntos:</p><br>

			<ol>
				<li>En caso de pérdida del material documental, los usuarios deberán reponerlo con un nuevo ejemplar original. Si el título está agotado, se sustituirá por otro que designe el responsable de la biblioteca.</li>
				<li>Cuando el material prestado llegue a sufrir algún daño por parte del usuario, éste deberá pagar el importe de su restauración.</li>
				<li>Los usuarios externos que habiendo solicitado el material para consulta en sala de lectura o para fotocopiado y no lo entreguen el mismo día, se harán acreedores a la suspensión de los servicios bibliotecarios durante 10 días hábiles. En caso de reincidencia, la suspensión será definitiva.</li>
				<li>Por incumplimiento a lo estipulado para el préstamo interbibliotecario el servicio será suspendido hasta la devolución del material y se deberá cubrir el importe por concepto de multa de dos pesos diarios por cada libro.</li>
			</ol>


			<h4 class="center">TRANSITORIOS</h4>

            <p align="justify"><em>PRIMERO.</em>El presente Reglamento deroga todas las disposiciones que en materia bibliotecaria existan en esta dependencia y se opongan a las contenidas en él.</p>

            <p align="justify"><em>SEGUNDO.</em>El presente Reglamento entrará en vigor, una vez que haya sido aprobado por la Comisión de Biblioteca y un día después de su aprobación por el Consejo Interno.</p>

            <p align="justify"><em>TERCERO.</em>Cualquier situación no prevista en este Reglamento, se ajustará a las disposiciones de la Comisión de Biblioteca.</p>


			<p align="justify">Aprobado por el Consejo Interno en su tercera sesión ordinaria celebrada el día 1º de marzo de 2005.</p>

