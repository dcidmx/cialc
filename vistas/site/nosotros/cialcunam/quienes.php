												El Centro de Investigaciones sobre América Latina y el Caribe CIALC (antes Centro Coordinador y Difusor de Estudios Latinoamericanos (CCyDEL) es el espacio académico que la Universidad Nacional Autónoma de México creó para realizar su
												vocación latinoamericanista. En el CIALC se ha procurado vincular el conocimiento de la realidad mexicana con el de la latinoamericana y caribeña. Este interés por América Latina es el resultado de la comprensión de que ésta es una
												región con marcadas semejanzas y paralelismos; que ha nacido de orígenes comunes y que es el producto de procesos históricos convergentes, por medio de los cuales, sin que medie intencionalidad común, se han desarrollado respuestas
												afines a problemas paralelos. Por ello, con el estudio sobre la región, el CIALC busca contribuir a la creación de un mayor y mejor conocimiento sobre América Latina y el Caribe, siempre desde una perspectiva propia y crítica, que
												ayude a construir una mejor comprensión de su realidad.
												</p>
												<p align="justify">
												El CIALC se encarga de la creación, implementación y realización de diversos proyectos interdisciplinarios de investigación sobre el área/tema de su estudio. Este Centro promueve y difunde los resultados de esos estudios, y de otros trabajos relevantes
												sobre la materia, a través de diversas publicaciones periódicas, monográficas, individuales y colectivas, así como de la participación en la organización de simposia, congresos y conferencias.
												</p>
												<p align="justify">Tanto en su origen como en su forma de funcionamiento, el CIALC ha dejado clara su vocación de liderazgo al impulsar la cooperación con diversas instituciones nacionales e internacionales que trabajan en los estudios latinoamericanos,
												el intercambio de conocimientos, la realización de planes conjuntos, el intercambio académico, y la participación en, y la promoción de, reuniones nacionales e internacionales que aborden la problemática latinoamericana.</p>

