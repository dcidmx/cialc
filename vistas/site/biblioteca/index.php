	          <h4>Biblioteca</h4>
	        </div>

	        <div class="divider-fullwidth bg-gray-light offset-top-15"></div>
	        <div class="post-body">
	          <p>La Biblioteca “Simón Bolívar” surgió con la creación del CIALC, para ser una parte fundamental de apoyo para las tareas de investigación, docencia y extensión que el centro realiza.</p>
	          <p>Actualmente posee un acervo de más de 22, 000 volúmenes de libros y 300 títulos de publicaciones periódicas; así mismo, ofrece una variedad de servicios especializados, por lo que constituye una referencia obligada para el conocimiento y la realización de los estudios latinoamericanos.</p>
	          <p>El lector encontrará, desde las obras fundamentales del siglo XVI al XIX, en formato digitalizado, pasando por lo más selecto del siglo XX y lo publicado recientemente sobre la historia, literatura, cultura y el desarrollo de las ideas y el pensamiento latinoamericano.</p>
	          <p>Mantiene relaciones de préstamo e intercambio de publicaciones, con instituciones afines, 60 convenios de préstamo interbibliotecario y 85 de canje, tanto nacionales como extranjeras.</p>
	          <p>El uso de las modernas tecnologías de la información ha permitido ampliar su radio de acción y ofrecer sus servicios a un mayor número de interesados en el pensamiento de América Latina y el Caribe y tener acceso a otras colecciones relevantes sobre la materia.</p>
